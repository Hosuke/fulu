export type 符類 = '入山符' | '真形圖' | '複文' | '鎮宅符' | '雷法符' | '籙' | '其他';
export type 信級 = '已核' | '待核';

export interface 出典條 { 典: string; 據?: string; 引?: string; 註?: string; 卷?: string }

export interface 符條 {
  id: string;                 // ASCII 符號（庫鍵）
  名: string;                 // 符名
  類: 符類;
  式: string | null;          // 結構之述（符頭/身/膽/腳各何物）——查不實置 null
  用: string | null;          // 用途（典載之用，非勸用——研究陳列）
  出典: 出典條[];
  異說?: string;
  信: 信級;
  幅比?: number;              // 高/寬。闕則豎條之基（紙.高/紙.寬＝3）；真形圖方幅等異式並陳於此
  據圖?: string;              // 所據圖版檔名（參攷-local/ 內，gitignored——記名以存判準）
}

