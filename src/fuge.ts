// ─────────────────────────────────────────────────────────────────────────────
// 符格 · Fuge —— 符籙紙段之律（本庫之「量度」）
// 符無 T1419 之經，段比例自考據立（docs/考據/ 逐卷繫源）；暫定候典之值
// 以〔候典〕注之，考實乃定。誦戒：載入斷言段界遞增且盡於紙末。
// ─────────────────────────────────────────────────────────────────────────────
export const 紙 = {
  寬: 24,          // 一符之寬（指單位，承粉本格語）
  高: 72,          // 寬一高三之基〔候典——黃紙硃書通行比，異式並陳於 fupu〕
} as const;

// 縱段四分（z 自紙頂 0 至紙末 高）——段界〔候典〕
export const 段 = {
  符頭末: 12,      // 0–12：符頭（三清三勾／敕令二字位）
  符身末: 44,      // 12–44：符身（雲篆・複文・諱字疊書）
  符膽末: 56,      // 44–56：符膽（密核）
  符腳末: 72,      // 56–72：符腳（叉腳押煞）
} as const;

const 序 = [0, 段.符頭末, 段.符身末, 段.符膽末, 段.符腳末];
for (let i = 1; i < 序.length; i++) {
  if (!(序[i] > 序[i - 1])) throw new Error(`符格誦戒破：段界不遞增（${序[i - 1]}→${序[i]}）`);
}
if (段.符腳末 !== 紙.高) throw new Error('符格誦戒破：末段不盡於紙末');
