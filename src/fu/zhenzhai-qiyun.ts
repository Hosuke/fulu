// 鎮宅靈符・厭除炁運陰陽不和 · 親筆（p180 下段右二）
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p180_題序璇璣八卦圖.jpg（下段右二）
// 判準：符頭（桿二右端圈・人字圈鏈二重）＋金形（亼干橫）＋滿幅桁＋大框（內
// 目箱・寬目帶・亀曲對）＋雙長足外撇（摹形從簡）。錨自 8 格裁圖
// （宅180_三符 qiyun）：X=(c-4.0)×2.2、Z=(r-2.2)×2.2+2。幅比 2.2。
// 逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅炁運: 符筆 = (bi) => {
  const u = bi.u, k = 2.2;
  const X = (c: number) => (c - 4.0) * k;
  const Z = (r: number) => (r - 2.2) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.3) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿二（右端圈）
  線(p => p.M(X(0.35), Z(2.65)).L(X(6.6), Z(2.55))); 圈(7.0, 2.55);
  線(p => p.M(X(0.45), Z(4.05)).L(X(6.5), Z(3.95))); 圈(6.9, 3.95);
  // 人字圈鏈二重
  圈(3.2, 4.9);
  線(p => p.M(X(2.85), Z(5.05)).L(X(0.45), Z(5.95)));
  線(p => p.M(X(3.55), Z(5.0)).L(X(6.45), Z(5.5))); 圈(6.8, 5.55);
  圈(3.2, 6.1);
  線(p => p.M(X(2.85), Z(6.25)).L(X(0.5), Z(7.1)));
  線(p => p.M(X(3.55), Z(6.2)).L(X(6.45), Z(6.7))); 圈(6.8, 6.75);
  // 金形（亼干橫——摹形）
  出(p => p.M(X(3.35), Z(7.2)).C(X(2.7), Z(7.6), X(2.0), Z(7.95), X(1.3), Z(8.2)), 0.36);
  出(p => p.M(X(3.45), Z(7.2)).C(X(4.1), Z(7.6), X(4.8), Z(7.95), X(5.5), Z(8.2)), 0.36);
  線(p => p.M(X(3.4), Z(7.4)).L(X(3.38), Z(8.75)), 0.38);
  線(p => p.M(X(1.5), Z(8.15)).L(X(5.3), Z(8.1)), 0.34);
  // 滿幅桁
  線(p => p.M(X(0.35), Z(9.0)).L(X(7.0), Z(8.92)), 0.44);

  // 大框（左右長柱）
  線(p => p.M(X(0.55), Z(9.45)).L(X(0.5), Z(21.5)), 0.4);
  出(p => p.M(X(6.9), Z(9.4)).L(X(6.87), Z(20.2))
    .C(X(6.86), Z(21.3), X(6.95), Z(22.3), X(7.15), Z(23.1)), 0.4);
  // 內目箱＋右內豎
  線(p => p.M(X(1.0), Z(9.9)).L(X(1.0), Z(12.0)).L(X(4.4), Z(11.95))
    .L(X(4.4), Z(9.85)).L(X(0.95), Z(9.9)));
  線(p => p.M(X(1.1), Z(10.95)).L(X(4.3), Z(10.9)), 0.32);
  線(p => p.M(X(5.6), Z(9.9)).L(X(5.58), Z(11.95)), 0.36);
  // 寬目帶（二桁三格）
  線(p => p.M(X(0.75), Z(12.5)).L(X(0.75), Z(14.7)).L(X(6.5), Z(14.62))
    .L(X(6.5), Z(12.42)).L(X(0.7), Z(12.5)));
  線(p => p.M(X(0.85), Z(13.25)).L(X(6.4), Z(13.18)), 0.32);
  線(p => p.M(X(0.85), Z(13.98)).L(X(6.4), Z(13.91)), 0.32);
  // 亀曲對（雙 S 蜿蜒——摹形）
  const 亀 = (x0: number) => 線(p => p.M(X(x0 + 2.1), Z(15.2))
    .C(X(x0 + 0.4), Z(15.3), X(x0 + 0.2), Z(15.9), X(x0 + 1.0), Z(16.1))
    .C(X(x0 + 1.8), Z(16.3), X(x0 + 1.9), Z(16.9), X(x0 + 1.0), Z(17.05))
    .C(X(x0 + 0.2), Z(17.2), X(x0 + 0.1), Z(17.8), X(x0 + 0.9), Z(18.0))
    .C(X(x0 + 1.7), Z(18.2), X(x0 + 1.9), Z(18.7), X(x0 + 1.2), Z(18.85)), 0.38);
  亀(0.9); 亀(3.6);
  // 雙長足外撇
  出(p => p.M(X(1.5), Z(19.3))
    .C(X(1.45), Z(20.6), X(1.4), Z(21.8), X(1.2), Z(22.8)), 0.4);
  出(p => p.M(X(4.6), Z(19.2))
    .C(X(4.65), Z(20.4), X(4.75), Z(21.6), X(5.0), Z(22.6)), 0.4);
};
