// ─────────────────────────────────────────────────────────────────────────────
// 起雷符（道法會元卷九十四・雨頭十五類符・三） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3923_卷94十五類符雨頭一覽-p26.png（右起第三）
// 判準：雨字符頭通制（頂桁・冠框・內左三橫右三弧・中豎長垂・●墨點・左右
// 長腳）＋雷田框（內十・小橫二對）承干莖。符下咒音記位不錄。摹形從簡。
// 錨自 8 格裁圖（格94_五 qilei）：X=(c-3.75)×2.55、Z=(r-4.8)×2.55+1.0。
// 幅比 3。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 起雷符: 符筆 = (bi) => {
  const u = bi.u, k = 2.55;
  const X = (c: number) => (c - 3.75) * k;
  const Z = (r: number) => (r - 4.8) * k + 1.0;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.34) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.36) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 點 = (x: number, z: number, r = 0.4) =>
    bi.塊(p => p.M(X(x - r), Z(z)).C(X(x - r), Z(z - r * 1.3), X(x + r), Z(z - r * 1.3), X(x + r), Z(z))
      .C(X(x + r), Z(z + r * 1.3), X(x - r), Z(z + r * 1.3), X(x - r), Z(z)).close());

  // 雨冠
  線(p => p.M(X(0.15), Z(5.05)).L(X(7.4), Z(4.98)), 0.84);
  出(p => p.M(X(0.35), Z(5.10)).L(X(0.33), Z(22.11)).C(X(0.32), Z(22.76), X(0.22), Z(23.24), X(0.08), Z(23.57)), 0.76);
  出(p => p.M(X(6.9), Z(5.10)).L(X(6.88), Z(19.68)).C(X(6.9), Z(20.33), X(7.0), Z(20.81), X(7.15), Z(21.14)), 0.76);
  線(p => p.M(X(0.9), Z(5.80)).L(X(2.6), Z(5.76)), 0.57);
  線(p => p.M(X(0.9), Z(6.40)).L(X(2.6), Z(6.36)), 0.57);
  線(p => p.M(X(0.9), Z(7.00)).L(X(2.6), Z(6.96)), 0.57);
  線(p => p.M(X(4.4), Z(5.80)).C(X(5.30), Z(5.62), X(5.80), Z(5.70), X(6.2), Z(5.85)), 0.57);
  線(p => p.M(X(4.4), Z(6.40)).C(X(5.30), Z(6.22), X(5.80), Z(6.30), X(6.2), Z(6.45)), 0.57);
  線(p => p.M(X(4.4), Z(7.00)).C(X(5.30), Z(6.82), X(5.80), Z(6.90), X(6.2), Z(7.05)), 0.57);
  線(p => p.M(X(3.7), Z(5.15)).L(X(3.68), Z(8.50)), 0.72);
  // 雷田（框・內十・小橫二對）
  線(p => p.M(X(1.7), Z(8.74)).L(X(1.7), Z(15.63)).L(X(5.8), Z(15.53)).L(X(5.8), Z(8.66)).L(X(1.65), Z(8.74)), 0.68);
  線(p => p.M(X(3.75), Z(8.82)).L(X(3.73), Z(15.55)), 0.65);
  線(p => p.M(X(1.8), Z(12.14)).L(X(5.7), Z(12.06)), 0.65);
  線(p => p.M(X(2.2), Z(10.36)).L(X(3.3), Z(10.31)), 0.57);
  線(p => p.M(X(4.2), Z(10.28)).L(X(5.3), Z(10.23)), 0.57);
  線(p => p.M(X(2.2), Z(13.77)).L(X(3.3), Z(13.72)), 0.57);
  線(p => p.M(X(4.2), Z(13.68)).L(X(5.3), Z(13.64)), 0.57);
  // 干承（橫・豎）
  線(p => p.M(X(2.4), Z(17.17)).L(X(5.2), Z(17.09)), 0.68);
  線(p => p.M(X(3.8), Z(15.63)).L(X(3.78), Z(19.84)), 0.72);
  點(3.85, 20.97, 0.42);
};
