// 天字式（散形四筆・北宋天心法） · 親筆
// 據圖版：參攷-local/敕令符頭/樣頁/（自 NLC-9797 冊4258 PDF p27 抽葉親見）
// （太上助國救民總真祕要 DZ1227，序 1116；卷二「散形」節首式）
// 判準：原刻拆畫圖——天字四筆逐繫：一（玉清上帝）・一（上清大帝）・
// 丿（太清大帝）・乀（玉皇上帝）；本繪依畫序親書楷體成字（同敕字式之裁）。
// 天剛符主治文（為國禳災…）記位 p26。幅比 1.2（z 0..28.8）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 天字式: 符筆 = (bi) => {
  const u = bi.u;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.0) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.1) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 一（玉清上帝）：上短橫
  橫(p => p.M(-5.6, 6.4).C(-2.4, 6.1, 1.6, 6.0, 5.4, 6.2));
  // 一（上清大帝）：下長橫
  橫(p => p.M(-8.6, 12.4).C(-4.0, 12.0, 3.0, 11.9, 8.4, 12.2));
  // 丿（太清大帝）：自上橫下貫左出
  出(p => p.M(0.5, 6.7).C(-0.5, 10.4, -2.0, 14.6, -4.3, 18.4)
    .C(-5.7, 20.8, -7.3, 22.9, -9.1, 24.6), 1.15);
  // 乀（玉皇上帝）：右捺重出
  出(p => p.M(-0.7, 13.3).C(1.7, 16.3, 4.3, 19.7, 6.6, 22.3)
    .C(7.5, 23.2, 8.5, 24.0, 9.3, 24.7), 1.25);
};
