// 判準：「第三發紫微令檄雷」之檄首——雨冠合體令首（如原刻所印，構近⿳雨斬貝）
// ＋卦記雙列。**此乃道藏原刻令首之摹，非坊傳「紫微諱」合體諱字**（名實之辨見
// docs/考據/紫微諱.md——坊傳諱形記位不摹之律不涉此印本令首）。
// 檄文祕傳不錄（記位 p30）。錨自 12 格裁圖（紫_lingshou.png）×2.2 入紙。幅比 1.15。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 紫微令首: 符筆 = (bi) => {
  const u = bi.u;
  const k = 2.2, cx0 = -4.0, z00 = 0.6;    // 裁圖格→紙：置中放大
  const X = (x: number) => (x - cx0) * k;
  const Z = (z: number) => (z - z00) * k + 1.5;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.85) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.9) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // ── 雨冠 ──
  橫(p => p.M(X(-5.1), Z(1.25)).L(X(-2.6), Z(1.2)));                 // 一
  橫(p => p.M(X(-5.25), Z(1.75)).L(X(-5.3), Z(2.85)), 0.75);         // 冖左垂
  橫(p => p.M(X(-5.25), Z(1.75)).L(X(-2.45), Z(1.7)).L(X(-2.5), Z(2.8)), 0.75); // 冖右折
  橫(p => p.M(X(-3.9), Z(1.7)).L(X(-3.9), Z(2.9)), 0.6);             // 中豎
  for (const [dx, dz] of [[-0.65, 0.45], [0.45, 0.4], [-0.65, 0.95], [0.45, 0.9]] as const)
    bi.dot(X(-3.9 + dx), Z(1.85 + dz), 0.16 * k);                    // 四點

  // ── 斬（車＋斤）──
  橫(p => p.M(X(-5.5), Z(3.15)).L(X(-3.9), Z(3.1)), 0.7);            // 車頂橫
  橫(p => p.M(X(-5.3), Z(3.5)).L(X(-4.15), Z(3.45)).L(X(-4.15), Z(4.25))
    .L(X(-5.3), Z(4.3)).L(X(-5.3), Z(3.5)), 0.6);                    // 車日
  橫(p => p.M(X(-4.7), Z(2.95)).L(X(-4.7), Z(4.75)), 0.6);           // 車豎
  橫(p => p.M(X(-5.55), Z(4.6)).L(X(-3.95), Z(4.55)), 0.6);          // 車底橫
  出(p => p.M(X(-3.5), Z(2.9)).C(X(-3.75), Z(3.35), X(-3.95), Z(3.7), X(-4.1), Z(3.95)), 0.65); // 斤撇一
  橫(p => p.M(X(-3.65), Z(3.55)).L(X(-2.4), Z(3.5)), 0.65);          // 斤橫
  橫(p => p.M(X(-3.1), Z(3.55)).L(X(-3.1), Z(4.85)), 0.65);          // 斤豎
  橫(p => p.M(X(-2.45), Z(3.55)).L(X(-2.45), Z(4.8)), 0.6);          // 斤右豎

  // ── 貝 ──
  橫(p => p.M(X(-4.85), Z(5.0)).L(X(-4.85), Z(6.35)), 0.65);         // 目左
  橫(p => p.M(X(-4.85), Z(5.05)).L(X(-3.35), Z(5.0)).L(X(-3.35), Z(6.3)), 0.65); // 目上右
  橫(p => p.M(X(-4.8), Z(5.45)).L(X(-3.45), Z(5.4)), 0.55);          // 目桁一
  橫(p => p.M(X(-4.8), Z(5.85)).L(X(-3.45), Z(5.8)), 0.55);          // 目桁二
  橫(p => p.M(X(-4.85), Z(6.3)).L(X(-3.35), Z(6.25)), 0.6);          // 目底
  出(p => p.M(X(-4.35), Z(6.45)).C(X(-4.6), Z(6.75), X(-4.85), Z(7.0), X(-5.1), Z(7.15)), 0.6); // 八左
  出(p => p.M(X(-3.85), Z(6.45)).C(X(-3.6), Z(6.75), X(-3.35), Z(7.0), X(-3.1), Z(7.15)), 0.6); // 八右

  // ── 卦記雙列（各三桁；右中桁斷為二）──
  for (const z of [8.65, 9.6, 10.55]) 橫(p => p.M(X(-5.75), Z(z)).L(X(-4.75), Z(z - 0.05)), 0.85);
  橫(p => p.M(X(-4.4), Z(8.6)).L(X(-3.4), Z(8.55)), 0.85);
  橫(p => p.M(X(-4.4), Z(9.55)).L(X(-4.0), Z(9.53)), 0.8);
  橫(p => p.M(X(-3.8), Z(9.52)).L(X(-3.4), Z(9.5)), 0.8);
  橫(p => p.M(X(-4.4), Z(10.5)).L(X(-3.4), Z(10.45)), 0.85);
};
