// ─────────────────────────────────────────────────────────────────────────────
// 道法會元卷十二・召雷飛符（二） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3843_卷12雲篆雷符-04.png（「二」標之符）
// 原文：「右符召某雷神——召雷飛符」（組凡三道，此其二）。
// 判準：雲篆屈曲——雙波桁（下桁左有並行短劃）→左S曲鏈（小環綴點・左曳長掃）
// ・右大乙環鏈（環滿開左・降曲左掃）→底波桁右深U鉤→末大掃波右出。
// 黃素朱書（卷十二旁注色制）→ 全符硃層；發用之制祕傳不錄（記位卷12）。
// 錨自 16 格裁圖（格_zhao2）：X=(col-8.2)×1.6、Z=(row-4.3)×1.6+3.5。幅比 1.3。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 召雷飛符二: 符筆 = (bi) => {
  const u = bi.u, k = 1.6;
  const X = (c: number) => (c - 8.2) * k;
  const Z = (r: number) => (r - 4.3) * k + 3.5;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.75) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  bi.硃(() => {
    // 雙波桁（波幅足）
    抖(p => p.M(X(3.6), Z(4.5))
      .C(X(4.3), Z(5.4), X(5.5), Z(5.5), X(6.8), Z(4.8))
      .C(X(8.2), Z(4.2), X(9.2), Z(5.0), X(10.6), Z(5.0))
      .C(X(11.6), Z(5.0), X(12.5), Z(4.9), X(13.2), Z(4.8)), 0.78);
    抖(p => p.M(X(3.6), Z(6.2))
      .C(X(4.5), Z(7.1), X(6.0), Z(7.0), X(7.2), Z(6.3))
      .C(X(8.5), Z(5.7), X(9.6), Z(6.7), X(12.0), Z(6.5)), 0.78);
    抖(p => p.M(X(4.2), Z(7.1)).C(X(4.9), Z(7.35), X(5.7), Z(7.35), X(6.3), Z(7.1)), 0.6);

    // 左S曲鏈：降曲・滿環・續降・左曳長掃（一氣連書）
    抖(p => p.M(X(5.0), Z(8.2))
      .C(X(4.1), Z(9.1), X(4.2), Z(10.1), X(5.3), Z(10.5))
      .C(X(6.4), Z(10.9), X(7.1), Z(10.1), X(6.4), Z(9.4))
      .C(X(5.8), Z(8.9), X(5.1), Z(9.5), X(5.4), Z(10.5))
      .C(X(5.7), Z(11.4), X(5.6), Z(12.0), X(4.9), Z(12.3))
      .C(X(4.2), Z(12.6), X(3.6), Z(12.7), X(3.2), Z(12.7)), 0.72);
    bi.dot(X(6.2), Z(10.0), 0.32);
    // 中短S
    抖(p => p.M(X(8.0), Z(9.3)).C(X(7.4), Z(10.1), X(7.5), Z(10.9), X(8.4), Z(11.5)), 0.66);

    // 右大乙環鏈：入弧・滿環開左・降曲・左掃長尾
    抖(p => p.M(X(9.0), Z(7.4))
      .C(X(10.5), Z(7.7), X(12.2), Z(8.0), X(12.9), Z(8.8))
      .C(X(13.6), Z(9.7), X(13.1), Z(10.6), X(12.0), Z(10.6))
      .C(X(11.0), Z(10.6), X(10.7), Z(9.7), X(11.4), Z(9.2)), 0.72);
    抖(p => p.M(X(12.1), Z(10.9))
      .C(X(12.6), Z(11.5), X(12.6), Z(12.1), X(11.9), Z(12.4))
      .C(X(10.8), Z(12.8), X(9.3), Z(12.7), X(8.1), Z(12.4)), 0.72);

    // 底波桁右深U鉤
    抖(p => p.M(X(3.4), Z(14.1))
      .C(X(4.8), Z(13.4), X(6.8), Z(13.4), X(8.2), Z(13.7))
      .C(X(9.5), Z(13.9), X(10.3), Z(14.4), X(10.3), Z(15.3))
      .C(X(10.3), Z(16.2), X(9.4), Z(16.6), X(8.6), Z(16.3))
      .C(X(8.2), Z(16.1), X(8.0), Z(15.8), X(8.1), Z(15.4)), 0.75);
    抖(p => p.M(X(11.8), Z(13.2)).C(X(11.2), Z(13.5), X(10.7), Z(13.7), X(10.3), Z(13.8)), 0.6);

    // 末大掃波右出
    抖(p => p.M(X(3.4), Z(17.7))
      .C(X(4.4), Z(16.7), X(5.6), Z(16.4), X(6.5), Z(17.0))
      .C(X(7.5), Z(17.7), X(8.3), Z(18.5), X(9.3), Z(19.0))
      .C(X(10.5), Z(19.5), X(11.4), Z(19.5), X(12.3), Z(19.3)), 0.78);
    抖(p => p.M(X(7.9), Z(18.0)).C(X(8.5), Z(18.6), X(9.0), Z(18.9), X(9.5), Z(19.1)), 0.6);
  });
};
