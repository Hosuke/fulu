// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭伏尸蛀煞鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p184_符葉四.jpg（下段右三）
// 判準：符頭（桿綴圈二・字符頭二列四行——「禍去福來／宅神魂魄」之形，
// 半可辨，摹形從簡以匣杙代）＋左密目梯（七箱）＋中杙＋右柱（目箱・
// 「子子」楷對・囧門箱疊）。錨自 12 格裁圖（宅184_C zhusha）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅蛀煞: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿綴圈二
  線(p => p.M(X(0.9), Z(1.1)).L(X(6.4), Z(1.0))); 圈(6.75, 1.0); 圈(6.8, 1.9);

  // 字符頭二列四行（摹形從簡：匣＋杙曲代半辨字形）
  const 字匣 = (cx: number, z0: number) => {
    線(p => p.M(X(cx - 1.1), Z(z0)).L(X(cx + 1.1), Z(z0 - 0.05)), 0.34);
    線(p => p.M(X(cx - 0.8), Z(z0 + 0.5)).C(X(cx - 0.4), Z(z0 + 0.9), X(cx + 0.3), Z(z0 + 0.9),
      X(cx + 0.8), Z(z0 + 0.5)), 0.32);
    線(p => p.M(X(cx), Z(z0 + 0.1)).L(X(cx - 0.02), Z(z0 + 1.2)), 0.32);
    線(p => p.M(X(cx - 0.7), Z(z0 + 1.4)).L(X(cx + 0.7), Z(z0 + 1.35)), 0.32);
  };
  for (const [cx, z0] of [[1.9, 1.9], [1.9, 3.4], [1.9, 4.9], [1.9, 6.4],
                          [4.9, 1.7], [4.9, 3.2], [4.9, 4.7], [4.9, 6.2]] as const)
    字匣(cx, z0);

  // 左密目梯（七箱）＋中杙
  線(p => p.M(X(0.7), Z(8.5)).L(X(0.7), Z(19.0)));
  線(p => p.M(X(3.1), Z(8.45)).L(X(3.1), Z(18.95)));
  for (let i = 0; i <= 7; i++)
    線(p => p.M(X(0.7), Z(8.5 + i * 1.5)).L(X(3.1), Z(8.45 + i * 1.5)));
  線(p => p.M(X(3.8), Z(8.6)).L(X(3.77), Z(18.8)), 0.4);

  // 右柱：目箱＋「子子」楷對＋囧門箱疊
  線(p => p.M(X(4.5), Z(8.5)).L(X(4.5), Z(10.4)).L(X(6.7), Z(10.35))
    .L(X(6.7), Z(8.45)).L(X(4.45), Z(8.5)));
  線(p => p.M(X(4.6), Z(9.45)).L(X(6.6), Z(9.42)), 0.32);
  // 子子（楷，摹形）
  const 子 = (cx: number) => {
    線(p => p.M(X(cx - 0.45), Z(11.0)).C(X(cx - 0.15), Z(11.1), X(cx + 0.2), Z(11.08), X(cx + 0.45), Z(10.95)), 0.34);
    出(p => p.M(X(cx), Z(11.05)).C(X(cx - 0.05), Z(11.4), X(cx - 0.05), Z(11.75), X(cx + 0.05), Z(12.05))
      .C(X(cx + 0.08), Z(12.25), X(cx - 0.1), Z(12.4), X(cx - 0.35), Z(12.35)), 0.36);
    線(p => p.M(X(cx - 0.55), Z(11.6)).L(X(cx + 0.55), Z(11.55)), 0.34);
  };
  子(5.0); 子(6.1);
  // 囧門箱疊
  線(p => p.M(X(4.5), Z(13.0)).L(X(4.5), Z(14.6)).L(X(6.7), Z(14.55))
    .L(X(6.7), Z(12.95)).L(X(4.45), Z(13.0)));
  線(p => p.M(X(5.0), Z(13.35)).C(X(5.2), Z(13.8), X(5.6), Z(13.9), X(5.9), Z(13.6)), 0.3);
  線(p => p.M(X(4.6), Z(14.15)).L(X(6.6), Z(14.1)), 0.3);
  線(p => p.M(X(4.5), Z(14.9)).L(X(4.5), Z(17.0)).L(X(6.7), Z(16.95))
    .L(X(6.7), Z(14.85)).L(X(4.45), Z(14.9)));
  線(p => p.M(X(5.6), Z(14.95)).L(X(5.58), Z(16.95)), 0.32);
  // 底門
  線(p => p.M(X(4.5), Z(17.6)).L(X(4.5), Z(19.0)));
  線(p => p.M(X(6.7), Z(17.55)).L(X(6.7), Z(18.95)));
  線(p => p.M(X(4.5), Z(17.65)).L(X(6.7), Z(17.6)), 0.34);
};
