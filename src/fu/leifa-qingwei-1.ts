// ─────────────────────────────────────────────────────────────────────────────
// 道法會元卷十二・清微令符（一） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3843_卷12雷霆聞命清微令符-07.png（「一」標）
// 原文：清微令符旁注「興方逺圓・青素朱書」→全符硃層；組凡三道，此其一。
// 判準：波挑入大環（O環右滿，尾出左下）→雙波桁（上短下長）。
// 幅比 1.1（z 0..26.4）。錨自 12 格裁圖（格_qw1）：X=(col-6.4)×1.9、
// Z=(row-3.7)×1.9+2。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 清微令符一: 符筆 = (bi) => {
  const u = bi.u, k = 1.9;
  const X = (c: number) => (c - 6.4) * k;
  const Z = (r: number) => (r - 3.7) * k + 2;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.7) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  bi.硃(() => {
    // 波挑入大環（尾出左下）
    抖(p => p.M(X(2.9), Z(6.6))
      .C(X(3.8), Z(6.1), X(4.7), Z(5.6), X(5.5), Z(5.5))
      .C(X(6.5), Z(4.1), X(8.4), Z(3.8), X(9.4), Z(4.4))
      .C(X(10.4), Z(5.1), X(10.1), Z(6.4), X(8.9), Z(7.2))
      .C(X(7.9), Z(7.8), X(6.9), Z(8.1), X(6.0), Z(7.5))
      .C(X(5.6), Z(7.2), X(5.5), Z(6.8), X(5.6), Z(6.4)), 0.74);
    // 雙波桁
    抖(p => p.M(X(4.2), Z(9.3))
      .C(X(5.6), Z(9.8), X(7.2), Z(9.9), X(8.4), Z(9.6))
      .C(X(8.9), Z(9.5), X(9.3), Z(9.5), X(9.6), Z(9.6)), 0.66);
    抖(p => p.M(X(3.2), Z(13.3))
      .C(X(4.6), Z(13.0), X(6.0), Z(13.5), X(7.4), Z(13.6))
      .C(X(8.4), Z(13.7), X(9.3), Z(13.6), X(10.0), Z(13.5)), 0.66);
  });
};
