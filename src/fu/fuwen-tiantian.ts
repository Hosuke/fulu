// 判準：**二天縱疊**（上小下大），撇捺作波狀長曳——初繪誤作並立，親核圖版
// （天天域.png 放大）改縱疊（2026-07-12 自校二輪）。楷隸筆意，橫藏回、撇捺尖出。
// 幅比 1.3（z 0..31.2）。釋讀〔待核〕，名從構件形貌。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 複文天天: 符筆 = (bi) => {
  const u = bi.u;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.85) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 曳 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.0) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 上天（略小）：橫二道，撇捺波曳
  橫(p => p.M(-2.8, 4.4).C(-0.9, 4.15, 0.9, 4.1, 2.8, 4.1));
  橫(p => p.M(-3.9, 6.6).C(-1.3, 6.3, 1.3, 6.2, 3.9, 6.1), 0.9);
  曳(p => p.M(0.2, 6.4)
    .C(-0.9, 8.0, -1.4, 9.0, -2.6, 10.0)
    .C(-3.4, 10.7, -4.1, 11.1, -4.7, 11.3));
  曳(p => p.M(0.1, 6.5)
    .C(0.9, 8.2, 1.6, 9.2, 2.8, 10.1)
    .C(3.6, 10.7, 4.3, 11.0, 4.9, 11.2));

  // 下天（略大）：橫二道，撇捺波曳更長
  橫(p => p.M(-3.2, 13.8).C(-1.0, 13.5, 1.0, 13.4, 3.2, 13.4));
  橫(p => p.M(-4.4, 16.2).C(-1.5, 15.9, 1.5, 15.8, 4.4, 15.7), 0.92);
  曳(p => p.M(0.2, 16.0)
    .C(-1.0, 18.2, -1.6, 19.6, -3.0, 21.0)
    .C(-4.0, 22.0, -5.0, 22.7, -5.8, 23.1));
  曳(p => p.M(0.1, 16.1)
    .C(1.1, 18.4, 1.8, 19.8, 3.2, 21.2)
    .C(4.2, 22.2, 5.2, 22.8, 6.0, 23.2));
};
