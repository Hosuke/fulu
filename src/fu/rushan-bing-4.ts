// ─────────────────────────────────────────────────────────────────────────────
// 老君入山符・丙四（篇末組第四符） · 親筆
// 據圖版：參攷-local/入山符/抱朴子登涉_佩帶符_p057.png（檔名沿誤，歸組
// 已訂：篇末丙組——考據卷六（乙）；欽定四庫全書繕本，PD）
// 判準：頂列星點六（二＋四）→框格連珠（框桁三・角珠・斜貫珠線・底桁綴
// 珠四）→疊書符身（左：尸弧桁列冂垂三豎；右：玉桁雙珠・葉眼垂環・田箱・
// 弓曲）→左右長垂界欄外撇。丙組材色未言，不施硃。幅比 1.5（z 0..36）。
// 錨：X=(col-12)×1.05、Z=(row-1.2)×1.05（格_bing4）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 入山符丙四: 符筆 = (bi) => {
  const u = bi.u;
  const k = 1.05;
  const X = (c: number) => (c - 12) * k;
  const Z = (r: number) => (r - 1.2) * k;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 星 = (c: number, r: number, 半 = 0.55) => bi.dot(X(c), Z(r), 半);

  // 左右長垂界欄
  出(p => p.M(X(6.2), Z(1.9)).C(X(6.0), Z(11.1), X(6.2), Z(21.0), X(5.9), Z(28.5))
    .C(X(5.8), Z(30.3), X(5.3), Z(31.6), X(4.6), Z(32.5)), 0.65);
  出(p => p.M(X(17.8), Z(1.8)).C(X(18.0), Z(10.6), X(17.8), Z(20.5), X(18.1), Z(28.0))
    .C(X(18.2), Z(29.8), X(18.7), Z(31.1), X(19.4), Z(32.0)), 0.65);

  // 頂列星點六
  星(9.9, 1.9); 星(14.4, 1.8);
  星(8.3, 4.3); 星(11.3, 4.4); 星(13.9, 4.2); 星(16.3, 4.3);

  // 框格連珠
  線(p => p.M(X(8.0), Z(6.0)).L(X(16.2), Z(5.8)), 0.58);
  出(p => p.M(X(8.0), Z(6.1)).C(X(7.7), Z(6.9), X(7.7), Z(7.7), X(8.0), Z(8.4)), 0.5);
  出(p => p.M(X(16.2), Z(5.9)).C(X(16.5), Z(6.7), X(16.5), Z(7.5), X(16.2), Z(8.2)), 0.5);
  線(p => p.M(X(8.9), Z(7.3)).L(X(15.7), Z(7.1)), 0.5);
  線(p => p.M(X(9.0), Z(8.3)).L(X(15.6), Z(8.1)), 0.5);
  星(8.1, 9.6); 星(16.2, 9.9);
  線(p => p.M(X(8.1), Z(10.1)).L(X(8.1), Z(14.2)), 0.5);
  線(p => p.M(X(16.2), Z(10.4)).L(X(16.2), Z(11.5)), 0.5);
  星(16.2, 12.1, 0.5);
  星(11.6, 10.8, 0.5);
  線(p => p.M(X(11.3), Z(11.2)).L(X(8.6), Z(13.9)), 0.45);
  線(p => p.M(X(8.2), Z(14.55)).L(X(16.3), Z(14.3)), 0.55);
  星(8.3, 14.6, 0.5); 星(11.1, 14.5, 0.5); 星(13.9, 14.4, 0.5); 星(16.3, 14.3, 0.5);

  // ── 疊書符身・左 ──
  線(p => p.M(X(7.7), Z(16.1)).C(X(9.0), Z(15.7), X(10.4), Z(15.6), X(11.5), Z(15.9)), 0.55);
  出(p => p.M(X(7.9), Z(18.7)).C(X(7.6), Z(20.2), X(7.6), Z(21.7), X(7.9), Z(23.1)), 0.5);
  for (const z of [18.5, 19.9, 21.3])
    線(p => p.M(X(8.1), Z(z)).L(X(12.0), Z(z - 0.3)), 0.55);
  線(p => p.M(X(8.1), Z(24.7)).L(X(12.6), Z(24.4)), 0.6);
  線(p => p.M(X(8.9), Z(26.3)).L(X(12.4), Z(26.0)), 0.55);
  線(p => p.M(X(9.2), Z(26.5)).L(X(9.2), Z(31.8)), 0.5);
  線(p => p.M(X(10.3), Z(26.5)).L(X(10.3), Z(32.0)), 0.5);
  線(p => p.M(X(11.4), Z(26.4)).L(X(11.4), Z(31.6)), 0.5);

  // ── 疊書符身・右 ──
  線(p => p.M(X(13.6), Z(15.9)).L(X(16.9), Z(15.7)), 0.55);
  線(p => p.M(X(15.1), Z(16.0)).L(X(15.05), Z(18.9)), 0.5);
  星(13.9, 19.4, 0.5); 星(15.3, 19.3, 0.5);
  線(p => p.M(X(13.9), Z(19.9)).L(X(13.9), Z(23.5)), 0.45);
  // 葉眼垂環
  線(p => p.M(X(16.4), Z(20.1))
    .C(X(15.8), Z(21.2), X(15.8), Z(22.3), X(16.4), Z(23.3))
    .C(X(17.0), Z(22.3), X(17.0), Z(21.2), X(16.4), Z(20.1)), 0.48);
  出(p => p.M(X(16.4), Z(23.3)).C(X(16.1), Z(23.9), X(16.3), Z(24.4), X(16.8), Z(24.5)), 0.45);
  // 田箱
  線(p => p.M(X(13.3), Z(25.8)).L(X(13.3), Z(28.7)), 0.5);
  線(p => p.M(X(13.3), Z(25.85)).L(X(17.3), Z(25.6)).L(X(17.35), Z(28.5)), 0.5);
  線(p => p.M(X(13.3), Z(28.7)).L(X(17.35), Z(28.5)), 0.5);
  線(p => p.M(X(13.4), Z(27.3)).L(X(17.3), Z(27.1)), 0.45);
  線(p => p.M(X(15.3), Z(25.8)).L(X(15.25), Z(28.6)), 0.45);
  // 弓曲
  出(p => p.M(X(15.3), Z(29.3)).C(X(14.8), Z(30.1), X(15.2), Z(30.9), X(16.0), Z(30.9))
    .C(X(16.6), Z(30.9), X(16.8), Z(31.4), X(16.5), Z(31.9)), 0.5);
};
