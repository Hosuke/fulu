// 鎮宅靈符・厭猪猫犬等自食子恠 · 親筆（p180 下段右三）
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p180_題序璇璣八卦圖.jpg（下段右三）
// 判準：密桁符頭（桁六・右上斜撇・右緣圈列三）＋中帶嵌十乂＋目箱對＋寬目帶＋
// 亀曲對垂尾（摹形從簡）。錨自 8 格裁圖（宅180_三符 zhumao）：
// X=(c-4.0)×2.4、Z=(r-2.0)×2.4+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅猪猫: 符筆 = (bi) => {
  const u = bi.u, k = 2.4;
  const X = (c: number) => (c - 4.0) * k;
  const Z = (r: number) => (r - 2.0) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.3) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：密桁六
  for (const [i, z] of [2.3, 3.2, 4.1, 5.0, 5.9, 6.8].entries())
    線(p => p.M(X(0.9), Z(z)).L(X(6.5), Z(z - 0.06)), 0.4 - (i % 2) * 0.02);
  // 右上斜撇
  出(p => p.M(X(7.3), Z(2.1)).C(X(7.0), Z(2.5), X(6.8), Z(2.9), X(6.7), Z(3.2)), 0.4);
  // 右緣圈列三
  圈(7.3, 2.4); 圈(7.35, 3.5); 圈(7.3, 4.6);

  // 中帶嵌十乂（摹形）
  線(p => p.M(X(0.9), Z(7.9)).L(X(0.9), Z(9.7)).L(X(6.7), Z(9.62))
    .L(X(6.7), Z(7.82)).L(X(0.85), Z(7.9)));
  線(p => p.M(X(3.8), Z(7.5)).L(X(3.78), Z(9.95)), 0.38);
  出(p => p.M(X(4.3), Z(8.2)).L(X(5.3), Z(9.3)), 0.34);
  出(p => p.M(X(5.3), Z(8.2)).L(X(4.3), Z(9.3)), 0.34);

  // 目箱對
  const 目 = (x0: number, x1: number, z0: number, z1: number, 桁 = 1) => {
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z1)).L(X(x1), Z(z1 - 0.04))
      .L(X(x1), Z(z0 - 0.04)).L(X(x0 - 0.04), Z(z0)));
    for (let i = 1; i <= 桁; i++)
      線(p => p.M(X(x0 + 0.1), Z(z0 + (z1 - z0) * i / (桁 + 1)))
        .L(X(x1 - 0.1), Z(z0 + (z1 - z0) * i / (桁 + 1) - 0.03)), 0.32);
  };
  目(0.9, 3.3, 10.2, 12.8, 2);
  目(3.9, 6.6, 10.2, 12.8, 1);
  線(p => p.M(X(5.25), Z(10.3)).L(X(5.23), Z(12.7)), 0.32);

  // 寬目帶（二桁三格）
  目(0.9, 6.7, 13.2, 15.8, 2);

  // 亀曲對垂尾（雙 S 蜿蜒——摹形）
  const 亀 = (x0: number) => 線(p => p.M(X(x0 + 2.0), Z(16.3))
    .C(X(x0 + 0.3), Z(16.4), X(x0 + 0.1), Z(16.95), X(x0 + 0.9), Z(17.15))
    .C(X(x0 + 1.7), Z(17.35), X(x0 + 1.8), Z(17.9), X(x0 + 0.9), Z(18.05))
    .C(X(x0 + 0.1), Z(18.2), X(x0 + 0.1), Z(18.75), X(x0 + 0.9), Z(18.95)), 0.38);
  亀(1.0); 亀(3.9);
  // 垂尾
  出(p => p.M(X(1.85), Z(19.0))
    .C(X(1.5), Z(19.8), X(1.2), Z(20.4), X(0.8), Z(20.9)), 0.4);
  出(p => p.M(X(4.75), Z(19.0))
    .C(X(4.5), Z(19.8), X(4.5), Z(20.4), X(4.7), Z(20.8)), 0.4);
};
