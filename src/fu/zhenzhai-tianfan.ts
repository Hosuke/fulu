// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭天返地覆馬不生兒之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p185_符葉五.jpg（上段右二）
// 判準：星紋符頭（桿二・圈柱三行）＋雙目梯（各七箱）夾中柱大力早干次口化
// 字形疊（摹形從簡）。錨自 12 格裁圖（宅185_A tianfan）：X=(c-3.95)×2.7、
// Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅天返: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿二＋圈柱三行
  線(p => p.M(X(1.0), Z(1.4)).L(X(6.3), Z(1.3))); 圈(0.7, 1.4); 圈(6.6, 1.3);
  線(p => p.M(X(1.0), Z(2.4)).L(X(6.3), Z(2.3))); 圈(0.7, 2.4); 圈(6.6, 2.3);
  for (const [x, z] of [[0.7, 3.4], [2.5, 3.5], [4.2, 3.4], [6.5, 3.3],
                        [0.7, 4.5], [3.3, 4.6], [5.0, 4.5], [6.5, 4.4],
                        [0.7, 5.6], [2.6, 5.7], [6.5, 5.5]] as const) 圈(x, z);

  // 雙目梯（各七箱）
  const 梯 = (x0: number, x1: number) => {
    線(p => p.M(X(x0), Z(6.6)).L(X(x0), Z(18.9)));
    線(p => p.M(X(x1), Z(6.55)).L(X(x1), Z(18.85)));
    for (let i = 0; i <= 7; i++)
      線(p => p.M(X(x0), Z(6.6 + i * 1.757)).L(X(x1), Z(6.55 + i * 1.757)));
  };
  梯(0.9, 3.2); 梯(4.7, 6.9);

  // 中柱：大力早干次口化字形疊（摹形從簡）
  線(p => p.M(X(3.5), Z(7.0)).L(X(4.5), Z(6.97)), 0.34);
  出(p => p.M(X(4.0), Z(6.7)).C(X(3.8), Z(7.4), X(3.6), Z(7.9), X(3.4), Z(8.2)), 0.34);
  出(p => p.M(X(4.0), Z(7.3)).C(X(4.2), Z(7.8), X(4.4), Z(8.2), X(4.6), Z(8.5)), 0.34);
  線(p => p.M(X(3.6), Z(9.0)).L(X(4.4), Z(8.97)), 0.32);
  出(p => p.M(X(4.2), Z(8.8)).C(X(4.1), Z(9.5), X(3.9), Z(10.0), X(3.6), Z(10.3)), 0.34);
  線(p => p.M(X(3.6), Z(10.9)).L(X(3.6), Z(12.0)).L(X(4.5), Z(11.97))
    .L(X(4.5), Z(10.87)).L(X(3.55), Z(10.9)), 0.32);
  線(p => p.M(X(4.05), Z(12.0)).L(X(4.03), Z(12.6)), 0.3);
  線(p => p.M(X(3.5), Z(13.1)).L(X(4.6), Z(13.07)), 0.34);
  線(p => p.M(X(4.05), Z(12.8)).L(X(4.03), Z(14.0)), 0.32);
  出(p => p.M(X(3.9), Z(14.4)).C(X(3.7), Z(15.0), X(3.6), Z(15.5), X(3.6), Z(15.9)), 0.32);
  出(p => p.M(X(4.1), Z(14.5)).C(X(4.3), Z(15.1), X(4.4), Z(15.6), X(4.4), Z(16.0)), 0.32);
  線(p => p.M(X(3.6), Z(16.5)).L(X(3.6), Z(17.4)).L(X(4.5), Z(17.37))
    .L(X(4.5), Z(16.45)).L(X(3.55), Z(16.5)), 0.32);
  出(p => p.M(X(3.7), Z(17.9)).C(X(3.9), Z(18.4), X(4.2), Z(18.7), X(4.6), Z(18.8)), 0.32);
};
