// ─────────────────────────────────────────────────────────────────────────────
// 道法會元卷十二・召雷飛符（一） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3843_卷12雲篆雷符-04.png（右起首符，「一」）
// 原文：「右符召某雷神——召雷飛符」（組凡三道，此其一；餘二異式並陳於譜註）。
// 判準：雲篆屈曲成文——左長曳・右弦月・雙翼波・中大渦（螺旋一圈半）・下二乁尾。
// 錨自 24 指格網（雷_zhaolei.png），仿射 k=1.4, cx0=-1, z00=6。幅比 1.75。
// 黃素朱書（卷十二旁注色制）→ 全符硃層；材式發用之制祕傳不錄（記位卷12）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 召雷飛符: 符筆 = (bi) => {
  const u = bi.u, k = 1.4, cx0 = -1, z00 = 6.0;
  const X = (x: number) => (x - cx0) * k;
  const Z = (z: number) => (z - z00) * k + 2;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.62) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  bi.硃(() => {
    // 左上長曳：自尖入，波折而下，止於小螺
    抖(p => p.M(X(-8.2), Z(6.6))
      .C(X(-6.8), Z(8.0), X(-5.6), Z(9.6), X(-4.8), Z(11.2))
      .C(X(-4.2), Z(12.4), X(-4.9), Z(13.2), X(-4.3), Z(14.2))
      .C(X(-3.6), Z(15.2), X(-3.2), Z(16.0), X(-3.9), Z(16.8))
      .C(X(-4.5), Z(17.4), X(-5.2), Z(17.0), X(-4.9), Z(16.3)));
    // 右上弦月：右肩下弧（中有版斷，一氣書之）
    抖(p => p.M(X(-0.5), Z(6.3))
      .C(X(1.8), Z(7.2), X(3.6), Z(9.2), X(4.4), Z(11.4))
      .C(X(4.8), Z(12.6), X(4.4), Z(13.4), X(3.2), Z(13.8))
      .C(X(2.0), Z(14.2), X(1.0), Z(14.0), X(0.6), Z(13.5)));
    // 雙翼波：右展二道
    抖(p => p.M(X(0.8), Z(14.7))
      .C(X(2.4), Z(14.3), X(4.2), Z(14.6), X(5.4), Z(15.3))
      .C(X(6.2), Z(15.8), X(6.6), Z(16.3), X(6.8), Z(16.8)), 0.7);
    抖(p => p.M(X(1.1), Z(16.3))
      .C(X(2.8), Z(16.2), X(4.4), Z(16.9), X(5.6), Z(17.8))
      .C(X(6.1), Z(18.2), X(6.4), Z(18.6), X(6.5), Z(19.0)), 0.7);
    // 左中垂波：長腿下走
    抖(p => p.M(X(-5.6), Z(14.3))
      .C(X(-6.2), Z(15.4), X(-5.3), Z(16.4), X(-6.1), Z(17.6))
      .C(X(-6.7), Z(18.6), X(-5.6), Z(19.6), X(-6.0), Z(20.8))
      .C(X(-6.3), Z(21.8), X(-5.2), Z(22.4), X(-5.6), Z(23.6))
      .C(X(-5.9), Z(24.8), X(-6.2), Z(25.8), X(-6.4), Z(26.8)));
    // 中大渦：內curl起，順旋圈半，出尾左下（9 形之勢）
    抖(p => p.M(X(1.2), Z(21.8))
      .C(X(2.2), Z(21.2), X(3.0), Z(21.9), X(2.6), Z(22.8))
      .C(X(2.0), Z(24.0), X(0.4), Z(23.9), X(0.0), Z(22.6))
      .C(X(-0.4), Z(20.9), X(1.2), Z(19.3), X(3.0), Z(19.5))
      .C(X(4.8), Z(19.7), X(5.6), Z(21.4), X(5.2), Z(23.2))
      .C(X(4.8), Z(25.0), X(3.0), Z(26.0), X(1.2), Z(25.9))
      .C(X(0.4), Z(25.9), X(-0.3), Z(25.9), X(-0.8), Z(26.1)), 0.7);
    // 下左乁尾：波肩右挑而長曳左下
    抖(p => p.M(X(-4.6), Z(28.4))
      .C(X(-3.0), Z(28.3), X(-1.6), Z(28.8), X(-0.6), Z(29.3))
      .C(X(-2.2), Z(30.2), X(-3.8), Z(31.8), X(-4.9), Z(33.4)));
    // 下右乁尾
    抖(p => p.M(X(0.9), Z(28.2))
      .C(X(2.2), Z(28.4), X(3.2), Z(28.9), X(3.7), Z(29.5))
      .C(X(4.6), Z(30.6), X(5.6), Z(31.6), X(6.5), Z(32.4)));
  });
};
