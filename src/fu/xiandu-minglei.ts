// 玉清大梵名雷玉符（紫微仙都九符・二） · 親筆
// 據圖版：參攷-local/紫微諱/樣頁/樣_94_雷霆玉經紫微仙都九符鳴雷起雲符-p51.png
// （左圖；原刻題「玉清大梵名雷玉符」——8x 親核「名」字確鑿；經文名目作「召雷」，
// 異文並陳〔待核〕；NLC 檔名「鳴雷」乃今人擬名，不從。摺＝橫程之數。）
// 判準：密摺蟠曲雲篆——頭三疊帶（左十摺綴波豎・中八摺・右五摺綴回結）＋
// 對帶承波線＋厂臂抱密摺＋滿幅帶＋跳莖對帶＋厂冂雙臂抱疊帶夾中莖＋
// 大形帶（長莖人腳）＋對疊帶（綴環結）＋底帶己足。摺帶連綿摹形從簡。
// 錨自 10 格裁圖（格_minglei）：X=(c-4.95)×2.4、Z=(r-0.35)×2.4+1.2。幅比 2.5。
// 逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 名雷玉符: 符筆 = (bi) => {
  const u = bi.u, k = 2.4;
  const X = (c: number) => (c - 4.95) * k;
  const Z = (r: number) => (r - 0.35) * k + 1.2;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.34) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.34) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  // 摺帶：連綿切返 n 摺自 rT 至 rB（距＝跨距/(n-1)；左起＝首摺自左端行右）
  const 摺 = (x0: number, x1: number, rT: number, rB: number, n: number, 左起 = true, 寬 = 0.4) =>
    抖(pa => {
      const p = n > 1 ? (rB - rT) / (n - 1) : 0;
      let dir = 左起 ? 1 : -1, y = rT;
      pa.M(X(dir > 0 ? x0 : x1), Z(y));
      for (let i = 0; i < n; i++) {
        const xe = dir > 0 ? x1 : x0;
        if (i < n - 1) {
          pa.L(X(xe - dir * p * 0.35), Z(y));
          pa.C(X(xe + dir * p * 1.05), Z(y - p * 0.05), X(xe + dir * p * 1.05), Z(y + p * 1.05), X(xe - dir * p * 0.35), Z(y + p));
          y += p; dir = -dir;
        } else pa.L(X(xe), Z(y));
      }
    }, 寬);

  // 頭・左疊帶（入鉤＋十摺＋右緣波豎）
  抖(p => p.M(X(0.42), Z(0.38)).C(X(0.72), Z(0.52), X(1.0), Z(0.56), X(1.2), Z(0.5)), 0.36);
  摺(0.8, 3.6, 0.55, 3.95, 10);
  抖(p => p.M(X(3.78), Z(2.2)).C(X(3.92), Z(2.7), X(3.7), Z(3.3), X(3.85), Z(3.85)), 0.34);
  // 頭・中疊帶（頂剔＋八摺）
  線(p => p.M(X(5.5), Z(0.3)).L(X(5.78), Z(0.5)), 0.32);
  摺(4.1, 6.3, 0.7, 3.5, 8);
  // 頭・右疊帶（五摺＋回結＋二摺）
  摺(6.9, 9.3, 0.6, 2.35, 5);
  抖(p => p.M(X(7.95), Z(2.55)).L(X(9.05), Z(2.53))
    .C(X(9.35), Z(2.55), X(9.35), Z(2.85), X(9.05), Z(2.87))
    .L(X(8.25), Z(2.9)).C(X(7.98), Z(2.93), X(7.98), Z(3.15), X(8.25), Z(3.17))
    .L(X(9.2), Z(3.15)), 0.38);
  摺(6.9, 9.3, 3.45, 4.1, 2);

  // 對帶承波線
  摺(0.8, 3.6, 4.25, 5.5, 3);
  摺(4.1, 9.35, 4.35, 5.0, 2);
  抖(p => p.M(X(4.5), Z(5.32)).C(X(5.7), Z(5.2), X(7.5), Z(5.4), X(9.25), Z(5.26)), 0.38);

  // 厂臂抱密摺
  抖(p => p.M(X(1.1), Z(5.95)).C(X(0.75), Z(6.3), X(0.7), Z(6.8), X(0.95), Z(7.15)), 0.38);
  摺(1.25, 3.6, 5.95, 7.15, 4);

  // 滿幅帶
  摺(0.9, 9.35, 7.35, 9.55, 5);

  // 跳莖對帶（左三摺・莖・右波與二摺）
  摺(0.9, 4.6, 9.75, 11.25, 3);
  線(p => p.M(X(4.72), Z(10.45)).L(X(4.7), Z(11.25)), 0.36);
  抖(p => p.M(X(5.2), Z(10.55)).C(X(6.4), Z(10.45), X(7.9), Z(10.65), X(9.2), Z(10.5)), 0.38);
  摺(4.95, 9.3, 10.95, 11.5, 2);

  // 厂冂雙臂抱疊帶夾中莖（角剔）
  抖(p => p.M(X(1.05), Z(12.0)).C(X(0.6), Z(12.9), X(0.55), Z(14.2), X(1.0), Z(15.1)), 0.4);
  抖(p => p.M(X(9.5), Z(12.0)).C(X(9.62), Z(12.9), X(9.62), Z(14.1), X(9.35), Z(14.9)), 0.4);
  線(p => p.M(X(1.3), Z(12.25)).L(X(1.6), Z(12.5)), 0.32);
  線(p => p.M(X(9.2), Z(12.2)).L(X(8.9), Z(12.45)), 0.32);
  摺(1.3, 4.5, 12.15, 13.9, 5);
  線(p => p.M(X(5.15), Z(12.9)).L(X(5.13), Z(15.2)), 0.36);
  摺(5.5, 9.0, 12.85, 14.15, 4);
  摺(1.3, 9.0, 15.0, 15.45, 2);

  // 大形帶：厂臂＋二摺＋滿幅四摺＋長莖人腳
  抖(p => p.M(X(1.2), Z(16.0)).C(X(0.8), Z(16.8), X(0.75), Z(18.2), X(1.15), Z(19.1)), 0.4);
  摺(1.4, 7.95, 16.1, 16.7, 2);
  摺(1.35, 9.2, 17.05, 19.15, 4);
  線(p => p.M(X(5.35), Z(16.35)).L(X(5.33), Z(18.25)), 0.38);
  抖(p => p.M(X(5.33), Z(18.25)).C(X(5.0), Z(18.7), X(4.7), Z(19.0), X(4.5), Z(19.2)), 0.36);
  抖(p => p.M(X(5.37), Z(18.25)).C(X(5.7), Z(18.7), X(5.95), Z(19.0), X(6.15), Z(19.2)), 0.36);

  // 對疊帶（綴環結二）
  摺(0.9, 4.6, 19.65, 21.15, 4);
  摺(5.0, 9.3, 19.65, 21.15, 4);
  抖(p => p.M(X(5.45), Z(19.95)).C(X(5.7), Z(19.9), X(5.75), Z(20.12), X(5.5), Z(20.15)), 0.38);
  抖(p => p.M(X(5.55), Z(21.8)).C(X(5.8), Z(21.75), X(5.85), Z(21.97), X(5.6), Z(22.0)), 0.38);

  // 底帶＋己足對
  摺(1.0, 9.2, 21.7, 22.65, 2);
  抖(p => p.M(X(1.05), Z(22.75))
    .C(X(1.0), Z(23.2), X(0.9), Z(23.45), X(0.95), Z(23.62))
    .C(X(1.05), Z(23.95), X(1.45), Z(23.9), X(1.5), Z(23.58))
    .C(X(1.5), Z(23.35), X(1.25), Z(23.3), X(1.15), Z(23.48)), 0.38);
  抖(p => p.M(X(9.15), Z(22.7))
    .C(X(9.32), Z(23.05), X(9.42), Z(23.3), X(9.38), Z(23.48))
    .C(X(9.33), Z(23.82), X(8.95), Z(23.77), X(8.96), Z(23.44))
    .C(X(8.97), Z(23.22), X(9.2), Z(23.2), X(9.25), Z(23.38)), 0.38);
};
