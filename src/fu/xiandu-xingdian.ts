// ─────────────────────────────────────────────────────────────────────────────
// 玉清大梵興電玉符（紫微仙都九符・五） · 親筆
// 據圖版：參攷-local/紫微諱/樣頁/樣_94_雷霆玉經紫微仙都九符禱雪借潮符-p53.png
// （右圖；題「玉清大梵興電玉符」承 p52 葉末）
// 判準：密摺蟠曲雲篆（摺＝橫程）——山冠雙簇（左世形冠：底弧左撇中二豎貫橫・右摺簇乙角）＋
// 左疊帶八摺承對簇＋右疊帶綴回結二＋滿幅密帶＋干帶（長莖冂框雙桁）＋
// 蛇頭曲結＋底對疊帶承滿幅八摺。摺帶連綿摹形從簡。
// 錨自 10 格裁圖（格_xingdian）：X=(c-5.0)×2.3、Z=(r-0.1)×2.3+0.8。
// 幅比 2.6。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 興電玉符: 符筆 = (bi) => {
  const u = bi.u, k = 2.3;
  const X = (c: number) => (c - 5.0) * k;
  const Z = (r: number) => (r - 0.1) * k + 0.8;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 摺 = (x0: number, x1: number, rT: number, rB: number, n: number, 左起 = true, 寬 = 0.4) =>
    抖(pa => {
      const p = n > 1 ? (rB - rT) / (n - 1) : 0;
      let dir = 左起 ? 1 : -1, y = rT;
      pa.M(X(dir > 0 ? x0 : x1), Z(y));
      for (let i = 0; i < n; i++) {
        const xe = dir > 0 ? x1 : x0;
        if (i < n - 1) {
          pa.L(X(xe - dir * p * 0.35), Z(y));
          pa.C(X(xe + dir * p * 1.05), Z(y - p * 0.05), X(xe + dir * p * 1.05), Z(y + p * 1.05), X(xe - dir * p * 0.35), Z(y + p));
          y += p; dir = -dir;
        } else pa.L(X(xe), Z(y));
      }
    }, 寬);

  // 山冠左（世形：底弧右揚・左長撇・中二豎貫小橫）
  抖(p => p.M(X(1.3), Z(1.5)).C(X(2.3), Z(1.62), X(3.6), Z(1.55), X(4.5), Z(1.25))
    .C(X(4.85), Z(1.1), X(5.05), Z(0.9), X(5.1), Z(0.75)), 0.44);
  抖(p => p.M(X(0.2), Z(0.4)).C(X(0.9), Z(0.55), X(1.7), Z(0.9), X(2.4), Z(1.45)), 0.46);
  抖(p => p.M(X(2.5), Z(0.05)).C(X(2.6), Z(0.5), X(2.75), Z(1.0), X(2.95), Z(1.48)), 0.4);
  抖(p => p.M(X(3.95), Z(0.05)).C(X(3.9), Z(0.5), X(3.8), Z(1.0), X(3.7), Z(1.45)), 0.4);
  線(p => p.M(X(2.05), Z(0.62)).L(X(4.0), Z(0.56)), 0.36);
  // 冠右（摺簇＋乙角）
  摺(5.5, 8.0, 0.35, 1.35, 2);
  抖(p => p.M(X(8.1), Z(0.85)).C(X(8.6), Z(0.4), X(9.2), Z(0.2), X(9.65), Z(0.35)), 0.4);

  // 左疊帶八摺承對簇
  摺(0.4, 4.9, 1.5, 5.3, 8);
  摺(0.5, 2.5, 5.5, 7.2, 4);
  摺(2.7, 4.6, 5.55, 7.25, 4);

  // 右疊帶綴回結二
  摺(5.4, 8.0, 1.5, 2.5, 3);
  抖(p => p.M(X(8.2), Z(1.5))
    .C(X(9.55), Z(1.45), X(9.6), Z(2.0), X(8.6), Z(2.0))
    .C(X(8.0), Z(2.0), X(8.05), Z(2.5), X(9.1), Z(2.5)), 0.4);
  摺(5.4, 9.7, 2.6, 4.6, 4);
  摺(5.5, 7.5, 4.75, 7.3, 5);
  抖(p => p.M(X(7.7), Z(4.8))
    .C(X(9.5), Z(4.7), X(9.6), Z(5.5), X(8.5), Z(5.55))
    .C(X(7.75), Z(5.6), X(7.8), Z(6.15), X(8.9), Z(6.15)), 0.4);
  摺(7.7, 9.5, 6.35, 7.3, 2);

  // 滿幅密帶
  摺(0.35, 9.65, 7.55, 9.3, 4);
  摺(0.5, 9.5, 9.55, 11.25, 3);

  // 干帶（長莖・上二摺對・冂框雙桁）
  線(p => p.M(X(4.9), Z(11.35)).L(X(4.88), Z(13.35)), 0.4);
  摺(0.6, 4.6, 11.45, 12.4, 2);
  摺(5.2, 9.4, 11.5, 12.45, 2);
  線(p => p.M(X(1.15), Z(13.65)).L(X(1.15), Z(12.6)).L(X(9.4), Z(12.52)).L(X(9.38), Z(13.55)), 0.42);
  線(p => p.M(X(1.3), Z(13.0)).L(X(9.3), Z(12.95)), 0.38);
  線(p => p.M(X(1.35), Z(13.45)).L(X(9.25), Z(13.4)), 0.38);

  // 帶（左臂）
  摺(0.4, 9.6, 13.85, 15.35, 3);
  抖(p => p.M(X(0.42), Z(13.85)).C(X(0.22), Z(14.2), X(0.25), Z(14.5), X(0.45), Z(14.7)), 0.36);

  // 蛇頭曲結承右二摺
  抖(p => p.M(X(2.35), Z(15.85))
    .C(X(1.5), Z(15.5), X(1.25), Z(16.05), X(1.9), Z(16.2))
    .C(X(2.5), Z(16.32), X(3.3), Z(15.85), X(4.0), Z(16.15))
    .C(X(4.25), Z(16.25), X(4.4), Z(16.2), X(4.45), Z(16.1)), 0.4);
  摺(4.6, 9.6, 15.75, 16.5, 2);

  // 底對疊帶（短莖）承滿幅八摺
  摺(0.4, 4.6, 16.85, 21.0, 8);
  線(p => p.M(X(4.9), Z(17.35)).L(X(4.88), Z(18.65)), 0.38);
  摺(5.3, 9.6, 16.85, 21.0, 8);
  摺(0.45, 9.55, 21.3, 26.3, 8);
};
