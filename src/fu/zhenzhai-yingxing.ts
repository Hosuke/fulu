// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭熒星〔字待核〕 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p184_符葉四.jpg（上段右二）
// 判準：星紋符頭（斷桿綴圈二・人字鏈・散圈・右圈柱）＋卌卌帶（雙格柵）＋
// 羽卅帶＋田田層＋底目。主治句題字「熒／然／煞」形近難定——考據卷
// 〔字待核〕仍之，名從「熒」暫記。摹形從簡註。錨自 12 格裁圖
// （宅184_A yingxing）：X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅熒星: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：斷桿綴圈二＋人字鏈＋散圈＋右圈柱
  線(p => p.M(X(1.2), Z(1.6)).L(X(1.9), Z(1.57))); 圈(0.9, 1.6); 圈(2.2, 1.55);
  線(p => p.M(X(3.2), Z(1.5)).L(X(3.7), Z(1.48))); 圈(2.9, 1.5); 圈(4.0, 1.45);
  圈(3.3, 2.9); 連([3.3, 2.9], [2.2, 4.0]); 連([3.3, 2.9], [4.3, 4.0]);
  圈(2.2, 4.0); 圈(4.3, 4.0);
  for (const [x, z] of [[2.3, 5.5], [3.5, 5.6], [2.4, 6.9], [3.6, 7.0]] as const) 圈(x, z);
  for (const z of [1.0, 2.2, 3.4, 4.6, 5.8, 6.9]) 圈(6.8, z);

  // 卌卌帶（雙格柵）
  const 柵 = (x0: number, x1: number) => {
    線(p => p.M(X(x0), Z(7.7)).L(X(x1), Z(7.65)));
    線(p => p.M(X(x0), Z(9.5)).L(X(x1), Z(9.45)));
    for (let i = 0; i < 4; i++) {
      const x = x0 + 0.4 + i * ((x1 - x0 - 0.8) / 3);
      線(p => p.M(X(x), Z(7.4)).L(X(x - 0.02), Z(9.7)), 0.34);
    }
  };
  柵(0.9, 3.3); 柵(3.7, 6.2);

  // 羽卅帶
  const 羽 = (x0: number) => {
    線(p => p.M(X(x0), Z(10.2)).C(X(x0 - 0.3), Z(11.0), X(x0 - 0.2), Z(11.7), X(x0 + 0.1), Z(12.1)), 0.34);
    線(p => p.M(X(x0 + 0.5), Z(10.4)).C(X(x0 + 0.9), Z(10.9), X(x0 + 1.1), Z(11.4), X(x0 + 1.1), Z(11.9)), 0.32);
  };
  羽(1.2); 羽(2.4);
  線(p => p.M(X(3.9), Z(10.5)).L(X(6.1), Z(10.45)));
  for (const x of [4.3, 5.0, 5.7]) 線(p => p.M(X(x), Z(10.1)).L(X(x - 0.02), Z(11.9)), 0.32);

  // 田田層
  const 田 = (x0: number) => {
    線(p => p.M(X(x0), Z(12.6)).L(X(x0), Z(15.0)).L(X(x0 + 2.2), Z(14.95))
      .L(X(x0 + 2.2), Z(12.55)).L(X(x0 - 0.05), Z(12.6)));
    線(p => p.M(X(x0 + 0.1), Z(13.8)).L(X(x0 + 2.1), Z(13.75)), 0.32);
    線(p => p.M(X(x0 + 1.1), Z(12.65)).L(X(x0 + 1.08), Z(14.95)), 0.32);
  };
  田(0.9); 田(3.7);

  // 底目
  線(p => p.M(X(1.0), Z(15.7)).L(X(1.0), Z(19.0)));
  線(p => p.M(X(6.0), Z(15.65)).L(X(6.0), Z(18.95)));
  for (const z of [15.7, 17.35, 19.0]) 線(p => p.M(X(1.0), Z(z)).L(X(6.0), Z(z - 0.05)));
};
