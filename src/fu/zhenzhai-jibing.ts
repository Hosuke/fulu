// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭除口舌疾病之災 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p183_符葉三.jpg（中段右一）
// 判準：星紋符頭（桿一・圈行三——各六圈）＋箱陣三列四行（日口箱，內綴
// 吉放大木杙形——摹形從簡）。錨自 12 格裁圖（宅183_A jibing）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅疾病: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿一＋圈行三
  線(p => p.M(X(0.9), Z(1.0)).L(X(6.9), Z(0.9))); 圈(0.55, 1.0, 0.32); 圈(7.2, 0.9);
  for (const z of [2.3, 3.7, 5.1])
    for (const i of [0, 1, 2, 3, 4, 5]) 圈(1.0 + i * 1.15, z + i * 0.02);

  // 箱陣三列四行（內綴杙形）
  const 箱 = (x0: number, z0: number, 內?: string) => {
    const w = 1.8, h = 2.1;
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z0 + h)).L(X(x0 + w), Z(z0 + h - 0.04))
      .L(X(x0 + w), Z(z0 - 0.04)).L(X(x0 - 0.04), Z(z0)));
    if (內 === '十') {
      線(p => p.M(X(x0 + w / 2), Z(z0 + 0.3)).L(X(x0 + w / 2 - 0.03), Z(z0 + h - 0.3)), 0.32);
      線(p => p.M(X(x0 + 0.3), Z(z0 + h / 2)).L(X(x0 + w - 0.3), Z(z0 + h / 2 - 0.03)), 0.32);
    } else if (內 === '大') {
      線(p => p.M(X(x0 + 0.3), Z(z0 + 0.6)).L(X(x0 + w - 0.3), Z(z0 + 0.56)), 0.32);
      線(p => p.M(X(x0 + w / 2), Z(z0 + 0.35)).C(X(x0 + w / 2 - 0.4), Z(z0 + 1.2),
        X(x0 + w / 2 - 0.6), Z(z0 + 1.7), X(x0 + 0.25), Z(z0 + h - 0.25)), 0.32);
      線(p => p.M(X(x0 + w / 2), Z(z0 + 0.9)).C(X(x0 + w / 2 + 0.3), Z(z0 + 1.4),
        X(x0 + w / 2 + 0.5), Z(z0 + 1.7), X(x0 + w - 0.25), Z(z0 + h - 0.25)), 0.32);
    } else if (內 === '一') {
      線(p => p.M(X(x0 + 0.25), Z(z0 + h / 2)).L(X(x0 + w - 0.25), Z(z0 + h / 2 - 0.03)), 0.32);
    }
  };
  const 行z = [6.4, 8.9, 11.4, 13.9];
  const 列x = [0.9, 3.1, 5.3];
  const 內容: (string | undefined)[][] = [
    ['一', '十', '一'],
    ['一', '大', '一'],
    ['一', '十', '大'],
    ['一', undefined, '一'],
  ];
  for (let r = 0; r < 4; r++)
    for (let c = 0; c < 3; c++)
      箱(列x[c], 行z[r], 內容[r][c]);
};
