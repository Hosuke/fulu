// 剛字式（散形十筆・北宋天心法） · 親筆
// 據圖版：參攷-local/敕令符頭/樣頁/（自 NLC-9797 冊4258 PDF p27 抽葉親見）
// （太上助國救民總真祕要 DZ1227，序 1116；卷二散形節）
// 判準：原刻拆畫圖——剛字逐筆繫：天剛大聖・北斗尊星（誅戩凶惡）・滅跡除形・
// 膽碎心傾・救民疾苦・大賜威靈・治病却禍・回死作生・天符到處・斷絕妖精・
// 魔王畏懼（尾誦「降臨真炁聖威奉行急急如律令」記位 p27–28）；
// 本繪依畫序親書楷體成字（同敕字式之裁）。幅比 1.2（z 0..28.8）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 剛字式: 符筆 = (bi) => {
  const u = bi.u;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.95) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.05) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // ── 岡部（左）──
  // 丨（冂左豎）
  橫(p => p.M(-8.8, 5.8).L(-8.9, 24.4));
  // 橫折（冂頂橫＋右豎，收小鉤）
  出(p => p.M(-8.8, 5.9).L(1.2, 5.5).L(1.35, 24.0)
    .C(1.35, 24.8, 0.9, 25.2, 0.1, 24.9));
  // 丷（內上二點）
  出(p => p.M(-5.3, 7.7).C(-5.9, 8.7, -6.5, 9.7, -7.1, 10.5), 0.85);
  出(p => p.M(-2.5, 7.6).C(-2.0, 8.6, -1.6, 9.6, -1.3, 10.5), 0.85);
  // 一（山上橫）
  橫(p => p.M(-7.2, 11.8).L(-0.4, 11.6), 0.85);
  // 山（中豎・豎折・右豎）
  橫(p => p.M(-3.9, 13.2).L(-3.95, 19.8), 0.9);
  橫(p => p.M(-6.6, 14.6).L(-6.7, 20.2).L(-1.1, 19.9), 0.85);
  橫(p => p.M(-1.2, 14.8).L(-1.1, 19.9), 0.85);

  // ── 刂部（右）──
  // 短丨
  橫(p => p.M(4.6, 7.0).L(4.65, 13.0), 0.9);
  // 亅（長豎左鉤）
  出(p => p.M(8.6, 5.2).L(8.75, 22.6)
    .C(8.75, 23.8, 8.2, 24.5, 7.1, 24.8), 1.15);
};
