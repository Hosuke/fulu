// ─────────────────────────────────────────────────────────────────────────────
// 祈雨符（道法會元卷九十四・雨頭十五類符・四） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3923_卷94十五類符雨頭一覽-p26.png（右起第四）
// 判準：雨字符頭通制（頂桁・冠框・內左三橫右三弧・中豎長垂・●墨點・左右
// 長腳）＋川流三縷波垂（中縷長貫●）。符下咒音記位不錄。摹形從簡。
// 錨自 8 格裁圖（格94_五 qiyu）：X=(c-3.75)×2.55、Z=(r-5.0)×2.55+1.0。
// 幅比 3。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 祈雨符: 符筆 = (bi) => {
  const u = bi.u, k = 2.55;
  const X = (c: number) => (c - 3.75) * k;
  const Z = (r: number) => (r - 5.0) * k + 1.0;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.34) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.36) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 點 = (x: number, z: number, r = 0.4) =>
    bi.塊(p => p.M(X(x - r), Z(z)).C(X(x - r), Z(z - r * 1.3), X(x + r), Z(z - r * 1.3), X(x + r), Z(z))
      .C(X(x + r), Z(z + r * 1.3), X(x - r), Z(z + r * 1.3), X(x - r), Z(z)).close());

  // 雨冠（內雨點二對）
  線(p => p.M(X(0.2), Z(5.25)).L(X(7.2), Z(5.18)), 0.84);
  出(p => p.M(X(0.4), Z(5.30)).L(X(0.38), Z(21.30)).C(X(0.37), Z(21.95), X(0.27), Z(22.43), X(0.12), Z(22.76)), 0.76);
  出(p => p.M(X(6.8), Z(5.30)).L(X(6.78), Z(24.21)).C(X(6.8), Z(24.86), X(6.92), Z(25.35), X(7.08), Z(25.67)), 0.76);
  線(p => p.M(X(1.0), Z(6.10)).L(X(1.9), Z(6.06)), 0.57);
  線(p => p.M(X(1.0), Z(6.80)).L(X(1.9), Z(6.76)), 0.57);
  線(p => p.M(X(4.6), Z(6.10)).L(X(5.5), Z(6.06)), 0.57);
  線(p => p.M(X(4.6), Z(6.80)).L(X(5.5), Z(6.76)), 0.57);
  線(p => p.M(X(3.4), Z(5.35)).L(X(3.38), Z(8.00)), 0.72);
  // 川流三縷波垂（中縷長）
  出(p => p.M(X(1.9), Z(8.20)).C(X(1.5), Z(10.12), X(2.3), Z(12.23), X(1.7), Z(14.17)).C(X(1.4), Z(15.79), X(2.1), Z(17.25), X(1.9), Z(18.38)), 0.68);
  出(p => p.M(X(4.7), Z(8.20)).C(X(4.3), Z(10.12), X(5.1000000000000005), Z(12.23), X(4.5), Z(14.17)).C(X(4.2), Z(15.79), X(4.9), Z(17.25), X(4.7), Z(18.38)), 0.68);
  出(p => p.M(X(3.3), Z(8.20)).C(X(2.9), Z(10.12), X(3.7), Z(12.23), X(3.1), Z(14.17)).C(X(2.8), Z(15.79), X(3.5), Z(17.25), X(3.3), Z(19.84)), 0.68);
  點(3.3, 20.81);
};
