// 鎮宅靈符・厭釜鳴狗上牀斬火光一切鬼 · 親筆（七十二道之首，p180 下段右一）
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p180_題序璇璣八卦圖.jpg（下段右一）
// 判準：圈桿符頭（桿二全幅端圈＋圈桿鏈三列・右緣圈列三）＋滿幅桁＋寬目箱＋
// 目箱對＋曰箱陣二層（右緣豎）＋底土土對（摹形從簡）。錨自 8 格裁圖
// （宅180_三符 fuming）：X=(c-4.0)×2.4、Z=(r-2.0)×2.4+2。幅比 2.2。
// 逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅釜鳴: 符筆 = (bi) => {
  const u = bi.u, k = 2.4;
  const X = (c: number) => (c - 4.0) * k;
  const Z = (r: number) => (r - 2.0) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.3) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿二全幅（端圈）
  線(p => p.M(X(0.85), Z(2.35)).L(X(6.6), Z(2.3))); 圈(0.5, 2.36); 圈(6.95, 2.3);
  線(p => p.M(X(0.85), Z(3.6)).L(X(6.6), Z(3.55))); 圈(0.5, 3.61); 圈(6.95, 3.55);
  // 圈桿鏈三列
  圈(0.5, 4.78); 線(p => p.M(X(0.85), Z(4.77)).L(X(2.25), Z(4.74))); 圈(2.6, 4.73);
  線(p => p.M(X(2.95), Z(4.72)).L(X(4.55), Z(4.69))); 圈(4.9, 4.68);
  圈(0.5, 5.96); 線(p => p.M(X(0.85), Z(5.95)).L(X(2.35), Z(5.92))); 圈(2.7, 5.91);
  線(p => p.M(X(3.05), Z(5.9)).L(X(4.55), Z(5.87))); 圈(4.9, 5.86);
  圈(0.55, 7.12); 線(p => p.M(X(0.9), Z(7.1)).L(X(2.15), Z(7.07))); 圈(2.5, 7.06);
  // 右緣圈列三
  圈(6.95, 4.45); 圈(6.95, 5.6); 圈(6.9, 6.8);

  // 滿幅桁
  線(p => p.M(X(0.4), Z(7.95)).L(X(7.1), Z(7.88)), 0.44);

  // 寬目箱
  const 目 = (x0: number, x1: number, z0: number, z1: number, 桁 = 1) => {
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z1)).L(X(x1), Z(z1 - 0.04))
      .L(X(x1), Z(z0 - 0.04)).L(X(x0 - 0.04), Z(z0)));
    for (let i = 1; i <= 桁; i++)
      線(p => p.M(X(x0 + 0.1), Z(z0 + (z1 - z0) * i / (桁 + 1)))
        .L(X(x1 - 0.1), Z(z0 + (z1 - z0) * i / (桁 + 1) - 0.03)), 0.32);
  };
  目(0.5, 7.0, 8.6, 10.6, 2);
  線(p => p.M(X(5.75), Z(8.7)).L(X(5.73), Z(10.5)), 0.32);
  // 目箱對
  目(0.5, 3.4, 11.0, 13.2, 2); 目(3.9, 7.0, 11.0, 13.2);
  // 曰箱陣二層＋右緣豎
  目(0.5, 2.4, 13.6, 15.6); 目(3.0, 5.1, 13.6, 15.6);
  目(0.5, 2.4, 16.0, 18.0); 目(3.0, 5.1, 16.0, 18.0);
  線(p => p.M(X(6.85), Z(13.6)).L(X(6.83), Z(18.0)), 0.38);

  // 底土土對
  線(p => p.M(X(1.4), Z(19.2)).L(X(1.38), Z(22.2)), 0.4);
  線(p => p.M(X(0.5), Z(20.65)).L(X(2.3), Z(20.6)), 0.38);
  線(p => p.M(X(0.4), Z(22.3)).L(X(2.4), Z(22.25)), 0.4);
  線(p => p.M(X(4.0), Z(19.1)).L(X(3.98), Z(22.1)), 0.4);
  線(p => p.M(X(3.1), Z(20.55)).L(X(4.9), Z(20.5)), 0.38);
  線(p => p.M(X(3.0), Z(22.2)).L(X(5.0), Z(22.15)), 0.4);
};
