// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭亡失克害人口之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p182_符葉二.jpg（上段右三）
// 判準：星紋符頭（W折長鏈三道・V鏈・散圈叢十餘）＋寬桁帶＋左軌目田層＋
// 弓迴脚。錨自 12 格裁圖（宅182_A wangshi）：X=(c-3.95)×2.7、
// Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅亡失: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：W折長鏈（圈四折三）＋V鏈＋散圈叢
  圈(0.7, 0.8); 圈(6.7, 1.1); 圈(0.8, 2.1); 圈(6.7, 2.3);
  連([0.7, 0.8], [6.7, 1.1]); 連([6.7, 1.1], [0.8, 2.1]); 連([0.8, 2.1], [6.7, 2.3]);
  圈(0.8, 4.0); 圈(3.6, 5.0); 圈(6.8, 4.2);
  連([0.8, 4.0], [3.6, 5.0]); 連([3.6, 5.0], [6.8, 4.2]);
  for (const [x, z] of [[2.6, 5.6], [4.3, 5.5], [1.5, 6.3], [3.3, 6.5], [4.6, 6.4], [5.9, 6.2],
                        [0.9, 7.3], [2.3, 7.5], [3.9, 7.6], [4.8, 7.2], [6.4, 7.3]] as const)
    圈(x, z);

  // 寬桁帶
  線(p => p.M(X(0.8), Z(8.4)).L(X(7.0), Z(8.3)), 0.42);
  線(p => p.M(X(0.9), Z(9.2)).L(X(6.9), Z(9.1)));
  線(p => p.M(X(0.9), Z(10.0)).L(X(6.9), Z(9.9)));

  // 左軌目田層
  線(p => p.M(X(0.8), Z(8.45)).L(X(0.8), Z(16.2)));
  線(p => p.M(X(1.5), Z(10.6)).L(X(1.5), Z(13.4)).L(X(6.4), Z(13.35))
    .L(X(6.4), Z(10.55)).L(X(1.45), Z(10.6)));
  線(p => p.M(X(4.0), Z(10.65)).L(X(3.95), Z(13.35)));
  線(p => p.M(X(1.6), Z(12.0)).L(X(6.3), Z(11.95)));

  // 弓迴脚（boustro二折＋張脚）
  線(p => p.M(X(1.6), Z(14.0)).L(X(5.2), Z(13.95))
    .C(X(5.7), Z(13.95), X(5.7), Z(14.85), X(5.2), Z(14.9))
    .L(X(2.3), Z(14.95)).C(X(1.8), Z(14.95), X(1.8), Z(15.85), X(2.3), Z(15.9))
    .L(X(5.6), Z(15.85)).C(X(6.1), Z(15.85), X(6.2), Z(16.4), X(6.1), Z(17.0)));
  線(p => p.M(X(0.8), Z(16.5)).C(X(0.7), Z(17.4), X(0.7), Z(18.1), X(0.8), Z(18.7)));
  線(p => p.M(X(6.1), Z(17.3)).C(X(6.1), Z(18.0), X(6.1), Z(18.5), X(6.1), Z(18.9)));
};
