// ─────────────────────────────────────────────────────────────────────────────
// 老君所戴神印（百鬼及蛇蝮虎狼神印圖） · 親筆
// 據圖版：四庫全卷 PDF p043 抽葉（參攷-local/入山符/，欽定四庫全書繕本，PD）
// 判準：印圖（刻棗心木方二寸之印文摹圖，非紙符）——頂二桁→王構右括→
// 雲弧疊四→重墨雙塊（左桁塊・右凸塊）→左口箱・彐疊桁→長桁→左亻小記・
// 乂火曲→右重墨桁曳長撇→右角箱（弧頂括脅內桁）→底凵張脅內目箱。
// 繫語「以棗心木方二寸刻之，再拜而帶之」——印刻非書，不施硃。
// 幅比 1.5（z 0..36）。錨：X=(col-12.6)×1.05、Z=(row-1.2)×1.05（格_yin）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 老君神印: 符筆 = (bi) => {
  const u = bi.u;
  const k = 1.05;
  const X = (c: number) => (c - 12.6) * k;
  const Z = (r: number) => (r - 1.2) * k;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 頂二桁
  線(p => p.M(X(5.9), Z(2.0)).C(X(9.5), Z(1.7), X(14.0), Z(1.7), X(18.0), Z(2.0)), 0.6);
  線(p => p.M(X(6.1), Z(3.0)).C(X(9.5), Z(2.7), X(13.0), Z(2.7), X(16.2), Z(3.0)), 0.55);

  // 王構右括
  線(p => p.M(X(7.5), Z(5.8)).L(X(17.5), Z(5.6)), 0.58);
  線(p => p.M(X(7.3), Z(7.0)).L(X(17.8), Z(6.8)), 0.58);
  線(p => p.M(X(7.6), Z(8.35)).L(X(18.4), Z(8.15)), 0.58);
  線(p => p.M(X(12.9), Z(5.8)).L(X(12.85), Z(8.3)));
  出(p => p.M(X(18.3), Z(4.7)).C(X(19.1), Z(6.4), X(19.5), Z(8.2), X(19.4), Z(10.2)));

  // 雲弧疊四＋左人曳
  線(p => p.M(X(7.4), Z(9.4)).C(X(10.5), Z(10.5), X(15.0), Z(10.4), X(18.2), Z(9.2)), 0.58);
  線(p => p.M(X(7.8), Z(10.4)).C(X(10.8), Z(11.4), X(15.2), Z(11.3), X(18.4), Z(10.2)), 0.55);
  線(p => p.M(X(8.2), Z(11.4)).C(X(11.0), Z(12.1), X(15.4), Z(12.0), X(18.6), Z(11.2)), 0.55);
  線(p => p.M(X(8.6), Z(12.3)).C(X(11.3), Z(12.9), X(15.5), Z(12.8), X(18.7), Z(12.1)), 0.55);
  出(p => p.M(X(5.7), Z(10.8)).C(X(5.3), Z(11.7), X(5.0), Z(12.6), X(4.9), Z(13.5)));

  // 重墨雙塊（左桁塊・右凸塊）
  bi.塊(t => t.M(X(5.9), Z(13.6)).L(X(12.4), Z(13.3)).L(X(12.5), Z(15.1))
    .L(X(6.0), Z(15.4)).close());
  bi.塊(t => t.M(X(13.4), Z(12.9)).L(X(18.8), Z(12.7)).L(X(18.9), Z(14.9))
    .L(X(17.2), Z(15.0)).L(X(17.25), Z(17.2)).L(X(14.5), Z(17.3))
    .L(X(14.45), Z(15.1)).L(X(13.45), Z(15.15)).close());

  // 左口箱・彐疊桁
  線(p => p.M(X(6.7), Z(16.4)).L(X(11.2), Z(16.2)).L(X(11.25), Z(18.0))
    .L(X(6.75), Z(18.2)).L(X(6.7), Z(16.45)), 0.55);
  線(p => p.M(X(6.8), Z(17.25)).L(X(11.1), Z(17.1)), 0.5);
  // 彐疊桁（右端圓迴連書）
  出(p => p.M(X(7.4), Z(18.9)).L(X(18.3), Z(18.6))
    .C(X(18.9), Z(18.6), X(18.9), Z(19.4), X(18.3), Z(19.4))
    .L(X(7.5), Z(19.7)), 0.55);
  出(p => p.M(X(7.6), Z(20.4)).L(X(18.4), Z(20.15))
    .C(X(19.0), Z(20.15), X(19.0), Z(20.9), X(18.4), Z(20.9))
    .L(X(7.7), Z(21.15)), 0.55);
  // 長桁
  線(p => p.M(X(7.7), Z(22.0)).C(X(11.0), Z(21.7), X(15.5), Z(21.7), X(18.9), Z(21.9)), 0.6);

  // 左亻小記
  出(p => p.M(X(5.6), Z(24.5)).C(X(5.5), Z(25.5), X(5.4), Z(26.5), X(5.3), Z(27.5)), 0.5);
  線(p => p.M(X(5.7), Z(25.3)).L(X(7.4), Z(25.2)), 0.45);
  線(p => p.M(X(5.8), Z(26.3)).L(X(7.3), Z(26.2)), 0.45);
  // 乂火曲
  出(p => p.M(X(8.7), Z(27.3)).C(X(9.2), Z(28.0), X(9.7), Z(28.6), X(10.2), Z(29.2)), 0.55);
  出(p => p.M(X(10.3), Z(27.2)).C(X(9.8), Z(27.9), X(9.2), Z(28.6), X(8.6), Z(29.3)), 0.55);
  出(p => p.M(X(8.3), Z(26.5)).C(X(8.1), Z(27.6), X(8.0), Z(28.6), X(8.0), Z(29.5)), 0.5);

  // 右重墨桁・長撇下曳
  bi.塊(t => t.M(X(14.2), Z(24.0)).L(X(19.4), Z(23.8)).L(X(19.45), Z(25.1))
    .L(X(14.25), Z(25.3)).close());
  出(p => p.M(X(14.3), Z(25.4)).C(X(13.2), Z(27.4), X(11.5), Z(29.8), X(9.5), Z(31.9)), 0.6);

  // 右角箱（弧頂・右括・內桁雙豎）
  線(p => p.M(X(13.6), Z(26.2)).C(X(15.3), Z(25.7), X(17.2), Z(25.6), X(18.8), Z(25.8)), 0.55);
  線(p => p.M(X(13.9), Z(26.7)).L(X(13.85), Z(30.3)));
  線(p => p.M(X(18.6), Z(26.5)).L(X(18.55), Z(30.2)));
  for (const z of [27.35, 28.15, 28.95])
    線(p => p.M(X(14.0), Z(z)).L(X(18.5), Z(z - 0.12)), 0.5);
  線(p => p.M(X(15.4), Z(26.7)).L(X(15.35), Z(30.1)), 0.5);
  線(p => p.M(X(16.9), Z(26.6)).L(X(16.85), Z(30.15)), 0.5);
  線(p => p.M(X(13.95), Z(30.3)).L(X(18.55), Z(30.15)), 0.55);
  出(p => p.M(X(19.0), Z(26.3)).C(X(19.4), Z(27.5), X(19.5), Z(28.8), X(19.4), Z(30.0)), 0.5);

  // 底凵張脅・內目箱
  出(p => p.M(X(6.9), Z(29.6)).C(X(6.0), Z(31.0), X(5.7), Z(32.4), X(5.6), Z(33.7)), 0.68);
  出(p => p.M(X(19.2), Z(29.5)).C(X(20.1), Z(30.9), X(20.4), Z(32.2), X(20.5), Z(33.5)), 0.68);
  線(p => p.M(X(8.6), Z(31.1)).L(X(8.55), Z(33.25)));
  線(p => p.M(X(8.6), Z(31.15)).L(X(17.9), Z(30.9)).L(X(17.95), Z(33.1)));
  線(p => p.M(X(8.7), Z(31.9)).L(X(17.8), Z(31.7)), 0.5);
  線(p => p.M(X(8.7), Z(32.6)).L(X(17.85), Z(32.4)), 0.5);
  線(p => p.M(X(8.6), Z(33.3)).L(X(17.9), Z(33.1)), 0.55);
};
