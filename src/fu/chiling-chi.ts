// （太上助國救民總真祕要 DZ1227，序 1116；涵芬樓 002-014a–b）
// 判準：原刻乃**拆畫圖**——敕字逐畫繫神（玉清上帝起筆…玉皇上帝敕收捺，十二位）；
// 本繪依其畫序**親書楷體成字**（成字之構為親筆之楷，非摹自成字圖版——明注）。
// 楷法：橫藏回、豎懸針、撇捺尖出。幅比 1.2（z 0..28.8）。
// 敕字符頭之用（驅滅強邪用三清敕）記位卷二，訣法不錄。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 敕字式: 符筆 = (bi) => {
  const u = bi.u;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.0) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.1) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // ── 束部（左）──
  // 一（玉清上帝）：長橫
  橫(p => p.M(-8.8, 8.4).C(-6.0, 8.1, -3.0, 8.0, 0.6, 8.2));
  // 豎（懸針，貫中）
  出(p => p.M(-4.1, 3.6).L(-4.15, 16.0).C(-4.15, 19.0, -4.1, 22.0, -4.0, 24.8));
  // 口（三筆：左豎・橫折豎・底橫）
  橫(p => p.M(-6.9, 10.6).L(-6.9, 14.4), 0.9);
  橫(p => p.M(-6.9, 10.7).L(-1.4, 10.5).L(-1.4, 14.3), 0.9);
  橫(p => p.M(-6.9, 14.4).L(-1.4, 14.3), 0.9);
  // 撇（自豎腰左出）
  出(p => p.M(-3.9, 15.2).C(-5.2, 17.4, -6.8, 19.6, -8.8, 21.6));
  // 反捺點（右出）
  出(p => p.M(-3.9, 15.4).C(-2.8, 16.8, -1.8, 18.2, -1.0, 19.8));

  // ── 攵部（右）──
  // 短撇
  出(p => p.M(4.4, 3.8).C(3.6, 5.2, 2.8, 6.4, 1.8, 7.5));
  // 橫
  橫(p => p.M(1.6, 8.6).C(3.6, 8.3, 5.6, 8.2, 7.8, 8.4));
  // 長撇（自橫右上向左下貫）
  出(p => p.M(6.6, 8.6).C(5.2, 12.5, 3.4, 17.5, 0.6, 22.5)
    .C(0.0, 23.5, -0.6, 24.3, -1.2, 25.0));
  // 捺（玉皇上帝敕——收筆重出）
  出(p => p.M(3.2, 12.6).C(4.6, 15.6, 6.2, 19.0, 8.0, 22.4)
    .C(8.5, 23.3, 9.0, 24.1, 9.4, 24.8), 1.25);
};
