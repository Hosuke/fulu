// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭除風火之恠 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p181_符葉一.jpg（中段右四）
// 判準：星紋符頭（桿二・乂交圈鏈・右圈柱）＋頂桁＋手風琴疊桁帶＋底山迴
// 匚曲張脚。錨自 12 格裁圖（宅181_A z4）：X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。
// 幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅風火: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.34 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.32 * k, Z(z)).L(X(x1) - 0.32 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：桿二＋乂交圈鏈＋右圈柱
  桿(0.5, 7.3, 1.0);
  桿(0.5, 7.1, 2.4);
  圈(0.6, 3.9); 圈(5.5, 3.7); 圈(0.6, 6.2); 圈(5.4, 6.0);
  連([0.6, 3.9], [5.4, 6.0]); 連([5.5, 3.7], [0.6, 6.2]);
  for (const z of [3.2, 4.6, 6.0]) 圈(7.2, z);

  // 頂桁
  線(p => p.M(X(0.5), Z(7.3)).L(X(7.3), Z(7.1)), 0.42);

  // 手風琴疊桁帶（左匡＋桁七）
  線(p => p.M(X(0.6), Z(7.8)).L(X(0.6), Z(13.5)));
  線(p => p.M(X(7.2), Z(7.7)).L(X(7.2), Z(13.4)));
  for (let i = 0; i < 7; i++)
    線(p => p.M(X(0.7), Z(7.9 + i * 0.8)).L(X(7.1), Z(7.8 + i * 0.8)));

  // 底山迴＋匚曲張脚
  線(p => p.M(X(1.5), Z(14.0)).L(X(1.5), Z(15.6))
    .C(X(1.5), Z(16.0), X(1.8), Z(16.2), X(2.3), Z(16.2))
    .C(X(2.8), Z(16.2), X(3.0), Z(15.9), X(3.0), Z(15.5))
    .L(X(3.0), Z(14.2)).C(X(3.0), Z(13.9), X(3.3), Z(13.8), X(3.7), Z(13.8))
    .L(X(4.5), Z(13.8)).L(X(4.5), Z(16.0)));
  線(p => p.M(X(5.6), Z(13.9)).C(X(5.5), Z(15.0), X(5.6), Z(16.0), X(5.9), Z(16.8)));
  線(p => p.M(X(6.9), Z(13.8)).C(X(7.0), Z(14.9), X(6.9), Z(15.9), X(6.6), Z(16.9)));
  線(p => p.M(X(1.6), Z(16.7)).C(X(1.4), Z(17.4), X(1.3), Z(18.0), X(1.3), Z(18.5)));
  線(p => p.M(X(4.4), Z(16.5)).C(X(4.5), Z(17.2), X(4.5), Z(17.8), X(4.4), Z(18.3)));
};
