// ─────────────────────────────────────────────────────────────────────────────
// 入山佩帶符・一 · 親筆
// 據圖版：參攷-local/入山符/抱朴子登涉_入山佩帶符_p045.png（四庫繕本，PD）
// 原文（王明本）：「入山佩帶符〔圖〕此三符，兼同著牛馬屋左右前後及豬欄上，辟虎狼也」
// ——組凡三符（p045–047，考據卷六節對應考）。材色原文未言，不入硃層（寧缺毋誤）。
// 判準：四轉大螺旋符首，尾曳左下折而右行成匡肩，右垂長欄；身左王形三波桁，
// 右 S 瀑；下承田匣與底桁三道；雙足外撇。幅比 2.05（Z=z+1）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 佩帶符一: 符筆 = (bi) => {
  const u = bi.u;
  const X = (x: number) => x;
  const Z = (z: number) => z + 1;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);

  // 螺旋符首（阿基米德螺線四轉，外圈出尾）＋曳尾成匡肩＋右垂長欄（一筆連書）
  線(p => {
    const cx = -0.3, cz = 6.3;
    const 轉 = 4, 步 = 24, 相 = 2.4;             // 四轉，每轉 24 段；相位 2.4 rad（≈0.764π），使出口落於螺心左下
    const θ末 = 轉 * 2 * Math.PI;
    const r內 = 0.55, b = (5.5 - r內) / θ末;      // 內起 0.55 指，外至 5.5 指
    p.M(X(cx + r內 * Math.cos(相)), Z(cz + r內 * Math.sin(相)));   // 起點同相，免橫弦（Codex 審）
    for (let i = 1; i <= 轉 * 步; i++) {
      const θ = (i / 步) * 2 * Math.PI;
      const r = r內 + b * θ;
      p.L(X(cx + r * Math.cos(θ + 相)), Z(cz + r * Math.sin(θ + 相)));
    }
    // 出尾：先順出口切向（左上）圓轉而下，乃曳左下・右行成匡肩・右角垂欄（消折角）
    p.C(X(-5.9), Z(9.6), X(-7.1), Z(10.8), X(-7.2), Z(13.4));
    p.C(X(-7.5), Z(14.9), X(-6.5), Z(15.7), X(-5.0), Z(15.8));
    p.L(X(6.2), Z(16.2));
    p.C(X(7.8), Z(16.3), X(8.3), Z(16.9), X(8.3), Z(18.2));
    p.L(X(8.4), Z(41.5));
    p.C(X(8.5), Z(43.8), X(8.8), Z(45.6), X(9.2), Z(47.0));
  }, 0.6);

  // 左王形：三波桁＋中豎＋足桁
  線(p => p.M(X(-8.6), Z(18.2)).C(X(-6.0), Z(19.4), X(-3.6), Z(19.4), X(-1.6), Z(18.4)));
  線(p => p.M(X(-8.8), Z(21.2)).C(X(-6.0), Z(22.4), X(-3.4), Z(22.4), X(-1.4), Z(21.4)));
  線(p => p.M(X(-9.0), Z(24.2)).C(X(-6.0), Z(25.6), X(-3.0), Z(25.6), X(-0.8), Z(24.4)));
  線(p => p.M(X(-4.7), Z(19.6)).L(X(-4.7), Z(25.2)));
  線(p => p.M(X(-4.0), Z(25.8)).C(X(-2.8), Z(25.5), X(-1.8), Z(25.4), X(-1.0), Z(25.5)));

  // 右 S 瀑（弓折下貫）＋短桁
  線(p => p.M(X(3.2), Z(18.4))
    .C(X(2.4), Z(19.6), X(4.6), Z(20.2), X(3.6), Z(21.6))
    .C(X(2.8), Z(22.8), X(4.6), Z(23.6), X(3.6), Z(25.0))
    .C(X(2.6), Z(26.4), X(1.2), Z(27.0), X(0.6), Z(28.2)));
  線(p => p.M(X(4.4), Z(18.7)).C(X(5.4), Z(18.5), X(6.2), Z(18.5), X(6.8), Z(18.6)), 0.5);
  線(p => p.M(X(2.6), Z(22.0)).C(X(4.2), Z(21.8), X(5.6), Z(21.9), X(6.6), Z(22.1)), 0.5);

  // 田匣（左直角部＋右匣含中桁）
  線(p => p.M(X(-6.4), Z(28.6)).C(X(-4.0), Z(28.0), X(-1.6), Z(28.0), X(0.4), Z(28.4)));
  線(p => p.M(X(-3.6), Z(28.4)).L(X(-3.6), Z(34.8)));
  線(p => p.M(X(-3.5), Z(31.4)).C(X(-1.5), Z(31.2), X(0.5), Z(31.2), X(2.2), Z(31.4)));
  線(p => p.M(X(2.4), Z(29.0)).L(X(7.6), Z(28.8)).L(X(7.7), Z(32.6)).L(X(2.4), Z(32.8)).L(X(2.4), Z(29.0)));
  線(p => p.M(X(5.0), Z(28.9)).L(X(5.0), Z(32.7)), 0.5);

  // 底桁三道（波曲，漸寬）
  線(p => p.M(X(-8.0), Z(35.0)).C(X(-3.0), Z(35.8), X(3.0), Z(35.6), X(8.2), Z(34.8)));
  線(p => p.M(X(-8.4), Z(38.4)).C(X(-3.0), Z(39.4), X(3.0), Z(39.2), X(8.3), Z(38.2)));
  線(p => p.M(X(-8.6), Z(41.8)).C(X(-3.0), Z(42.8), X(3.0), Z(42.6), X(8.3), Z(41.4)));

  // 左匡與雙足（左欄下垂外撇）
  線(p => p.M(X(-8.2), Z(30.6))
    .C(X(-8.5), Z(35.0), X(-8.7), Z(39.5), X(-8.9), Z(43.0))
    .C(X(-9.1), Z(45.0), X(-9.8), Z(46.4), X(-10.8), Z(47.2)));
};
