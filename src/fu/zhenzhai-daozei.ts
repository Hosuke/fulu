// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭盜賊口舌無端之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p181_符葉一.jpg（下段右一）
// 判準：星紋符頭（桿二・右圈對三行）＋左目梯早起＋寬帶雙叩箱・右軌＋
// 兄叩四聯（口首垂足）＋底匚・迴脚。錨自 12 格裁圖（宅181_B x1）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅盜賊: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.32 * k, Z(z)).L(X(x1) - 0.32 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：桿二＋右圈對三行
  桿(0.5, 7.2, 1.0);
  桿(0.5, 7.1, 2.35);
  for (const [x, z] of [[5.0, 3.4], [6.8, 3.4], [5.1, 5.0], [6.9, 5.0],
                        [5.2, 6.4], [6.9, 6.4]] as const) 圈(x, z);

  // 左目梯早起（三箱）
  線(p => p.M(X(0.6), Z(3.0)).L(X(0.6), Z(7.9)));
  線(p => p.M(X(3.9), Z(2.95)).L(X(3.9), Z(7.85)));
  for (let i = 0; i <= 3; i++)
    線(p => p.M(X(0.6), Z(3.0 + i * 1.63)).L(X(3.9), Z(2.95 + i * 1.63)));

  // 寬帶：左目箱＋右雙叩箱＋右軌
  線(p => p.M(X(0.6), Z(8.4)).L(X(0.6), Z(12.4)));
  線(p => p.M(X(3.2), Z(8.35)).L(X(3.2), Z(12.35)));
  for (const z of [8.4, 10.4, 12.4]) 線(p => p.M(X(0.6), Z(z)).L(X(3.2), Z(z - 0.05)));
  線(p => p.M(X(4.4), Z(8.5)).L(X(4.4), Z(10.4)).L(X(6.6), Z(10.3))
    .L(X(6.6), Z(8.45)).L(X(4.35), Z(8.5)));
  線(p => p.M(X(4.5), Z(10.7)).L(X(4.5), Z(12.3)).L(X(6.3), Z(12.25))
    .L(X(6.3), Z(10.65)).L(X(4.45), Z(10.7)));
  線(p => p.M(X(6.9), Z(8.5)).L(X(6.9), Z(15.8)));
  線(p => p.M(X(7.4), Z(8.45)).L(X(7.4), Z(15.75)));

  // 兄叩四聯（口首＋垂足）
  const 叩 = (cx: number) => {
    線(p => p.M(X(cx - 0.55), Z(13.0)).L(X(cx - 0.55), Z(14.0)).L(X(cx + 0.55), Z(13.95))
      .L(X(cx + 0.55), Z(12.95)).L(X(cx - 0.6), Z(13.0)));
    線(p => p.M(X(cx - 0.4), Z(14.2)).C(X(cx - 0.6), Z(14.9), X(cx - 0.8), Z(15.4), X(cx - 1.05), Z(15.7)));
    線(p => p.M(X(cx + 0.4), Z(14.2)).C(X(cx + 0.5), Z(14.9), X(cx + 0.55), Z(15.4), X(cx + 0.55), Z(15.8)));
  };
  叩(1.15); 叩(2.5); 叩(3.85); 叩(5.15);

  // 底匚＋迴脚
  線(p => p.M(X(0.6), Z(16.1)).L(X(0.6), Z(17.7)).L(X(3.7), Z(17.6))
    .L(X(3.7), Z(16.05)));
  線(p => p.M(X(0.7), Z(16.9)).L(X(3.6), Z(16.8)));
  線(p => p.M(X(5.0), Z(16.0)).C(X(4.9), Z(17.0), X(5.1), Z(17.7), X(5.7), Z(18.1)));
  線(p => p.M(X(7.2), Z(16.0)).C(X(7.3), Z(17.0), X(7.1), Z(17.8), X(6.6), Z(18.3)));
};
