// ─────────────────────────────────────────────────────────────────────────────
// 道法會元卷十二・雷霆聞命符（雨冠首） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3843_卷12雷霆聞命清微令符-07.png（右起首符）
// 原文：「右符雷霆聞命……」（其後發用之語祕傳不錄，記位卷十二）——組符各以雨冠字領首
// （本符首字雨冠，隸定〔待核〕；組內又見霊・霆等冠），下承雲篆蛇身。
// 錨自 24 指格網（雷_wenming.png），仿射 k=1.7, cx0=4.2, z00=5。幅比 2.1。
// 黃素朱書 → 硃層；發用之制屬行持，祕傳不錄（記位卷12）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 雷霆聞命符: 符筆 = (bi) => {
  const u = bi.u, k = 1.7, cx0 = 4.2, z00 = 5.0;
  const X = (x: number) => (x - cx0) * k;
  const Z = (z: number) => (z - z00) * k + 2;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.62) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 楷 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.6) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);

  bi.硃(() => {
    // 雨冠首字（隸定待核——雨冠＋下部，依圖版摹形）
    楷(p => p.M(X(2.6), Z(5.4)).L(X(4.6), Z(5.3)));                 // 一
    楷(p => p.M(X(2.4), Z(6.0)).L(X(2.4), Z(7.6)));                 // 冂左
    楷(p => p.M(X(2.4), Z(6.0)).L(X(4.8), Z(5.9)).L(X(4.8), Z(7.5)));// 冂右
    楷(p => p.M(X(3.6), Z(5.9)).L(X(3.6), Z(7.7)), 0.5);            // 中豎
    for (const [dx, dz] of [[-0.6, 0.5], [0.5, 0.45], [-0.6, 1.2], [0.5, 1.15]] as const)
      bi.dot(X(3.6 + dx), Z(6.2 + dz), 0.22 * k);                   // 四點
    楷(p => p.M(X(2.5), Z(8.2))
      .C(X(3.3), Z(8.0), X(4.3), Z(8.0), X(5.0), Z(8.2))
      .C(X(5.1), Z(8.9), X(4.8), Z(9.5), X(4.2), Z(9.8)), 0.55);    // 下部乛曲
    楷(p => p.M(X(3.4), Z(8.4))
      .C(X(3.2), Z(9.2), X(2.9), Z(9.8), X(2.5), Z(10.2)), 0.5);    // 下部撇〔摹形〕

    // 蛇身・頂波：左起右行，末端上鉤
    抖(p => p.M(X(-0.6), Z(14.9))
      .C(X(0.6), Z(13.9), X(2.0), Z(13.9), X(3.4), Z(14.3))
      .C(X(5.2), Z(14.7), X(7.0), Z(14.4), X(8.2), Z(14.2))
      .C(X(8.8), Z(14.0), X(9.0), Z(13.6), X(8.9), Z(13.2)));
    // 左瀑：疊波直下（夾髮迴折，幅寬）
    抖(p => p.M(X(-0.4), Z(16.0))
      .C(X(2.2), Z(16.4), X(2.6), Z(17.6), X(0.4), Z(18.2))
      .C(X(-1.4), Z(18.7), X(-1.2), Z(20.0), X(0.8), Z(20.4))
      .C(X(2.8), Z(20.8), X(2.8), Z(22.0), X(0.6), Z(22.5))
      .C(X(-1.2), Z(22.9), X(-1.0), Z(24.1), X(0.9), Z(24.5)));
    // 左瀑（下段）
    抖(p => p.M(X(1.0), Z(25.4))
      .C(X(-1.2), Z(25.9), X(-1.2), Z(27.2), X(0.8), Z(27.6))
      .C(X(2.6), Z(28.0), X(2.6), Z(29.3), X(0.5), Z(29.7))
      .C(X(-1.3), Z(30.1), X(-1.1), Z(31.4), X(0.4), Z(31.8))
      .C(X(1.0), Z(32.1), X(1.2), Z(32.7), X(1.0), Z(33.1)));
    // 右瀑：幅更廣（夾髮迴折，中含 U 灣）
    抖(p => p.M(X(4.6), Z(16.2))
      .C(X(7.4), Z(16.4), X(9.6), Z(16.6), X(9.4), Z(17.9))
      .C(X(9.2), Z(19.1), X(6.4), Z(19.0), X(6.4), Z(20.3))
      .C(X(6.4), Z(21.6), X(9.6), Z(21.4), X(9.4), Z(22.8))
      .C(X(9.2), Z(24.1), X(6.6), Z(23.9), X(6.8), Z(25.1)));
    抖(p => p.M(X(6.9), Z(26.0))
      .C(X(6.2), Z(27.4), X(6.6), Z(28.7), X(7.9), Z(29.4))
      .C(X(9.6), Z(30.3), X(10.4), Z(30.9), X(10.1), Z(32.0))
      .C(X(9.9), Z(32.8), X(8.9), Z(33.2), X(7.9), Z(33.2)));
    // 中短波（二瀑之間，S 形長之）
    抖(p => p.M(X(3.8), Z(19.8))
      .C(X(5.2), Z(20.5), X(3.6), Z(21.7), X(5.0), Z(22.5))
      .C(X(6.0), Z(23.1), X(5.6), Z(23.9), X(4.6), Z(24.3)), 0.6);
  });
};
