// ─────────────────────────────────────────────────────────────────────────────
// 上清三台神符（二，右起次符） · 親筆
// 據圖版：參攷-local/紫微諱/樣頁/樣_1430_天樞院回車畢道正法卷上三台神符-p31.png
// （左緣三符柱之中；行持說明與咒記位不錄；黃綾書＝書材註記）
// 判準：楷意疊書符柱——●騎點首桿＋与頭亖五＋亐形（中豎左鉤）＋土形＋
// 塔亖五遞寬＋亖四貫豎＋目框＋反形＋桁四貫中豎中●＋乙鉤六（2×3）＋
// 咼曲群（左豎鉤中咼內乙右乙底皿）＋与三承大乙＋己大曲二重＋底乙長垂曲尾。
// 摹形從簡。錨自 6 格柱裁圖（格柱_柱二，52 行）：X=(c-2.9)×1.38、
// Z=(r-0.8)×1.38+1.1。幅比 3。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 三台神符二: 符筆 = (bi) => {
  const u = bi.u, k = 1.38;
  const X = (c: number) => (c - 2.9) * k;
  const Z = (r: number) => (r - 0.8) * k + 1.1;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.28) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.3) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 點 = (x: number, z: number, r = 0.3) =>
    bi.塊(p => p.M(X(x - r), Z(z)).C(X(x - r), Z(z - r * 1.3), X(x + r), Z(z - r * 1.3), X(x + r), Z(z))
      .C(X(x + r), Z(z + r * 1.3), X(x - r), Z(z + r * 1.3), X(x - r), Z(z)).close());
  const 乙 = (x0: number, z0: number, w = 1.4, h = 1.7, 寬 = 0.28) =>
    線(p => p.M(X(x0), Z(z0))
      .C(X(x0 + w * 0.9), Z(z0 - 0.1), X(x0 + w), Z(z0 + h * 0.35), X(x0 + w * 0.45), Z(z0 + h * 0.5))
      .C(X(x0), Z(z0 + h * 0.65), X(x0 + 0.1), Z(z0 + h), X(x0 + w * 0.7), Z(z0 + h)), 寬);

  // ●騎點首桿
  線(p => p.M(X(0.35), Z(1.1)).L(X(5.7), Z(1.0)), 0.32);
  點(3.2, 1.0, 0.36);
  // 与頭亖五（右与曲）
  for (const z of [1.9, 2.6, 3.3, 4.0, 4.6])
    線(p => p.M(X(0.35), Z(z)).L(X(3.4), Z(z - 0.03)), 0.28);
  乙(3.8, 1.6, 1.8, 2.0, 0.3);
  線(p => p.M(X(3.7), Z(3.0)).L(X(5.6), Z(2.95)), 0.28);

  // 亐形（桁＋中豎左鉤＋右短桁）
  線(p => p.M(X(0.5), Z(5.1)).L(X(3.3), Z(5.05)), 0.3);
  出(p => p.M(X(2.0), Z(4.8)).L(X(1.98), Z(6.2))
    .C(X(1.96), Z(6.5), X(1.7), Z(6.55), X(1.4), Z(6.4)), 0.3);
  線(p => p.M(X(3.6), Z(5.3)).L(X(5.5), Z(5.25)), 0.26);
  線(p => p.M(X(3.6), Z(6.1)).L(X(5.5), Z(6.05)), 0.26);

  // 土形
  線(p => p.M(X(0.6), Z(8.2)).L(X(3.0), Z(8.15)), 0.3);
  線(p => p.M(X(1.8), Z(7.9)).L(X(1.78), Z(9.2)), 0.3);
  線(p => p.M(X(0.5), Z(9.25)).L(X(3.1), Z(9.2)), 0.3);
  線(p => p.M(X(3.6), Z(8.4)).L(X(5.4), Z(8.35)), 0.26);

  // 塔亖五遞寬（左緣豎）
  線(p => p.M(X(1.4), Z(9.9)).L(X(4.2), Z(9.87)), 0.28);
  線(p => p.M(X(1.2), Z(10.6)).L(X(4.4), Z(10.57)), 0.28);
  線(p => p.M(X(1.0), Z(11.3)).L(X(4.6), Z(11.27)), 0.28);
  線(p => p.M(X(0.8), Z(12.0)).L(X(4.8), Z(11.97)), 0.28);
  線(p => p.M(X(0.6), Z(12.7)).L(X(5.0), Z(12.67)), 0.3);
  線(p => p.M(X(1.3), Z(9.9)).L(X(1.28), Z(12.7)), 0.28);

  // 亖四貫中豎
  for (const z of [13.6, 14.3, 15.0, 15.7])
    線(p => p.M(X(0.5), Z(z)).L(X(5.0), Z(z - 0.04)), 0.28);
  線(p => p.M(X(2.6), Z(13.6)).L(X(2.58), Z(15.7)), 0.28);

  // 目框
  線(p => p.M(X(0.9), Z(16.5)).L(X(0.9), Z(19.3)).L(X(4.8), Z(19.25))
    .L(X(4.8), Z(16.45)).L(X(0.85), Z(16.5)), 0.3);
  線(p => p.M(X(1.0), Z(17.4)).L(X(4.7), Z(17.36)), 0.26);
  線(p => p.M(X(1.0), Z(18.3)).L(X(4.7), Z(18.26)), 0.26);

  // 反形
  線(p => p.M(X(0.9), Z(19.9)).L(X(4.4), Z(19.85)), 0.3);
  出(p => p.M(X(1.0), Z(19.93)).C(X(0.95), Z(20.5), X(0.9), Z(21.0), X(0.85), Z(21.5)), 0.28);
  出(p => p.M(X(2.2), Z(20.4)).C(X(3.0), Z(20.85), X(3.9), Z(21.3), X(4.6), Z(21.6)), 0.28);

  // 桁四貫中豎（中●）
  線(p => p.M(X(0.4), Z(22.3)).L(X(5.6), Z(22.25)), 0.3);
  線(p => p.M(X(0.5), Z(23.2)).L(X(5.4), Z(23.15)), 0.28);
  線(p => p.M(X(0.6), Z(24.1)).L(X(5.3), Z(24.05)), 0.28);
  線(p => p.M(X(0.7), Z(25.0)).L(X(5.2), Z(24.95)), 0.28);
  線(p => p.M(X(2.9), Z(22.3)).L(X(2.88), Z(26.0)), 0.3);
  點(2.9, 24.6, 0.26);

  // 乙鉤六（2×3）
  乙(0.5, 26.6); 乙(2.35, 26.5); 乙(3.95, 26.7);
  乙(0.6, 28.9); 乙(2.25, 28.8); 乙(3.85, 29.0);

  // 咼曲群：左豎鉤＋中咼（內乙二）＋右乙＋底皿曲
  出(p => p.M(X(0.9), Z(30.9)).L(X(0.88), Z(33.9))
    .C(X(0.86), Z(34.2), X(0.65), Z(34.3), X(0.45), Z(34.2)), 0.3);
  線(p => p.M(X(1.8), Z(31.1)).L(X(1.8), Z(33.0)).L(X(4.2), Z(32.95))
    .L(X(4.2), Z(31.05)).L(X(1.75), Z(31.1)), 0.28);
  乙(2.2, 31.4, 1.1, 1.2, 0.24); 乙(2.4, 32.0, 1.1, 0.9, 0.24);
  乙(4.4, 31.2, 1.2, 1.6, 0.26);
  線(p => p.M(X(1.5), Z(33.4))
    .C(X(1.4), Z(34.1), X(1.9), Z(34.45), X(2.9), Z(34.4))
    .C(X(3.8), Z(34.35), X(4.3), Z(34.0), X(4.3), Z(33.5)), 0.28);

  // 与三承大乙
  乙(0.5, 34.95, 1.4, 1.6); 乙(2.4, 34.85, 1.3, 1.6); 乙(4.0, 34.95, 1.4, 1.6);
  線(p => p.M(X(0.8), Z(37.0))
    .C(X(3.4), Z(36.85), X(4.9), Z(37.3), X(3.4), Z(37.8))
    .C(X(2.0), Z(38.25), X(2.6), Z(38.5), X(4.6), Z(38.4)), 0.3);

  // 己大曲二重
  for (const z0 of [38.9, 41.5])
    線(p => p.M(X(0.7), Z(z0))
      .C(X(4.4), Z(z0 - 0.15), X(5.1), Z(z0 + 0.75), X(2.9), Z(z0 + 1.0))
      .C(X(0.9), Z(z0 + 1.25), X(0.9), Z(z0 + 1.95), X(3.1), Z(z0 + 2.1))
      .C(X(4.3), Z(z0 + 2.18), X(5.0), Z(z0 + 2.3), X(5.1), Z(z0 + 2.4)), 0.3);

  // 底乙長垂曲尾
  乙(1.2, 44.5, 2.6, 1.9, 0.3);
  出(p => p.M(X(2.8), Z(46.7)).L(X(2.78), Z(50.4))
    .C(X(2.76), Z(50.9), X(2.5), Z(51.15), X(2.1), Z(51.2)), 0.32);
};
