// ─────────────────────────────────────────────────────────────────────────────
// 道法會元卷十二・清微令符（二） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3843_卷12雷霆聞命清微令符-07.png（「二」標）
// 原文：清微令符旁注「青素朱書」→全符硃層；組凡三道，此其二。
// 判準：一氣大蟒——入flick波折→右奔大切返（右U・左U・再右大彎）→
// 降曲垂左尾；別出左腿（中腰分行，虛斷點綴）雙足並落。
// 幅比 1.2（z 0..28.8）。錨自 12 格裁圖（格_qw2）：X=(col-5.8)×1.75、
// Z=(row-1.0)×1.75+2。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 清微令符二: 符筆 = (bi) => {
  const u = bi.u, k = 1.75;
  const X = (c: number) => (c - 5.8) * k;
  const Z = (r: number) => (r - 1.0) * k + 2;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.74) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  bi.硃(() => {
    // 大蟒一氣：flick→切返→降曲→左尾
    抖(p => p.M(X(2.4), Z(1.3))
      .C(X(2.9), Z(1.8), X(3.6), Z(2.4), X(4.4), Z(2.9))
      .C(X(5.4), Z(3.4), X(6.6), Z(3.2), X(7.6), Z(3.3))
      .C(X(8.5), Z(3.4), X(8.8), Z(4.0), X(8.4), Z(4.6))
      .C(X(7.6), Z(5.5), X(5.4), Z(5.2), X(4.0), Z(5.4))
      .C(X(3.0), Z(5.6), X(2.7), Z(6.2), X(3.1), Z(6.8))
      .C(X(3.6), Z(7.3), X(5.4), Z(6.9), X(6.7), Z(6.9))
      .C(X(7.8), Z(6.9), X(8.9), Z(7.0), X(9.2), Z(7.8))
      .C(X(9.5), Z(8.6), X(9.0), Z(9.6), X(8.4), Z(10.6))
      .C(X(7.8), Z(11.6), X(7.4), Z(12.4), X(6.9), Z(13.2))
      .C(X(6.4), Z(14.0), X(5.9), Z(14.5), X(5.4), Z(14.6)), 0.78);
    // 左腿別出（中腰分行）
    抖(p => p.M(X(5.2), Z(7.0))
      .C(X(4.7), Z(8.2), X(4.0), Z(9.4), X(3.8), Z(10.6))
      .C(X(3.6), Z(11.5), X(3.4), Z(12.3), X(3.2), Z(12.9)), 0.72);
    // 虛斷點綴（腿間殘墨）
    bi.dot(X(4.6), Z(9.2), 0.28);
    bi.dot(X(4.4), Z(10.4), 0.26);
    bi.dot(X(4.3), Z(11.6), 0.26);
  });
};
