// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭蛇蟲作諸恠祟 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p183_符葉三.jpg（下段右三）
// 判準：星紋符頭（桿三・八字大撇對綴圈）＋箱陣帶（匚目・田・目）＋雙目
// 疊中杙＋弓曲厂張足。錨自 12 格裁圖（宅183_B shechong）：X=(c-3.95)×2.7、
// Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅蛇蟲: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.32 * k, Z(z)).L(X(x1) - 0.32 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：桿三＋八字大撇對綴圈
  桿(0.6, 7.2, 1.0);
  桿(0.6, 7.2, 2.4);
  桿(0.6, 7.2, 3.8);
  線(p => p.M(X(2.6), Z(4.9)).C(X(2.1), Z(5.8), X(1.7), Z(6.6), X(1.4), Z(7.3)), 0.42);
  線(p => p.M(X(4.3), Z(4.9)).C(X(4.8), Z(5.8), X(5.3), Z(6.6), X(5.7), Z(7.2)), 0.42);
  圈(1.1, 5.8); 圈(6.4, 5.9);

  // 箱陣帶：匚目・田・目
  線(p => p.M(X(1.0), Z(8.2)).L(X(7.0), Z(8.1)), 0.42);
  線(p => p.M(X(1.1), Z(8.6)).L(X(1.1), Z(11.0)).L(X(3.3), Z(10.95))
    .L(X(3.3), Z(8.55)));
  線(p => p.M(X(1.2), Z(9.8)).L(X(3.2), Z(9.75)), 0.34);
  線(p => p.M(X(3.7), Z(8.6)).L(X(3.7), Z(10.9)).L(X(5.1), Z(10.87))
    .L(X(5.1), Z(8.55)).L(X(3.65), Z(8.6)));
  線(p => p.M(X(4.4), Z(8.6)).L(X(4.38), Z(10.88)), 0.32);
  線(p => p.M(X(3.75), Z(9.75)).L(X(5.05), Z(9.72)), 0.32);
  線(p => p.M(X(5.5), Z(8.6)).L(X(5.5), Z(10.85)).L(X(7.0), Z(10.8))
    .L(X(7.0), Z(8.5)).L(X(5.45), Z(8.6)));
  線(p => p.M(X(5.6), Z(9.7)).L(X(6.9), Z(9.67)), 0.32);

  // 雙目疊中杙
  const 梯 = (x0: number, x1: number) => {
    線(p => p.M(X(x0), Z(11.6)).L(X(x0), Z(15.5)));
    線(p => p.M(X(x1), Z(11.55)).L(X(x1), Z(15.45)));
    for (let i = 0; i <= 2; i++)
      線(p => p.M(X(x0), Z(11.6 + i * 1.95)).L(X(x1), Z(11.55 + i * 1.95)));
  };
  梯(1.1, 3.4); 梯(4.6, 6.9);
  線(p => p.M(X(4.0), Z(11.7)).L(X(3.97), Z(15.4)), 0.34);

  // 弓曲＋厂張足
  線(p => p.M(X(1.3), Z(15.9))
    .C(X(0.9), Z(16.5), X(1.1), Z(17.1), X(1.8), Z(17.15))
    .C(X(2.4), Z(17.2), X(2.6), Z(16.7), X(2.2), Z(16.45)), 0.36);
  線(p => p.M(X(4.8), Z(15.9))
    .C(X(4.4), Z(16.5), X(4.6), Z(17.1), X(5.3), Z(17.15))
    .C(X(5.9), Z(17.2), X(6.1), Z(16.7), X(5.7), Z(16.45)), 0.36);
  線(p => p.M(X(1.5), Z(17.7)).C(X(1.3), Z(18.3), X(1.2), Z(18.8), X(1.2), Z(19.2)));
  線(p => p.M(X(6.2), Z(17.6)).C(X(6.4), Z(18.2), X(6.5), Z(18.7), X(6.5), Z(19.1)));
};
