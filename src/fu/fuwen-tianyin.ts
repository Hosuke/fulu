// 複文・天天因 · 親筆
// 據圖版：參攷-local/複文/太平經符_Talismans_in_Taipingjing.jpg（左半右起第3行第4格）
// 判準：雙小天並肩居上，下承因匡（口含大）——三字合體。楷隸筆意。
// 幅比 1.15（z 0..27.6）。釋讀〔待核〕，名從構件形貌。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 複文天天因: 符筆 = (bi) => {
  const u = bi.u;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.8) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 曳 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.9) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 雙小天並肩
  for (const cx of [-3.4, 3.4]) {
    橫(p => p.M(cx - 1.6, 4.2).L(cx + 1.6, 4.0), 0.7);
    橫(p => p.M(cx - 2.3, 6.0).L(cx + 2.3, 5.8), 0.75);
    曳(p => p.M(cx + 0.1, 5.9).C(cx - 0.7, 7.2, cx - 1.7, 8.4, cx - 2.9, 9.4), 0.8);
    曳(p => p.M(cx + 0.1, 6.0).C(cx + 0.8, 7.2, cx + 1.8, 8.3, cx + 3.0, 9.1), 0.8);
  }

  // 因匡：口含大
  橫(p => p.M(-4.6, 11.5).L(-4.5, 21.6), 0.9);
  橫(p => p.M(4.6, 11.3).L(4.5, 21.6), 0.9);
  橫(p => p.M(-4.7, 11.6).L(4.7, 11.4), 0.85);
  橫(p => p.M(-4.6, 21.5).L(4.6, 21.4), 0.9);
  橫(p => p.M(-2.9, 14.2).L(2.9, 14.0), 0.8);
  曳(p => p.M(0.0, 14.1).C(-1.1, 16.5, -2.5, 18.7, -4.1, 20.9));
  曳(p => p.M(0.0, 14.2).C(1.1, 16.5, 2.5, 18.6, 4.1, 20.7));
};
