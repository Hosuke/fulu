// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・宜官職斬伏尸為禍 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p182_符葉二.jpg（下段右二）
// 判準：星紋符頭（波桿二頂圈・直桿二・斜圈鏈・散圈〔漫漶錄可辨〕）＋寬迴帶＋
// 雙目田箱＋螺蟠尾二・中垂尾・叉足。錨自 12 格裁圖（宅182_B yiguan）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅宜官: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：波桿頂圈＋桿二＋斜圈鏈＋散圈
  線(p => p.M(X(1.0), Z(1.2)).C(X(1.9), Z(0.85), X(2.8), Z(0.78), X(3.22), Z(0.9)));
  線(p => p.M(X(4.1), Z(0.9)).C(X(5.1), Z(1.0), X(6.2), Z(1.1), X(6.7), Z(1.15)));
  圈(0.7, 1.25); 圈(3.65, 0.82); 圈(7.0, 1.15);
  線(p => p.M(X(1.0), Z(1.9)).C(X(2.5), Z(1.6), X(4.5), Z(1.6), X(6.6), Z(1.85)));
  圈(0.7, 1.95); 圈(6.9, 1.9);
  線(p => p.M(X(1.0), Z(2.6)).L(X(6.8), Z(2.45))); 圈(0.7, 2.6); 圈(7.1, 2.45);
  線(p => p.M(X(1.0), Z(4.0)).L(X(6.9), Z(3.85))); 圈(0.7, 4.0); 圈(7.2, 3.85);
  連([5.9, 4.5], [6.9, 5.3]); 圈(5.9, 4.5); 圈(6.9, 5.3);
  for (const [x, z] of [[1.0, 5.4], [2.5, 6.0], [1.5, 7.0], [3.2, 7.2], [4.5, 7.4], [5.4, 6.4]] as const)
    圈(x, z);

  // 寬迴帶（boustro圓折三桁）
  線(p => p.M(X(1.0), Z(7.9)).L(X(6.3), Z(7.85))
    .C(X(6.8), Z(7.85), X(6.8), Z(8.8), X(6.3), Z(8.85))
    .L(X(1.8), Z(8.9)).C(X(1.3), Z(8.9), X(1.3), Z(9.85), X(1.8), Z(9.9))
    .L(X(6.3), Z(9.85)).C(X(6.8), Z(9.85), X(6.9), Z(10.4), X(6.8), Z(10.8)));

  // 雙目田箱
  const 箱 = (x0: number, x1: number) => {
    線(p => p.M(X(x0), Z(10.8)).L(X(x0), Z(13.6)).L(X(x1), Z(13.55))
      .L(X(x1), Z(10.75)).L(X(x0 - 0.05), Z(10.8)));
    線(p => p.M(X(x0 + 0.1), Z(12.2)).L(X(x1 - 0.1), Z(12.15)));
    線(p => p.M(X((x0 + x1) / 2), Z(10.85)).L(X((x0 + x1) / 2 - 0.04), Z(13.55)), 0.34);
  };
  箱(1.1, 3.5);
  箱(4.3, 6.7);

  // 螺蟠尾二＋中垂尾＋叉足
  const 蟠 = (x0: number) => 線(p => p.M(X(x0), Z(13.9))
    .C(X(x0 - 0.6), Z(14.6), X(x0 - 0.5), Z(15.5), X(x0 + 0.2), Z(15.9))
    .C(X(x0 + 0.8), Z(16.2), X(x0 + 0.9), Z(16.9), X(x0 + 0.3), Z(17.2))
    .C(X(x0 - 0.1), Z(17.4), X(x0 - 0.3), Z(17.1), X(x0 - 0.1), Z(16.9)));
  蟠(1.9); 蟠(5.7);
  線(p => p.M(X(3.9), Z(13.8)).C(X(3.8), Z(15.3), X(3.9), Z(16.8), X(3.85), Z(18.2)));
  線(p => p.M(X(1.3), Z(17.9)).C(X(1.2), Z(18.5), X(1.2), Z(18.9), X(1.25), Z(19.2)));
  線(p => p.M(X(6.5), Z(17.8)).C(X(6.6), Z(18.4), X(6.6), Z(18.8), X(6.55), Z(19.1)));
};
