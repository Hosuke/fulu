// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭鎮凶惡之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p181_符葉一.jpg（中段右一）
// 判準：星紋符頭（桿二・人字圈鏈・圈陣五五四）＋手風琴桁帶＋雙目梯柱＋
// 冂張脚。錨自 12 格裁圖（宅181_A z1）：X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。
// 幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅鎮凶: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.32 * k, Z(z)).L(X(x1) - 0.32 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：桿二＋人字圈鏈＋圈陣
  桿(0.5, 7.4, 1.0);
  桿(0.5, 7.2, 2.3);
  線(p => p.M(X(2.88), Z(3.38)).L(X(3.70), Z(3.13)));
  線(p => p.M(X(4.50), Z(3.11)).L(X(5.22), Z(3.30)));
  圈(2.5, 3.5); 圈(4.1, 3.0); 圈(5.6, 3.4);
  for (const [x, z] of [[0.7, 4.4], [2.3, 4.6], [4.1, 4.5], [5.8, 4.4], [7.3, 4.3],
                        [0.7, 5.7], [2.4, 5.9], [4.1, 5.8], [5.9, 5.7], [7.3, 5.6],
                        [1.4, 7.0], [3.0, 7.2], [4.7, 7.1], [6.2, 7.0]] as const)
    圈(x, z);

  // 手風琴桁帶（外匡內桁三）
  線(p => p.M(X(0.6), Z(8.3)).L(X(0.6), Z(11.2)).L(X(7.5), Z(11.1))
    .L(X(7.5), Z(8.2)).L(X(0.55), Z(8.3)), 0.42);
  for (const z of [9.0, 9.7, 10.4]) 線(p => p.M(X(0.7), Z(z)).L(X(7.4), Z(z - 0.1)));

  // 雙目梯柱（各三箱）
  const 梯 = (x0: number, x1: number, z0: number, 層: number) => {
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z0 + 層 * 1.6)));
    線(p => p.M(X(x1), Z(z0 - 0.05)).L(X(x1), Z(z0 + 層 * 1.6 - 0.05)));
    for (let i = 0; i <= 層; i++)
      線(p => p.M(X(x0), Z(z0 + i * 1.6)).L(X(x1), Z(z0 + i * 1.6 - 0.06)));
  };
  梯(0.8, 3.2, 11.7, 3);
  梯(4.4, 7.0, 11.7, 3);

  // 冂張脚
  線(p => p.M(X(0.8), Z(16.6)).C(X(0.5), Z(17.4), X(0.3), Z(18.0), X(0.2), Z(18.5)));
  線(p => p.M(X(7.0), Z(16.6)).C(X(7.3), Z(17.4), X(7.5), Z(18.0), X(7.6), Z(18.5)));
  線(p => p.M(X(2.4), Z(16.7)).L(X(2.4), Z(18.2)));
  線(p => p.M(X(5.4), Z(16.7)).L(X(5.4), Z(18.2)));
};
