// 判準：散形逐件與繫名（名錄於譜，此惟摹形）：一（天帝釋章）・一（佩帶天剛）・
// 丿（五方凶惡之鬼何不消亡）・丿（飛仙一吸萬鬼伏藏）・乚（開天門）・
// 「（閉地戶）・一（留人門）・乚（塞鬼路）＋日月雙螺旋（念日君七煞呪・
// 念月君七縛呪，呪文記位不錄）＋星點（尾出）。
// 佈局承原葉相對之位而略並（文欄既去）：右列前四、左列後四、下列三光。
// 幅比 1.35（z 0..32.4）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 三光散形: 符筆 = (bi) => {
  const u = bi.u;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.8) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // ── 右列（自上而下）──
  // 一（天帝釋章）
  線(p => p.M(2.6, 3.4).C(4.8, 3.2, 7.0, 3.1, 9.2, 3.3));
  // 一（佩帶天剛）
  線(p => p.M(2.6, 7.6).C(4.8, 7.4, 7.0, 7.3, 9.2, 7.5));
  // 丿（五方凶惡之鬼何不消亡）
  線(p => p.M(8.8, 11.0).C(7.4, 12.6, 5.8, 14.1, 4.0, 15.4), 0.9);
  // 丿（飛仙一吸萬鬼伏藏）
  線(p => p.M(9.0, 17.2).C(7.6, 18.9, 6.0, 20.4, 4.2, 21.8), 0.9);

  // ── 左列（自上而下）──
  // 乚（開天門）
  線(p => p.M(-8.6, 2.8).L(-8.5, 6.0).C(-8.4, 6.9, -7.7, 7.3, -6.4, 7.3)
    .C(-5.8, 7.3, -5.3, 7.1, -4.9, 6.7), 0.85);
  // 「（閉地戶）
  線(p => p.M(-8.8, 9.4).C(-8.9, 8.9, -8.6, 8.6, -8.0, 8.6)
    .L(-4.6, 8.5).L(-4.5, 11.6), 0.85);
  // 一（留人門）
  線(p => p.M(-8.8, 14.2).C(-7.2, 14.0, -5.6, 13.9, -4.2, 14.1));
  // 乚（塞鬼路）
  線(p => p.M(-8.6, 16.6).L(-8.5, 19.8).C(-8.4, 20.7, -7.7, 21.1, -6.4, 21.1)
    .C(-5.8, 21.1, -5.3, 20.9, -4.9, 20.5), 0.85);

  // ── 下列：三光（日螺旋・星點・月螺旋）──
  const 螺 = (cx: number, cz: number, r0: number) =>
    bi.一筆({ 寬: 0.5 * u, 起: '尖', 收: '出' }, p => {
      p.M(cx + r0, cz);
      const 圈數 = 2.6, 步 = 44;
      for (let i = 1; i <= 步; i++) {
        const t = i / 步, a = t * 圈數 * Math.PI * 2, r = r0 * (1 - 0.88 * t);
        p.L(cx + r * Math.cos(a), cz + r * Math.sin(a));
      }
    });
  螺(-6.2, 26.6, 2.5);   // 日（念日君七煞呪之位）
  螺(6.2, 26.6, 2.5);    // 月（念月君七縛呪之位）
  // 星點（實墨，尾出上左）
  const K = 0.5523, r = 1.35, cx = 0, cz = 27.0;
  bi.塊(t => t.M(cx + r, cz)
    .C(cx + r, cz + K * r, cx + K * r, cz + r, cx, cz + r)
    .C(cx - K * r, cz + r, cx - r, cz + K * r, cx - r, cz)
    .C(cx - r, cz - K * r, cx - K * r, cz - r, cx, cz - r)
    .C(cx + K * r, cz - r, cx + r, cz - K * r, cx + r, cz)
    .close());
  線(p => p.M(-0.9, 26.0).C(-1.5, 25.3, -1.9, 24.6, -2.1, 23.9), 0.55);
};
