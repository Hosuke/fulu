// ─────────────────────────────────────────────────────────────────────────────
// 三光符・聚形 · 親筆
// 據圖版：參攷-local/敕令符頭/樣頁/（自 NLC-9797 冊4258 PDF p31 抽葉親見；
// 太上助國救民總真祕要 DZ1227，序 1116；「聚形」標名原印於葉）
// 判準：三光（日・月・星）之聚形——人字檐三圈（圈為日月之位）＋天形字架
// （二橫貫豎、儿足）＋冂大框＋三芒星團（實墨大點出三角芒）；散形逐件
// p30–31（開天門・閉地戶・留人門・塞鬼路・念日君月君呪記位不錄）。
// 錨自 24 指格網（字_sanguang格.png）：X=(col-13.6)×1.3、Z=(row-7)×1.3+2。
// 幅比 2.0（z 0..48）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 三光聚形: 符筆 = (bi) => {
  const u = bi.u;
  const k = 1.3;
  const X = (c: number) => (c - 13.6) * k;
  const Z = (r: number) => (r - 7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.6) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 鈍 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.62) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (c: number, r: number, 半: number) => {
    const ctx = bi.ctx; ctx.save(); ctx.lineWidth = 0.5 * u;
    bi.A(X(c), Z(r), 半 * k); ctx.restore();
  };

  // ── 人字檐三圈（日月星之位）──
  圈(12.5, 7.2, 0.6);
  圈(6.3, 12.3, 0.65);
  圈(20.7, 12.2, 0.65);
  // 檐線（止於圈緣）
  線(p => p.M(X(12.05), Z(7.75)).L(X(6.85), Z(11.75)), 0.62);
  線(p => p.M(X(12.95), Z(7.75)).L(X(20.15), Z(11.65)), 0.62);

  // ── 天形字架 ──
  // 斜挑（貫上桁）
  線(p => p.M(X(14.0), Z(14.8)).C(X(13.0), Z(15.6), X(12.1), Z(16.4), X(11.2), Z(17.1)), 0.5);
  // 上桁（左端垂鉤起，端鈍如木刻）
  鈍(p => p.M(X(6.0), Z(17.4)).L(X(6.05), Z(16.6))
    .C(X(6.1), Z(16.3), X(6.4), Z(16.2), X(6.8), Z(16.2))
    .L(X(20.9), Z(15.9)), 0.66);
  // 下桁（左端垂鉤起，端鈍）
  鈍(p => p.M(X(6.0), Z(20.3)).L(X(6.05), Z(19.5))
    .C(X(6.1), Z(19.2), X(6.4), Z(19.1), X(6.8), Z(19.1))
    .L(X(21.2), Z(18.8)), 0.66);
  // 中豎入左足
  線(p => p.M(X(13.0), Z(15.4)).L(X(13.0), Z(20.9))
    .C(X(13.0), Z(21.8), X(12.5), Z(22.6), X(11.6), Z(23.1))
    .C(X(11.2), Z(23.4), X(10.7), Z(23.5), X(10.2), Z(23.6)), 0.66);
  // 右足
  線(p => p.M(X(13.0), Z(20.6)).C(X(13.3), Z(21.9), X(14.1), Z(22.9), X(15.3), Z(23.4)), 0.64);

  // ── 冂大框（底端外撇）──
  線(p => p.M(X(5.6), Z(40.9)).C(X(6.3), Z(40.6), X(6.6), Z(40.0), X(6.6), Z(39.0))
    .L(X(6.6), Z(24.6))
    .C(X(6.6), Z(23.6), X(7.1), Z(23.2), X(8.1), Z(23.1))
    .L(X(19.4), Z(22.9))
    .C(X(20.4), Z(22.9), X(20.9), Z(23.3), X(20.9), Z(24.3))
    .L(X(20.9), Z(39.2))
    .C(X(20.9), Z(40.2), X(21.2), Z(40.7), X(21.9), Z(41.0)), 0.8);

  // ── 三芒星團（實墨） ──
  const K = 0.5523, r = 3.95 * k, cx = X(13.4), cz = Z(35.2);
  bi.塊(t => t.M(cx + r, cz)
    .C(cx + r, cz + K * r, cx + K * r, cz + r, cx, cz + r)
    .C(cx - K * r, cz + r, cx - r, cz + K * r, cx - r, cz)
    .C(cx - r, cz - K * r, cx - K * r, cz - r, cx, cz - r)
    .C(cx + K * r, cz - r, cx + r, cz - K * r, cx + r, cz)
    .close());
  // 三芒（上・左・右）
  bi.塊(t => t.M(X(13.5), Z(29.3)).L(X(12.5), Z(32.3)).L(X(14.7), Z(32.2)).close());
  bi.塊(t => t.M(X(7.7), Z(32.8)).C(X(9.0), Z(33.0), X(10.0), Z(33.3), X(10.6), Z(33.7)).L(X(9.8), Z(36.0)).C(X(9.0), Z(34.9), X(8.3), Z(33.8), X(7.7), Z(32.8)).close());
  bi.塊(t => t.M(X(18.9), Z(32.6)).C(X(17.7), Z(32.9), X(16.8), Z(33.2), X(16.2), Z(33.6)).L(X(17.0), Z(35.8)).C(X(17.7), Z(34.7), X(18.3), Z(33.6), X(18.9), Z(32.6)).close());
};
