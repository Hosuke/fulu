// ─────────────────────────────────────────────────────────────────────────────
// 老君入山符・丙五（篇末組第五符・末葉） · 親筆
// 據圖版：四庫全卷 PDF p058 抽葉（參攷-local/入山符/，欽定四庫全書繕本，PD）
// 判準：頂冠（雙弧翼夾中卵）→寬箱三桁（雙括脅）→王桁小構→蟠曲迷紋
// （左回紋鏈・中乂與目环・右大蟠鏈綴環——逐曲釋讀〔待核〕，摹其勢與
// 曲數）→斜貫雙桁→底段（尸桁・己曲・中垂尾・右蟠尾・雙撇張脚）。
// 丙組材色未言，不施硃。幅比 1.5（z 0..36）。
// 錨：X=(col-12.2)×1.05、Z=(row-1.1)×1.05（格_bing5）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 入山符丙五: 符筆 = (bi) => {
  const u = bi.u;
  const k = 1.05;
  const X = (c: number) => (c - 12.2) * k;
  const Z = (r: number) => (r - 1.1) * k;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // ── 頂冠：雙弧翼夾中卵 ──
  線(p => p.M(X(6.2), Z(2.2)).C(X(7.8), Z(1.8), X(9.6), Z(1.7), X(10.9), Z(1.9)), 0.55);
  線(p => p.M(X(6.4), Z(3.3)).C(X(8.0), Z(2.9), X(9.7), Z(2.8), X(10.9), Z(3.0)), 0.5);
  線(p => p.M(X(12.9), Z(1.9)).C(X(14.2), Z(1.7), X(16.0), Z(1.8), X(17.6), Z(2.3)), 0.55);
  線(p => p.M(X(12.9), Z(3.0)).C(X(14.1), Z(2.8), X(15.8), Z(2.9), X(17.4), Z(3.4)), 0.5);
  線(p => p.M(X(11.9), Z(1.6))
    .C(X(11.2), Z(2.3), X(11.2), Z(3.3), X(11.9), Z(4.0))
    .C(X(12.6), Z(3.3), X(12.6), Z(2.3), X(11.9), Z(1.6)), 0.5);

  // ── 寬箱三桁（雙括脅） ──
  出(p => p.M(X(6.5), Z(5.2)).C(X(6.1), Z(8.0), X(6.2), Z(11.0), X(6.2), Z(13.8)), 0.6);
  出(p => p.M(X(17.7), Z(4.9)).C(X(18.1), Z(7.7), X(18.0), Z(10.7), X(18.1), Z(13.5)), 0.6);
  線(p => p.M(X(6.6), Z(5.1)).L(X(17.7), Z(4.8)), 0.58);
  線(p => p.M(X(7.5), Z(6.1)).L(X(17.5), Z(5.8)), 0.55);
  線(p => p.M(X(7.7), Z(7.1)).L(X(17.3), Z(6.8)), 0.55);
  線(p => p.M(X(7.8), Z(7.9)).L(X(13.0), Z(7.7)), 0.5);
  // 王桁小構
  線(p => p.M(X(7.9), Z(8.5)).L(X(7.9), Z(9.8)), 0.5);
  線(p => p.M(X(8.0), Z(8.7)).L(X(12.4), Z(8.5)), 0.5);
  線(p => p.M(X(8.1), Z(9.6)).L(X(12.3), Z(9.4)), 0.5);
  線(p => p.M(X(12.6), Z(8.3)).L(X(12.55), Z(9.5)), 0.5);

  // ── 蟠曲迷紋 ──
  // 左回紋鏈（己字疊二，幅張）
  出(p => p.M(X(8.5), Z(10.2))
    .C(X(7.5), Z(10.7), X(7.6), Z(11.7), X(8.6), Z(11.8))
    .L(X(11.0), Z(11.7))
    .C(X(12.0), Z(11.7), X(12.0), Z(12.7), X(11.0), Z(12.8))
    .L(X(8.6), Z(12.9))
    .C(X(7.7), Z(13.0), X(7.7), Z(13.9), X(8.6), Z(14.0))
    .L(X(9.8), Z(13.9)), 0.5);
  // 中乂
  出(p => p.M(X(8.6), Z(12.8)).C(X(9.3), Z(13.4), X(9.9), Z(13.9), X(10.5), Z(14.4)), 0.5);
  出(p => p.M(X(10.4), Z(12.7)).C(X(9.8), Z(13.3), X(9.1), Z(13.9), X(8.5), Z(14.5)), 0.5);
  // 目环（雙圈連桁）
  線(p => p.M(X(10.9), Z(13.0))
    .C(X(10.4), Z(13.5), X(10.4), Z(14.1), X(10.9), Z(14.5))
    .C(X(11.4), Z(14.1), X(11.4), Z(13.5), X(10.9), Z(13.0)), 0.45);
  線(p => p.M(X(12.6), Z(12.9))
    .C(X(12.1), Z(13.4), X(12.1), Z(14.0), X(12.6), Z(14.4))
    .C(X(13.1), Z(14.0), X(13.1), Z(13.4), X(12.6), Z(12.9)), 0.45);
  線(p => p.M(X(11.2), Z(13.4)).L(X(12.3), Z(13.35)), 0.42);
  線(p => p.M(X(11.2), Z(14.0)).L(X(12.3), Z(13.95)), 0.42);
  // 右大蟠鏈（己曲疊三綴小環）
  出(p => p.M(X(14.0), Z(9.9))
    .C(X(13.3), Z(10.5), X(13.4), Z(11.4), X(14.2), Z(11.5))
    .L(X(16.2), Z(11.4))
    .C(X(17.0), Z(11.4), X(17.0), Z(12.3), X(16.2), Z(12.4))
    .L(X(14.4), Z(12.5))
    .C(X(13.6), Z(12.6), X(13.6), Z(13.5), X(14.4), Z(13.6))
    .L(X(15.6), Z(13.5)), 0.5);
  bi.ctx.save(); bi.ctx.lineWidth = 0.4 * u; bi.A(X(15.7), Z(13.3), 0.4 * k); bi.ctx.restore();
  出(p => p.M(X(16.4), Z(13.6)).C(X(17.1), Z(14.2), X(17.0), Z(15.0), X(16.2), Z(15.3))
    .C(X(15.4), Z(15.6), X(15.3), Z(16.4), X(16.0), Z(16.7)), 0.5);

  // 中左桁箱・惢曲
  線(p => p.M(X(7.5), Z(15.5)).L(X(12.3), Z(15.2)), 0.55);
  線(p => p.M(X(7.6), Z(16.3)).L(X(12.4), Z(16.0)), 0.55);
  出(p => p.M(X(7.5), Z(15.6)).C(X(7.2), Z(16.4), X(7.3), Z(17.2), X(7.6), Z(17.9)), 0.5);
  出(p => p.M(X(9.9), Z(17.8)).C(X(9.5), Z(18.3), X(9.1), Z(18.8), X(8.7), Z(19.2)), 0.5);
  出(p => p.M(X(10.1), Z(18.0)).C(X(10.5), Z(18.4), X(10.9), Z(18.7), X(11.2), Z(19.0)), 0.5);

  // 下段乂
  出(p => p.M(X(12.4), Z(18.7)).C(X(13.2), Z(19.4), X(13.9), Z(20.0), X(14.6), Z(20.7)), 0.55);
  出(p => p.M(X(14.4), Z(18.6)).C(X(13.7), Z(19.3), X(13.0), Z(20.0), X(12.3), Z(20.7)), 0.55);

  // 蟠曲垂尾三道（左・中・右）
  出(p => p.M(X(8.2), Z(20.4))
    .C(X(7.2), Z(21.3), X(7.4), Z(22.3), X(8.5), Z(22.5))
    .C(X(9.5), Z(22.7), X(9.6), Z(23.7), X(8.6), Z(24.0))
    .C(X(7.7), Z(24.3), X(7.7), Z(25.3), X(8.4), Z(25.7))
    .C(X(9.0), Z(26.0), X(9.0), Z(26.6), X(8.5), Z(26.9)), 0.5);
  出(p => p.M(X(12.2), Z(20.9))
    .C(X(11.2), Z(21.9), X(11.6), Z(23.0), X(12.7), Z(23.2))
    .C(X(13.8), Z(23.4), X(13.9), Z(24.5), X(12.9), Z(24.9))
    .C(X(12.0), Z(25.3), X(12.2), Z(26.3), X(13.3), Z(26.8))
    .C(X(13.9), Z(27.1), X(13.9), Z(27.7), X(13.4), Z(28.0)), 0.5);
  出(p => p.M(X(15.6), Z(17.8))
    .C(X(16.7), Z(18.8), X(17.9), Z(19.2), X(18.0), Z(20.3))
    .C(X(18.1), Z(21.4), X(16.8), Z(21.8), X(16.9), Z(22.9))
    .C(X(17.0), Z(24.0), X(18.2), Z(24.3), X(18.1), Z(25.3))
    .C(X(18.0), Z(26.1), X(17.2), Z(26.4), X(17.2), Z(27.0)), 0.5);

  // ── 斜貫雙桁 ──
  線(p => p.M(X(8.7), Z(26.8)).L(X(17.7), Z(25.6)), 0.58);
  線(p => p.M(X(8.8), Z(27.6)).L(X(17.6), Z(26.4)), 0.58);

  // ── 底段 ──
  // 尸桁雙弧
  線(p => p.M(X(9.5), Z(28.6)).C(X(11.5), Z(28.9), X(14.5), Z(28.7), X(17.3), Z(28.2)), 0.55);
  線(p => p.M(X(9.6), Z(29.5)).C(X(11.6), Z(29.8), X(14.4), Z(29.6), X(17.2), Z(29.1)), 0.55);
  // 己曲（左下）
  出(p => p.M(X(10.2), Z(30.5))
    .C(X(9.6), Z(31.1), X(9.8), Z(31.9), X(10.7), Z(32.0))
    .C(X(11.7), Z(32.1), X(12.0), Z(31.4), X(11.4), Z(30.9))
    .C(X(10.8), Z(31.9), X(11.0), Z(32.6), X(12.5), Z(32.5)), 0.5);
  // 中垂尾
  出(p => p.M(X(13.0), Z(28.5)).C(X(12.6), Z(30.2), X(13.0), Z(31.9), X(12.6), Z(33.6)), 0.55);
  // 右蟠尾
  出(p => p.M(X(14.8), Z(28.7))
    .C(X(14.3), Z(29.7), X(14.7), Z(30.6), X(15.6), Z(30.7))
    .C(X(16.4), Z(30.8), X(16.5), Z(31.6), X(15.9), Z(32.0))
    .C(X(15.4), Z(32.4), X(15.6), Z(33.0), X(16.1), Z(33.2)), 0.5);
  // 左右張脚
  出(p => p.M(X(7.6), Z(24.5)).C(X(7.0), Z(27.5), X(6.8), Z(30.3), X(6.3), Z(32.8)), 0.62);
  出(p => p.M(X(17.9), Z(24.8)).C(X(18.4), Z(27.3), X(18.8), Z(29.7), X(19.2), Z(31.8)), 0.62);
  出(p => p.M(X(19.5), Z(30.4)).C(X(19.8), Z(31.2), X(20.1), Z(31.9), X(20.3), Z(32.6)), 0.5);
};
