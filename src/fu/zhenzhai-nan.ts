// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭南方土氣害人口 · 親筆（五方土氣組・南）
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p183_符葉三.jpg（上排右二）
// 判準：三圈桿符頭（長短相間）＋橫列小圈七＋散圈二列＋2×2 匣身承滿幅帶＋
// 雙纓環鏈足（左右各四環長曳）。錨自 24 指格網（宅_nan.png），X=x、Z=z-4。
// 幅比 2.0。原印右緣稍漫漶，依殘跡補全注〔簡〕。釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅南方: 符筆 = (bi) => {
  const u = bi.u;
  const X = (x: number) => x;
  const Z = (z: number) => z - 4;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.34) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.42) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.26 * u;
    bi.A(X(x), Z(z), r); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.55, Z(z)).L(X(x1) - 0.55, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：三圈桿（長短相間）＋右孤圈
  桿(-8.0, -2.4, 7.0);
  桿(-5.6, 1.5, 8.1);
  桿(-8.0, -3.2, 9.4);
  圈(3.9, 7.1, 0.4); 圈(3.8, 10.2, 0.4);

  // 橫列小圈七＋散圈
  for (const x of [-8.2, -6.6, -5.0, -3.4, -1.8, -0.2, 1.2]) 圈(x, 13.3, 0.34);
  圈(3.9, 12.9, 0.4);
  圈(-8.1, 15.6, 0.42); 圈(3.8, 15.4, 0.4);
  for (const x of [-8.2, -5.6, -3.0, -0.6, 1.6, 3.8]) 圈(x, 18.2, 0.38);

  // 2×2 匣身（中縫豎），承滿幅帶
  線(p => p.M(X(-8.2), Z(19.9)).L(X(-2.0), Z(19.8)).L(X(-2.0), Z(23.2)).L(X(-8.2), Z(23.3)).L(X(-8.2), Z(19.9)));
  線(p => p.M(X(-8.0), Z(21.6)).L(X(-2.2), Z(21.5)), 0.38);
  線(p => p.M(X(-1.2), Z(19.7)).L(X(4.6), Z(19.6)).L(X(4.6), Z(23.0)).L(X(-1.2), Z(23.1)).L(X(-1.2), Z(19.7)));
  線(p => p.M(X(-1.0), Z(21.4)).L(X(4.4), Z(21.3)), 0.38);
  線(p => p.M(X(-8.3), Z(24.1)).L(X(-2.0), Z(24.0)).L(X(-2.0), Z(27.4)).L(X(-8.3), Z(27.5)).L(X(-8.3), Z(24.1)));
  線(p => p.M(X(-8.1), Z(25.8)).L(X(-2.2), Z(25.7)), 0.38);
  線(p => p.M(X(-1.2), Z(23.9)).L(X(4.7), Z(23.8)).L(X(4.7), Z(27.2)).L(X(-1.2), Z(27.3)).L(X(-1.2), Z(23.9)));
  線(p => p.M(X(-1.0), Z(25.6)).L(X(4.5), Z(25.5)), 0.38);
  // 滿幅帶（二桁＋中縫）
  線(p => p.M(X(-8.4), Z(28.3)).L(X(4.8), Z(28.1)).L(X(4.8), Z(31.3)).L(X(-8.4), Z(31.5)).L(X(-8.4), Z(28.3)), 0.46);
  線(p => p.M(X(-8.2), Z(29.8)).L(X(4.6), Z(29.7)), 0.38);
  線(p => p.M(X(-1.6), Z(28.2)).L(X(-1.6), Z(31.4)), 0.36);

  // 雙纓環鏈足（左右各四環，尾長曳）
  const 鏈 = (起x: number, 起z: number, 環: Array<[number, number]>, 尾: [number, number]) =>
    bi.一筆({ 寬: 0.4 * u, 起: '藏', 收: '出' }, p => {
      p.M(X(起x), Z(起z));
      for (const [ex, ez] of 環) {
        p.C(X(ex + 0.95), Z(ez - 0.7), X(ex - 1.0), Z(ez - 0.85), X(ex - 0.8), Z(ez + 0.15));
        p.C(X(ex - 0.55), Z(ez + 0.85), X(ex + 0.75), Z(ez + 0.7), X(ex + 0.5), Z(ez - 0.2));
      }
      p.C(X(尾[0] + 1.3), Z(尾[1] - 1.4), X(尾[0] + 0.6), Z(尾[1] - 0.5), X(尾[0]), Z(尾[1]));
    });
  鏈(-3.2, 31.8, [[-5.4, 33.2], [-3.4, 34.8], [-5.6, 36.3], [-3.6, 37.8]], [-7.4, 39.9]);
  鏈(2.4, 31.6, [[0.4, 33.0], [2.4, 34.6], [0.2, 36.1], [2.2, 37.6]], [-1.4, 39.7]);
};
