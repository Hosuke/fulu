// 〈歘火律令鄧天君大法〉7a，NLC 冊3909 原刻，1445）
// 判準：聚形＝諸散形合成之全像（Meulenbeld 80.7a；「聚形」字樣未印於葉，
// 術語出學術——考據卷 2.1 補記）。圖之構：雷神黑體全像（獸首鬣張・白目・
// 張吻・執刃・躍步爪足）＋雨點五＋連環雲帶二道＋星斗連線圖。
// 旁列律令之文祕傳不錄（記位 80.7a）。錨自 24 指格網（雷_juxing.png），
// X=x、Z=z 直入紙。幅比 2.45。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 雷神聚形: 符筆 = (bi) => {
  const u = bi.u;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 雨點五（頭圓尾銳上指）
  const 雨點 = (x: number, z: number, r: number, 尾z: number) => {
    bi.塊(t => t.M(x, 尾z)
      .C(x + 0.28, 尾z + (z - 尾z) * 0.55, x + r, z - r * 0.9, x + r, z)
      .C(x + r, z + r, x - r, z + r, x - r, z)
      .C(x - r, z - r * 0.9, x - 0.28, 尾z + (z - 尾z) * 0.55, x, 尾z)
      .close());
  };
  雨點(-8.2, 13.8, 0.8, 11.5);
  雨點(-3.4, 13.2, 0.9, 10.9);
  雨點(2.4, 13.6, 0.8, 11.5);
  雨點(-9.4, 18.1, 0.7, 15.7);
  雨點(4.2, 17.9, 0.7, 15.4);

  // 雷神黑體全像（一塊；順時針自鬣起）
  bi.塊(t => t.M(-1.4, 21.7)
    // 鬣（脊鋸）
    .L(-0.8, 22.5).L(-0.2, 21.9).L(0.3, 22.6).L(1.0, 22.3).L(1.3, 23.1)
    .C(1.3, 23.7, 1.1, 24.2, 0.9, 24.6)
    // 尾帶（背出右曲，內鉤）
    .C(2.0, 24.9, 2.9, 25.9, 3.1, 27.2)
    .C(3.2, 28.1, 2.6, 28.7, 1.9, 28.4)
    .C(2.3, 29.1, 2.4, 29.6, 2.1, 29.9)
    .C(1.5, 29.4, 1.0, 28.4, 0.8, 27.2)
    // 軀右緣下行入右腿
    .C(0.6, 29.2, 0.6, 30.6, 1.0, 32.0)
    .C(1.5, 33.6, 2.2, 36.0, 2.7, 38.2)
    .C(2.8, 38.7, 2.9, 39.1, 3.0, 39.3)
    // 右足爪（三趾）
    .L(3.8, 39.3).L(3.5, 39.8).L(4.0, 40.1).L(3.3, 40.3).L(3.2, 40.7)
    .C(2.6, 40.3, 2.1, 40.0, 1.9, 39.6)
    // 右腿內緣上收至胯
    .C(1.4, 37.6, 0.8, 35.4, 0.0, 33.8)
    .C(-0.4, 33.2, -0.7, 32.9, -1.0, 32.8)
    // 左腿（前屈）：大腿左下・膝・脛右下至足
    .C(-2.4, 32.6, -4.2, 32.6, -5.3, 33.1)
    .C(-5.9, 33.5, -6.0, 34.2, -5.6, 34.7)
    .C(-5.0, 35.3, -4.4, 35.7, -3.9, 35.9)
    // 左足爪（三趾）
    .L(-3.1, 35.5).L(-3.0, 36.1).L(-2.5, 36.3).L(-3.1, 36.7).L(-3.3, 37.1)
    .C(-4.4, 36.9, -5.6, 36.4, -6.3, 35.6)
    .C(-6.8, 34.9, -6.7, 34.0, -6.1, 33.2)
    .C(-5.2, 32.1, -4.0, 31.5, -3.2, 31.3)
    // 臀背左緣上行（背豐）
    .C(-3.8, 30.4, -3.9, 28.8, -3.7, 27.6)
    // 左臂：肩・肘外張・前臂上舉至拳
    .C(-4.1, 27.1, -4.7, 26.7, -5.1, 26.2)
    .C(-5.5, 25.7, -5.8, 25.2, -5.9, 24.8)
    .C(-5.5, 24.6, -5.1, 24.7, -4.7, 25.0)
    .C(-4.2, 25.4, -3.7, 25.7, -3.3, 25.8)
    // 腋・頸前・頷
    .C(-3.1, 25.4, -3.0, 25.1, -3.0, 24.9)
    // 張吻（下顎尖・口凹・上吻尖）
    .L(-4.3, 24.6).L(-3.7, 24.1).L(-4.6, 23.7)
    // 吻上緣回眉・額
    .C(-3.9, 23.2, -3.2, 23.0, -2.7, 23.0)
    .C(-2.5, 22.6, -2.3, 22.2, -2.1, 21.9)
    .C(-1.9, 21.7, -1.6, 21.6, -1.4, 21.7)
    .close());
  // 白目（鑿）
  bi.鑿(t => {
    const k = 0.5523, r = 0.3, x = -2.35, z = 23.15;
    t.M(x + r, z).C(x + r, z + k * r, x + k * r, z + r, x, z + r)
      .C(x - k * r, z + r, x - r, z + k * r, x - r, z)
      .C(x - r, z - k * r, x - k * r, z - r, x, z - r)
      .C(x + k * r, z - r, x + r, z - k * r, x + r, z).close();
  });
  // 刃（拳中長刃指左下）
  bi.塊(t => t.M(-5.6, 24.25).L(-8.9, 28.15).L(-8.35, 28.75).L(-5.05, 24.95).close());

  // 連環雲帶（右長帶・左短帶——沿路綴環）
  const 帶 = (起: [number, number], 段: Array<[number, number] | { 環: [number, number] }>, 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, p => {
      p.M(起[0], 起[1]);
      let 前: [number, number] = 起;
      for (const s of 段) {
        if (Array.isArray(s)) {
          p.C(前[0] + (s[0] - 前[0]) * 0.4, 前[1] + (s[1] - 前[1]) * 0.2,
              前[0] + (s[0] - 前[0]) * 0.7, 前[1] + (s[1] - 前[1]) * 0.85, s[0], s[1]);
          前 = s;
        } else {
          const [ex, ez] = s.環;
          p.C(ex + 0.85, ez - 0.65, ex - 0.9, ez - 0.8, ex - 0.7, ez + 0.15);
          p.C(ex - 0.5, ez + 0.75, ex + 0.65, ez + 0.6, ex + 0.45, ez - 0.2);
          前 = [ex + 0.45, ez - 0.2];
        }
      }
    });
  帶([4.8, 15.6], [[5.5, 18.6], { 環: [8.2, 20.8] }, { 環: [7.6, 23.2] }, { 環: [6.6, 25.6] },
    [7.2, 28.5], [7.3, 32.0], [7.0, 35.0], { 環: [6.6, 36.6] }, [6.3, 38.6], { 環: [6.7, 40.3] },
    [5.6, 41.8], { 環: [4.4, 42.7] }, { 環: [2.9, 42.9] }, { 環: [1.4, 42.6] }, [-1.5, 42.4], [-3.6, 42.0]]);
  帶([-10.2, 29.3], [[-10.1, 31.6], { 環: [-9.2, 33.9] }, { 環: [-8.3, 35.3] },
    { 環: [-7.2, 36.8] }, { 環: [-6.1, 38.1] }, [-5.2, 38.7]]);

  // 星斗連線圖（五曜方陣＋珠鏈）
  const 圈 = (x: number, z: number, r = 0.42) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(x, z, r); c.restore();
  };
  const 星線 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t0 = 0.5 / L;
    線(p => p.M(a[0] + dx * t0, a[1] + dz * t0).L(b[0] - dx * t0, b[1] - dz * t0), 0.45);
  };
  const TL: [number, number] = [-5.8, 50.3], TR: [number, number] = [4.9, 50.0],
        BL: [number, number] = [-5.6, 54.7], BR: [number, number] = [4.6, 54.5],
        C0: [number, number] = [-0.6, 52.3];
  星線(TL, TR); 星線(TL, BL); 星線(BL, BR); 星線(TR, C0); 星線(TL, C0);
  for (const s of [TL, TR, BL, BR, C0]) 圈(s[0], s[1], 0.48);
  // 珠鏈（小圈五綴，自上而垂接中星）
  let pz = 46.6;
  for (let i = 0; i < 5; i++) {
    圈(-0.75 - 0.05 * i, pz, 0.26);
    if (i < 4) 線(p => p.M(-0.75 - 0.05 * i, pz + 0.28).L(-0.8 - 0.05 * i, pz + 0.85), 0.35);
    pz += 1.12;
  }
  星線([-0.95, 51.1], C0);
};
