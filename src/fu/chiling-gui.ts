// 筆目承次開右端（p30）：天帝釋章・佩帶天剛・五方凶惡之鬼何不消亡・飛仙一吸・
// 萬鬼伏藏・天剛大聖收入鬼心（存見上天節）——葉端前筆漫漶，錄其可辨者；
// 本繪依畫序親書楷體成字（同敕字式之裁）。幅比 1.2（z 0..28.8）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鬼字式: 符筆 = (bi) => {
  const u = bi.u;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.9) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.0) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 丿（頂撇）
  出(p => p.M(0.7, 3.0).C(-0.3, 3.9, -1.5, 4.8, -2.9, 5.5), 0.9);
  // ── 田部 ──
  橫(p => p.M(-5.8, 6.6).L(-5.9, 14.0));
  橫(p => p.M(-5.8, 6.7).L(4.6, 6.4).L(4.7, 13.8));
  橫(p => p.M(-5.8, 10.3).L(4.6, 10.1), 0.8);
  橫(p => p.M(-0.6, 6.6).L(-0.65, 13.9), 0.8);
  橫(p => p.M(-5.9, 14.0).L(4.7, 13.8), 0.85);
  // ── 儿部 ──
  // 丿（左足長撇）
  出(p => p.M(-2.8, 14.3).C(-3.6, 17.4, -5.0, 20.8, -7.4, 23.6)
    .C(-8.0, 24.3, -8.7, 24.9, -9.4, 25.4), 1.1);
  // 乚（右足豎彎鉤，收上挑）
  出(p => p.M(2.6, 14.2).L(2.8, 21.4)
    .C(2.9, 23.4, 4.0, 24.6, 6.2, 24.9)
    .C(7.2, 25.0, 8.2, 24.6, 9.0, 23.8), 1.15);
  // ── 厶部（足間）──
  出(p => p.M(-1.4, 17.8).C(-2.4, 19.4, -3.5, 20.9, -4.8, 22.3)
    .C(-3.4, 22.1, -2.0, 21.8, -0.7, 21.4), 0.85);
  出(p => p.M(-0.9, 19.2).C(-0.2, 20.2, 0.4, 21.2, 0.9, 22.3), 0.85);
};
