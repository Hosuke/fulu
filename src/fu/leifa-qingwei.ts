// 道法會元卷十二・清微令符（三） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3843_卷12雷霆聞命清微令符-07.png（清微令符
// 標題左之「三」符；組凡三道，此其三——全形最清者）。旁注色制：青素朱書。
// 判準：環首（橢環含雙波桁）→ 環尾下折二疊（冎狀）→ 雙曳尾（左長乁・右垂鉤）。
// 錨自 24 指格網（雷_qingwei2.png），仿射 k=2.2, cx0=-5.35, z00=7.5。幅比 1.65。
// 青素朱書 → 硃層；旁注發用之語祕傳不錄（記位卷12）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 清微令符: 符筆 = (bi) => {
  const u = bi.u, k = 2.2, cx0 = -5.35, z00 = 7.5;
  const X = (x: number) => (x - cx0) * k;
  const Z = (z: number) => (z - z00) * k + 2;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.62) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  bi.硃(() => {
    // 環首：橢環一周（起左肩，順時針，環尾出於右下接下段）
    抖(p => p.M(X(-7.6), Z(8.6))
      .C(X(-6.4), Z(7.6), X(-4.0), Z(7.4), X(-2.6), Z(8.0))
      .C(X(-1.4), Z(8.6), X(-1.2), Z(10.2), X(-1.6), Z(11.4))
      .C(X(-2.0), Z(12.6), X(-3.6), Z(13.2), X(-5.4), Z(13.1))
      .C(X(-7.2), Z(13.0), X(-8.4), Z(12.2), X(-8.5), Z(10.8))
      .C(X(-8.6), Z(9.8), X(-8.2), Z(9.0), X(-7.6), Z(8.6)), 0.8);
    // 環內雙波桁
    抖(p => p.M(X(-7.5), Z(9.6))
      .C(X(-6.6), Z(9.2), X(-5.8), Z(9.8), X(-5.0), Z(9.5))
      .C(X(-4.4), Z(9.3), X(-3.8), Z(9.5), X(-3.4), Z(9.8)), 0.65);
    抖(p => p.M(X(-7.4), Z(11.2))
      .C(X(-6.2), Z(10.8), X(-5.0), Z(11.3), X(-4.0), Z(11.0))
      .C(X(-3.4), Z(10.8), X(-2.9), Z(10.9), X(-2.6), Z(11.1)), 0.65);
    // 環尾下折二疊（冎狀）：自環右下出，左折、右折
    抖(p => p.M(X(-2.2), Z(12.6))
      .C(X(-2.6), Z(13.8), X(-4.2), Z(14.2), X(-5.6), Z(14.1))
      .C(X(-7.2), Z(14.0), X(-8.0), Z(14.8), X(-7.9), Z(15.9))
      .C(X(-7.8), Z(17.0), X(-6.6), Z(17.4), X(-5.2), Z(17.3))
      .C(X(-3.8), Z(17.2), X(-3.0), Z(17.8), X(-3.1), Z(18.9)), 0.8);
    // 左長乁尾
    抖(p => p.M(X(-5.0), Z(18.2))
      .C(X(-6.2), Z(19.2), X(-7.4), Z(20.6), X(-8.4), Z(21.9))
      .C(X(-9.0), Z(22.7), X(-9.5), Z(23.3), X(-9.7), Z(23.6)));
    // 右垂鉤尾
    抖(p => p.M(X(-3.3), Z(19.2))
      .C(X(-3.8), Z(20.6), X(-3.6), Z(22.0), X(-4.0), Z(23.2))
      .C(X(-4.1), Z(23.7), X(-4.2), Z(24.0), X(-4.3), Z(24.2)));
  });
};
