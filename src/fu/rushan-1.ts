// ─────────────────────────────────────────────────────────────────────────────
// 老君入山符・一 · 親筆
// 據圖版：參攷-local/入山符/抱朴子登涉_入山符_p031_符一.png（四庫繕本，PD）
// 判準：門框雙立下張・頂雙彡疊弧夾中隔・中列尸乙三聯＋目箱＋乙鉤・
// 末段連珠星陣（橫貫二星→星門→門內菱陣）。丹書桃板——全符硃層。
// 幅比 1.5（z 0..36）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 入山符一: 符筆 = (bi) => {
  const u = bi.u;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 星 = (x: number, z: number, r = 0.5) => bi.dot(x, z, r);

  bi.硃(() => {
    // 門框：左右雙立，下段外張
    線(p => p.M(-7.6, 4.2)
      .C(-8.5, 9.0, -8.7, 14.0, -8.2, 19.0)
      .C(-7.9, 23.0, -8.5, 26.5, -9.6, 29.5), 0.75);
    線(p => p.M(7.2, 3.0)
      .C(8.1, 8.0, 8.3, 13.5, 8.0, 18.5)
      .C(7.8, 22.5, 8.6, 26.0, 9.7, 28.8), 0.75);

    // 頂雙彡：左右各五疊弧（開向下），夾中隔
    for (let i = 0; i < 5; i++) {
      const z = 4.6 + i * 1.55;
      線(p => p.M(-6.3, z + 0.5).C(-4.6, z - 0.5, -2.4, z - 0.5, -0.9, z + 0.4));
    }
    for (let i = 0; i < 5; i++) {
      const z = 4.2 + i * 1.55;
      線(p => p.M(0.9, z + 0.5).C(2.4, z - 0.5, 4.6, z - 0.5, 6.3, z + 0.4));
    }
    線(p => p.M(-0.05, 3.6).L(-0.1, 11.6));
    // 左列再垂二寬弧
    線(p => p.M(-7.0, 12.9).C(-5.0, 11.9, -2.6, 11.9, -0.8, 12.8));
    線(p => p.M(-7.0, 14.4).C(-5.0, 13.4, -2.6, 13.4, -0.8, 14.3));

    // 中列：尸乙三聯（豎＋三桁＋乙鉤）
    for (const x0 of [-6.2, -2.4, 1.4]) {
      線(p => p.M(x0, 15.6).L(x0, 19.6));                       // 豎
      for (let j = 0; j < 3; j++) {
        const z = 16.1 + j * 1.15;
        線(p => p.M(x0 + 0.1, z).C(x0 + 1.0, z - 0.3, x0 + 2.0, z - 0.3, x0 + 2.8, z));
      }
      線(p => p.M(x0 + 0.4, 19.9)                               // 乙鉤
        .C(x0 + 0.2, 20.9, x0 + 0.8, 21.3, x0 + 1.7, 21.1)
        .C(x0 + 2.2, 21.0, x0 + 2.5, 20.7, x0 + 2.6, 20.3));
    }
    // 右聯之異：L 豎加橫桁
    線(p => p.M(5.0, 16.0).L(5.0, 21.5).C(5.0, 22.3, 5.5, 22.6, 6.3, 22.5));
    線(p => p.M(5.1, 18.3).L(6.9, 18.3));

    // 中下：孤乙鉤＋目箱（凹欄二桁）
    線(p => p.M(-4.6, 22.3)
      .C(-4.8, 23.3, -4.2, 23.7, -3.3, 23.5)
      .C(-2.8, 23.4, -2.5, 23.1, -2.4, 22.7));
    線(p => p.M(-1.5, 21.6).C(-1.9, 22.9, -1.9, 24.1, -1.5, 25.2)); // 目箱左欄（凹）
    線(p => p.M(2.6, 21.5).C(3.0, 22.8, 3.0, 24.0, 2.6, 25.1));     // 右欄（凹）
    線(p => p.M(-1.5, 21.8).C(-0.2, 21.5, 1.2, 21.5, 2.5, 21.8));   // 頂桁
    線(p => p.M(-1.6, 23.4).L(2.7, 23.4));                          // 中桁
    線(p => p.M(-1.5, 25.0).C(-0.2, 25.3, 1.2, 25.3, 2.5, 25.0));   // 底桁

    // 末段：連珠星陣
    星(0.2, 25.9, 0.42);
    線(p => p.M(-5.0, 27.2).L(5.2, 27.0), 0.5);
    星(-5.0, 27.2); 星(5.2, 27.0);
    星(0.2, 28.4, 0.42);
    // 星門：橫楣（中綴一星）＋雙柱垂星
    線(p => p.M(-5.3, 29.9).L(5.3, 29.7), 0.5);
    星(-5.3, 29.9); 星(0.15, 29.8, 0.42); 星(5.3, 29.7);
    線(p => p.M(-5.3, 29.9).L(-5.3, 34.4), 0.5);
    線(p => p.M(5.3, 29.7).L(5.3, 34.1), 0.5);
    星(-5.3, 34.6); 星(5.3, 34.3);
    // 門內菱陣（四星圍菱，聯以細線）
    線(p => p.M(0.1, 31.2).L(-2.3, 32.8).L(0.2, 34.6).L(2.5, 32.3).L(0.1, 31.2), 0.45);
    星(0.1, 31.2); 星(-2.3, 32.8); 星(0.2, 34.6); 星(2.5, 32.3);
  });
};
