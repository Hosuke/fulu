// ─────────────────────────────────────────────────────────────────────────────
// 太上符（道法會元卷一百二・「太上」二字符頭七符・五） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3931_卷102太上二字符頭七符-p07.png（右起第五）
// 判準：太上符頭通制（頂小鉤・大橫・左右長弧腳下垂入身・上字・敕曲核心
// ——十字桁貫豎承大与曲）＋大迴框摺（內十）・目田框對・迴摺蛇尾。「右七符用皁紙」書材註記；行持說明
// 與咒記位不錄；七符之六在葉（右起第一葉緣截斷僅存左緣，俟原影補〔待核〕）。
// 摹形從簡。錨自 8 格裁圖（格102_五 ts4）：X=(c-3.95)×2.35、
// Z=(r-2.1)×2.35+1.0。幅比 3。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 太上符五: 符筆 = (bi) => {
  const u = bi.u, k = 2.35;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 2.1) * k + 1.0;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.58) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 太上冠：頂小鉤・大橫・左右長弧腳・上字
  出(p => p.M(X(3.75), Z(2.3)).C(X(3.85), Z(2.7), X(3.95), Z(3.0), X(4.0), Z(3.2)), 0.5);
  線(p => p.M(X(0.55), Z(3.7)).L(X(7.25), Z(3.62)), 0.62);
  出(p => p.M(X(3.85), Z(3.75)).C(X(2.6), Z(4.9), X(1.3), Z(5.7), X(0.8), Z(6.7)).L(X(0.78), Z(10.4)), 0.6);
  出(p => p.M(X(4.05), Z(3.75)).C(X(5.3), Z(4.9), X(6.6), Z(5.7), X(7.1), Z(6.7)).L(X(7.08), Z(8.6)), 0.6);
  線(p => p.M(X(3.95), Z(4.2)).L(X(3.93), Z(6.1)), 0.55);
  // 冠腳彎合
  出(p => p.M(X(0.78), Z(10.4)).C(X(0.9), Z(10.9), X(1.7), Z(11.15), X(3.0), Z(11.1)), 0.58);
  出(p => p.M(X(7.08), Z(8.6)).C(X(7.0), Z(9.3), X(6.6), Z(9.7), X(6.1), Z(9.8)), 0.58);
  線(p => p.M(X(4.05), Z(5.0)).L(X(5.1), Z(4.96)), 0.5);
  線(p => p.M(X(2.5), Z(6.2)).L(X(5.6), Z(6.14)), 0.55);
  // 敕曲核心
  線(p => p.M(X(1.7), Z(8.6)).L(X(4.6), Z(8.54)), 0.55);
  線(p => p.M(X(2.95), Z(7.9)).L(X(2.93), Z(10.5)), 0.55);
  出(p => p.M(X(2.95), Z(9.8)).L(X(2.2), Z(10.6)), 0.45);
  出(p => p.M(X(3.0), Z(9.8)).L(X(3.7), Z(10.5)), 0.45);
  線(p => p.M(X(4.6), Z(8.1)).C(X(5.9), Z(8.0), X(6.3), Z(8.8), X(5.3), Z(9.1)).C(X(4.4), Z(9.4), X(4.5), Z(10.1), X(5.5), Z(10.2)).C(X(6.1), Z(10.26), X(6.3), Z(10.6), X(6.2), Z(10.9)), 0.55);
  // 大迴框摺（內十）
  線(p => p.M(X(6.4), Z(11.4)).C(X(1.3), Z(11.5), X(1.0), Z(12.4), X(2.2), Z(12.5)).L(X(5.4), Z(12.4)).C(X(6.5), Z(12.45), X(6.5), Z(13.4), X(5.4), Z(13.5)).L(X(1.7), Z(13.6)).L(X(1.68), Z(15.8)), 0.6);
  線(p => p.M(X(3.9), Z(13.7)).L(X(3.88), Z(15.7)), 0.5);
  線(p => p.M(X(2.4), Z(14.7)).L(X(5.5), Z(14.64)), 0.5);
  // 目田框對
  線(p => p.M(X(1.9), Z(16.3)).L(X(1.9), Z(18.6)).L(X(4.0), Z(18.55)).L(X(4.0), Z(16.25)).L(X(1.85), Z(16.3)), 0.55);
  線(p => p.M(X(2.0), Z(17.45)).L(X(3.9), Z(17.4)), 0.48);
  線(p => p.M(X(4.6), Z(16.25)).L(X(4.6), Z(18.5)).L(X(6.5), Z(18.45)).L(X(6.5), Z(16.2)).L(X(4.55), Z(16.25)), 0.55);
  線(p => p.M(X(5.55), Z(16.3)).L(X(5.53), Z(18.45)), 0.48);
  // 迴摺蛇尾
  線(p => p.M(X(6.3), Z(19.3)).C(X(1.6), Z(19.4), X(1.2), Z(20.2), X(2.4), Z(20.3)).L(X(5.3), Z(20.2)).C(X(6.4), Z(20.25), X(6.4), Z(21.1), X(5.3), Z(21.2)).L(X(2.2), Z(21.3)).C(X(1.2), Z(21.4), X(1.2), Z(22.2), X(2.4), Z(22.25)), 0.6);
  出(p => p.M(X(2.4), Z(22.3)).C(X(3.6), Z(22.5), X(4.6), Z(23.0), X(5.2), Z(23.6)), 0.58);
};
