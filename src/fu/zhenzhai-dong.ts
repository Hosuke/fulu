// 鎮宅靈符・厭東方土氣精耗鬼 · 親筆（五方土氣組・東）
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p184_符葉四.jpg（上排左一）
// 判準：圈桿梯符頭（七桿遞短＋右豎聯圈＋山形星）＋三匣並列承中帶＋雙蛇對摺各自成外緣長足。
// 錨自 24 指格網（宅_dong.png），X=x、Z=z-4 直入紙。幅比 2.0。釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅東方: 符筆 = (bi) => {
  const u = bi.u;
  const X = (x: number) => x;
  const Z = (z: number) => z - 4;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.34) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.42) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.26 * u;
    bi.A(X(x), Z(z), r); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number, 左圈 = true, 右圈 = true) => {
    線(p => p.M(X(x0) + (左圈 ? 0.55 : 0), Z(z)).L(X(x1) - (右圈 ? 0.55 : 0), Z(z)));
    if (左圈) 圈(x0, z); if (右圈) 圈(x1, z);
  };

  // 符頭：圈桿梯（七桿遞短）
  桿(-6.6, 5.0, 7.3);
  桿(-6.6, 4.7, 9.2);
  桿(-6.7, 2.7, 11.2);
  桿(-6.6, 2.6, 13.4);
  桿(-6.6, 2.5, 15.5);
  桿(-6.7, 2.3, 17.3);
  桿(-6.7, -3.6, 19.1);
  // 右豎聯圈＋孤圈
  圈(6.3, 11.5, 0.38);
  線(p => p.M(X(6.4), Z(14.5)).L(X(6.4), Z(17.4)));
  圈(6.4, 14.0); 圈(6.4, 17.9);
  // 山形星（頂圈射二斜，中含一圈）＋散圈
  線(p => p.M(X(1.3), Z(20.2)).L(X(-0.4), Z(21.4)));
  線(p => p.M(X(1.7), Z(20.2)).L(X(5.85), Z(21.05)));
  圈(1.5, 19.8); 圈(-0.7, 21.7); 圈(6.3, 21.2); 圈(1.6, 21.4, 0.36);
  圈(-6.7, 21.4, 0.36); 圈(0.6, 19.2, 0.36);

  // 符身：三匣並列（左一桁、中二桁、右二桁——四輪依原刻正拓撲，Codex 審 2026-07-12）
  線(p => p.M(X(-6.4), Z(22.8)).L(X(-2.4), Z(22.7)).L(X(-2.4), Z(26.0)).L(X(-6.4), Z(26.1)).L(X(-6.4), Z(22.8)));
  線(p => p.M(X(-6.2), Z(24.5)).L(X(-2.6), Z(24.4)));
  線(p => p.M(X(-1.6), Z(22.6)).L(X(2.2), Z(22.5)).L(X(2.2), Z(25.9)).L(X(-1.6), Z(26.0)).L(X(-1.6), Z(22.6)));
  線(p => p.M(X(-1.4), Z(23.8)).L(X(2.0), Z(23.7)));
  線(p => p.M(X(-1.4), Z(24.9)).L(X(2.0), Z(24.8)));
  線(p => p.M(X(2.8), Z(22.5)).L(X(6.6), Z(22.4)).L(X(6.6), Z(25.8)).L(X(2.8), Z(25.9)).L(X(2.8), Z(22.5)));
  線(p => p.M(X(3.0), Z(23.7)).L(X(6.4), Z(23.6)));
  線(p => p.M(X(3.0), Z(24.8)).L(X(6.4), Z(24.7)));
  // 中帶（中縫豎＋一桁）
  線(p => p.M(X(-6.6), Z(26.9)).L(X(6.8), Z(26.8)).L(X(6.8), Z(29.5)).L(X(-6.6), Z(29.6)).L(X(-6.6), Z(26.9)));
  線(p => p.M(X(-6.4), Z(28.2)).L(X(6.6), Z(28.1)));
  線(p => p.M(X(0.1), Z(26.9)).L(X(0.1), Z(29.5)));

  // 雙蛇對摺（自帶底中央雙莖出，各向外三折，末自成外緣長足）
  線(p => p.M(X(-0.5), Z(29.7))
    .C(X(-0.9), Z(30.3), X(-1.6), Z(30.6), X(-2.6), Z(30.7))
    .L(X(-5.2), Z(30.8))
    .C(X(-6.4), Z(30.9), X(-6.4), Z(32.3), X(-5.2), Z(32.4))
    .L(X(-2.4), Z(32.3))
    .C(X(-1.2), Z(32.3), X(-1.2), Z(33.7), X(-2.4), Z(33.8))
    .L(X(-5.2), Z(33.9))
    .C(X(-6.3), Z(34.0), X(-6.4), Z(34.9), X(-6.3), Z(35.6))
    .C(X(-6.2), Z(37.0), X(-6.2), Z(38.4), X(-6.3), Z(39.8))
    .C(X(-6.4), Z(40.3), X(-6.8), Z(40.6), X(-7.3), Z(40.7)), 0.5);
  線(p => p.M(X(0.9), Z(29.7))
    .C(X(1.3), Z(30.3), X(2.0), Z(30.6), X(3.0), Z(30.6))
    .L(X(5.4), Z(30.5))
    .C(X(6.6), Z(30.5), X(6.6), Z(31.9), X(5.4), Z(32.0))
    .L(X(2.6), Z(32.1))
    .C(X(1.4), Z(32.2), X(1.4), Z(33.6), X(2.6), Z(33.7))
    .L(X(5.4), Z(33.6))
    .C(X(6.5), Z(33.6), X(6.6), Z(34.5), X(6.6), Z(35.2))
    .C(X(6.6), Z(36.6), X(6.6), Z(38.0), X(6.7), Z(39.4))
    .C(X(6.8), Z(39.9), X(7.2), Z(40.2), X(7.7), Z(40.3)), 0.5);
};
