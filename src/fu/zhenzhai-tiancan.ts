// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・田蠶六畜蕃息 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p185_符葉五.jpg（中段右三）
// 判準：符頭（桿・井字大箱格＋左右圈柱）＋左目梯六箱・右柱之𥫗死大字形疊
// （摹形從簡）＋底桁。錨自 12 格裁圖（宅185_B tiancan）：X=(c-3.95)×2.7、
// Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅田蠶: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿＋井字大箱格＋左右圈柱
  線(p => p.M(X(0.9), Z(1.3)).L(X(6.4), Z(1.2))); 圈(0.6, 1.3); 圈(6.7, 1.2);
  線(p => p.M(X(1.2), Z(2.2)).L(X(1.2), Z(5.2)).L(X(4.6), Z(5.15))
    .L(X(4.6), Z(2.15)).L(X(1.15), Z(2.2)));
  線(p => p.M(X(2.3), Z(2.25)).L(X(2.28), Z(5.15)), 0.32);
  線(p => p.M(X(3.5), Z(2.2)).L(X(3.48), Z(5.1)), 0.32);
  線(p => p.M(X(1.3), Z(3.2)).L(X(4.5), Z(3.15)), 0.32);
  線(p => p.M(X(1.3), Z(4.2)).L(X(4.5), Z(4.15)), 0.32);
  for (const z of [2.5, 3.6, 4.7]) 圈(5.1, z);
  for (const z of [2.4, 3.5, 4.6]) 圈(0.8, z);

  // 左目梯六箱
  線(p => p.M(X(0.9), Z(6.2)).L(X(0.9), Z(16.2)));
  線(p => p.M(X(3.4), Z(6.15)).L(X(3.4), Z(16.15)));
  for (let i = 0; i <= 6; i++)
    線(p => p.M(X(0.9), Z(6.2 + i * 1.67)).L(X(3.4), Z(6.15 + i * 1.67)));

  // 右柱：之𥫗死大字形疊（摹形從簡）
  出(p => p.M(X(4.6), Z(6.7)).C(X(4.4), Z(7.2), X(4.6), Z(7.6), X(5.2), Z(7.6))
    .C(X(5.7), Z(7.6), X(5.9), Z(7.2), X(5.6), Z(7.0))
    .C(X(5.2), Z(7.8), X(5.4), Z(8.4), X(6.1), Z(8.6)), 0.36);
  for (const x0 of [4.7, 5.7] as const) {
    出(p => p.M(X(x0), Z(9.2)).C(X(x0 - 0.3), Z(9.7), X(x0 - 0.4), Z(10.1), X(x0 - 0.4), Z(10.4)), 0.32);
    線(p => p.M(X(x0), Z(9.7)).L(X(x0 - 0.02), Z(10.6)), 0.32);
  }
  // 死形
  線(p => p.M(X(4.4), Z(11.3)).L(X(6.3), Z(11.25)), 0.36);
  出(p => p.M(X(4.8), Z(11.6)).C(X(4.5), Z(12.4), X(4.6), Z(13.0), X(5.1), Z(13.4)), 0.34);
  出(p => p.M(X(5.6), Z(11.6)).L(X(5.6), Z(12.7))
    .C(X(5.6), Z(13.3), X(6.0), Z(13.5), X(6.5), Z(13.35)), 0.34);
  // 大形
  線(p => p.M(X(4.5), Z(14.3)).L(X(6.2), Z(14.25)), 0.36);
  出(p => p.M(X(5.4), Z(14.0)).C(X(5.1), Z(15.0), X(4.8), Z(15.8), X(4.4), Z(16.4)), 0.36);
  出(p => p.M(X(5.5), Z(14.7)).C(X(5.8), Z(15.4), X(6.1), Z(16.0), X(6.4), Z(16.4)), 0.36);

  // 底桁
  線(p => p.M(X(1.0), Z(17.2)).L(X(6.4), Z(17.1)), 0.42);
  線(p => p.M(X(1.0), Z(18.2)).L(X(6.4), Z(18.1)), 0.42);
};
