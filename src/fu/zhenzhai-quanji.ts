// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭犬雞狐狸鳴上屋 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p183_符葉三.jpg（下段右一）
// 判準：星紋符頭（桿二・圈行三）＋雙目梯（各五箱）夾中杙柱（木叉・中箱・
// 匕匕・十——摹形從簡）＋底箱。錨自 12 格裁圖（宅183_B quanji）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅犬雞: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.32 * k, Z(z)).L(X(x1) - 0.32 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：桿二＋圈行三
  桿(0.7, 7.3, 1.1);
  桿(0.7, 7.3, 2.5);
  for (const [x, z] of [[1.0, 3.5], [2.5, 3.6], [4.3, 3.5], [6.4, 3.6],
                        [1.1, 4.9], [2.6, 5.0], [4.4, 4.9], [6.5, 5.0],
                        [1.2, 6.3], [4.5, 6.3], [6.6, 6.4]] as const) 圈(x, z);

  // 雙目梯（各五箱）
  const 梯 = (x0: number, x1: number) => {
    線(p => p.M(X(x0), Z(7.2)).L(X(x0), Z(17.4)));
    線(p => p.M(X(x1), Z(7.15)).L(X(x1), Z(17.35)));
    for (let i = 0; i <= 5; i++)
      線(p => p.M(X(x0), Z(7.2 + i * 2.04)).L(X(x1), Z(7.15 + i * 2.04)));
  };
  梯(0.9, 3.3);
  梯(4.6, 7.0);

  // 中杙柱：木叉・中箱・匕匕・十
  線(p => p.M(X(3.95), Z(7.2)).L(X(3.9), Z(17.2)), 0.34);
  線(p => p.M(X(3.5), Z(7.7)).C(X(3.7), Z(8.2), X(3.9), Z(8.5), X(3.95), Z(8.6)), 0.32);
  線(p => p.M(X(4.4), Z(7.7)).C(X(4.2), Z(8.2), X(4.0), Z(8.5), X(3.95), Z(8.6)), 0.32);
  線(p => p.M(X(3.5), Z(10.2)).L(X(3.5), Z(11.4)).L(X(4.4), Z(11.37))
    .L(X(4.4), Z(10.17)).L(X(3.46), Z(10.2)), 0.32);
  線(p => p.M(X(3.5), Z(12.8)).C(X(3.3), Z(13.3), X(3.4), Z(13.7), X(3.7), Z(13.9)), 0.32);
  線(p => p.M(X(4.4), Z(12.8)).C(X(4.6), Z(13.3), X(4.5), Z(13.7), X(4.2), Z(13.9)), 0.32);
  線(p => p.M(X(3.5), Z(15.4)).L(X(4.4), Z(15.37)), 0.32);

  // 底箱
  線(p => p.M(X(2.4), Z(17.8)).L(X(2.4), Z(18.9)).L(X(5.4), Z(18.85))
    .L(X(5.4), Z(17.75)));
};
