// 鎮宅靈符・厭產耗鬼不來剋害 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p186_符葉六_跋末神降忌食.jpg（中段右三）
// 判準：菱形圈鏈符頭（四隅圈連菱・心圈・右緣圈列・散圈・圈桿短）＋左目箱
// 三疊承尸框・右密目梯承目箱・乙曲尾（摹形從簡）。錨自 12 格裁圖
// （宅186_B chanhao）：X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。
// 逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅產耗: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：菱形圈鏈（四隅圈連菱＋心圈）
  const 菱: [number, number][] = [[2.2, 1.4], [0.6, 2.9], [2.7, 4.4], [5.1, 2.9]];
  for (let i = 0; i < 4; i++) { 連(菱[i], 菱[(i + 1) % 4]); 圈(菱[i][0], 菱[i][1]); }
  圈(2.6, 2.9);
  // 右緣圈列
  圈(6.6, 1.3); 圈(6.7, 2.5); 圈(6.6, 3.8);
  // 散圈
  圈(0.6, 4.0); 圈(2.3, 5.4); 圈(4.7, 4.8);
  // 圈桿短
  圈(0.6, 5.7); 圈(2.5, 5.8);
  線(p => p.M(X(4.2), Z(5.6)).L(X(6.1), Z(5.55))); 圈(3.9, 5.6); 圈(6.4, 5.55);

  // 左目箱三疊
  const 目 = (x0: number, x1: number, z0: number, z1: number) => {
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z1)).L(X(x1), Z(z1 - 0.05))
      .L(X(x1), Z(z0 - 0.05)).L(X(x0 - 0.05), Z(z0)));
    線(p => p.M(X(x0 + 0.1), Z((z0 + z1) / 2)).L(X(x1 - 0.1), Z((z0 + z1) / 2 - 0.03)), 0.32);
  };
  目(0.8, 3.2, 6.4, 9.0); 目(0.8, 3.2, 9.4, 12.0); 目(0.8, 3.2, 12.4, 15.0);
  // 尸口框（頂桁左長垂＋內口——摹形）
  線(p => p.M(X(0.9), Z(15.6)).L(X(3.1), Z(15.55)), 0.36);
  線(p => p.M(X(1.0), Z(15.6)).L(X(0.95), Z(19.15)), 0.38);
  線(p => p.M(X(1.4), Z(16.4)).L(X(1.4), Z(18.2)).L(X(2.9), Z(18.15))
    .L(X(2.9), Z(16.35)).L(X(1.35), Z(16.4)), 0.32);

  // 右目箱
  目(4.0, 6.5, 6.4, 9.0);
  // 密目梯（五桁四箱）
  線(p => p.M(X(4.0), Z(9.4)).L(X(4.0), Z(14.8)));
  線(p => p.M(X(6.5), Z(9.35)).L(X(6.5), Z(14.75)));
  for (let i = 0; i <= 4; i++)
    線(p => p.M(X(4.0), Z(9.4 + i * 1.35)).L(X(6.5), Z(9.35 + i * 1.35)));
  // 鬼曲長尾（雙盤 S＋末小圈）
  線(p => p.M(X(5.9), Z(15.3))
    .C(X(4.6), Z(15.5), X(4.3), Z(16.1), X(4.9), Z(16.5))
    .C(X(5.5), Z(16.9), X(5.4), Z(17.5), X(4.7), Z(17.7))
    .C(X(4.2), Z(17.9), X(4.3), Z(18.5), X(4.9), Z(18.7))
    .C(X(5.3), Z(18.85), X(5.7), Z(18.7), X(5.7), Z(18.4)), 0.36);
  圈(5.95, 18.5, 0.2);
};
