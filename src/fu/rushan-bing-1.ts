// ─────────────────────────────────────────────────────────────────────────────
// 老君入山符・丙一（篇末組第一符） · 親筆
// 據圖版：參攷-local/入山符/抱朴子登涉_佩帶符_p054.png（檔名沿誤，歸組
// 已訂：篇末丙組——考據卷六（乙）；欽定四庫全書繕本，PD）
// 判準：三層雲篆橫疊（左右桁列夾中脊蓮節鏈・重墨塊二）→寬弧雙桁→日框
// 五桁→雲曲垂尾四道→雙菱連珠星陣（菱一・橫列三星・菱二）；左右長垂界欄。
// 丙組繫語「此符是老君入山符，下說如文」（p059）——材色未言，不施硃。
// 幅比 1.5（z 0..36）。錨：X=(col-12)×1.05、Z=(row-0.8)×1.05（格_bing1）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 入山符丙一: 符筆 = (bi) => {
  const u = bi.u;
  const k = 1.05;
  const X = (c: number) => (c - 12) * k;
  const Z = (r: number) => (r - 0.8) * k;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 星 = (c: number, r: number, 半 = 0.55) => bi.dot(X(c), Z(r), 半);

  // ── 三層雲篆橫疊 ──
  // 頂弧雙翼
  線(p => p.M(X(6.3), Z(2.1)).C(X(8.2), Z(1.7), X(10.2), Z(1.6), X(11.9), Z(1.8)), 0.6);
  線(p => p.M(X(12.7), Z(1.8)).C(X(14.4), Z(1.6), X(16.3), Z(1.8), X(17.8), Z(2.2)), 0.6);
  // 左桁列四＋左括
  for (const z of [3.2, 4.5, 5.8, 7.1])
    線(p => p.M(X(6.5), Z(z)).L(X(11.4), Z(z - 0.2)));
  出(p => p.M(X(6.4), Z(3.9)).C(X(6.0), Z(4.9), X(6.0), Z(5.9), X(6.4), Z(6.9)));
  // 右桁列三＋右括
  for (const z of [3.4, 4.7, 6.0])
    線(p => p.M(X(13.4), Z(z)).L(X(17.8), Z(z - 0.2)));
  出(p => p.M(X(17.8), Z(4.0)).C(X(18.2), Z(4.9), X(18.2), Z(5.8), X(17.8), Z(6.6)));
  // 中脊蓮節鏈（節三・尾錐）
  const 節 = (z0: number, z1: number, 幅: number) => {
    線(p => p.M(X(12.3), Z(z0)).C(X(12.3 - 幅), Z((z0 + z1) / 2), X(12.3 - 幅 * 0.6), Z(z1 - 0.2), X(12.3), Z(z1)), 0.5);
    線(p => p.M(X(12.3), Z(z0)).C(X(12.3 + 幅), Z((z0 + z1) / 2), X(12.3 + 幅 * 0.6), Z(z1 - 0.2), X(12.3), Z(z1)), 0.5);
  };
  節(2.9, 4.7, 0.85); 節(4.9, 6.7, 0.8); 節(7.0, 8.3, 0.6);
  出(p => p.M(X(12.3), Z(8.4)).C(X(12.2), Z(9.1), X(12.2), Z(9.8), X(12.3), Z(10.4)), 0.5);
  // 重墨塊二（右上・左下）
  bi.塊(t => t.M(X(13.3), Z(6.9)).L(X(17.4), Z(6.7)).L(X(17.45), Z(7.6))
    .L(X(13.35), Z(7.8)).close());
  bi.塊(t => t.M(X(6.9), Z(9.2)).L(X(10.9), Z(9.0)).L(X(10.95), Z(9.9))
    .L(X(6.95), Z(10.1)).close());
  // 左右小鉤
  出(p => p.M(X(6.6), Z(8.6)).C(X(6.2), Z(9.1), X(6.3), Z(9.6), X(6.8), Z(9.9)), 0.5);
  出(p => p.M(X(17.7), Z(8.6)).C(X(17.9), Z(9.2), X(17.6), Z(9.7), X(17.0), Z(9.9)), 0.5);

  // ── 寬弧雙桁 ──
  線(p => p.M(X(7.9), Z(11.4)).C(X(10.5), Z(11.9), X(13.5), Z(11.7), X(15.7), Z(11.1)), 0.6);
  線(p => p.M(X(8.6), Z(12.9)).L(X(15.6), Z(12.6)), 0.58);
  線(p => p.M(X(8.9), Z(14.2)).L(X(15.9), Z(13.9)), 0.58);

  // ── 左右長垂界欄 ──
  出(p => p.M(X(6.7), Z(12.4)).C(X(6.5), Z(18.5), X(6.6), Z(24.5), X(6.2), Z(29.5))
    .C(X(6.0), Z(30.6), X(5.6), Z(31.4), X(5.0), Z(32.0)), 0.62);
  出(p => p.M(X(17.4), Z(14.5)).C(X(17.6), Z(20.0), X(17.6), Z(25.5), X(17.9), Z(29.8))
    .C(X(18.0), Z(30.8), X(18.3), Z(31.6), X(18.8), Z(32.2)), 0.62);

  // ── 日框五桁 ──
  出(p => p.M(X(8.3), Z(15.9)).C(X(8.0), Z(17.3), X(8.0), Z(18.7), X(8.1), Z(20.1)), 0.55);
  出(p => p.M(X(15.9), Z(15.7)).C(X(16.2), Z(17.1), X(16.2), Z(18.5), X(16.1), Z(20.3)), 0.55);
  for (const z of [15.9, 17.2, 18.4, 19.6, 20.8])
    線(p => p.M(X(8.2), Z(z)).L(X(16.0), Z(z - 0.25)), 0.55);

  // ── 雲曲垂尾四道 ──
  出(p => p.M(X(8.7), Z(21.6)).C(X(8.2), Z(22.6), X(9.8), Z(23.4), X(9.3), Z(24.6))
    .C(X(9.1), Z(25.2), X(8.7), Z(25.6), X(8.2), Z(25.8)), 0.5);
  出(p => p.M(X(10.8), Z(21.5)).C(X(10.2), Z(23.3), X(11.3), Z(25.2), X(10.5), Z(27.3)), 0.5);
  出(p => p.M(X(14.2), Z(21.6)).C(X(13.6), Z(22.7), X(14.6), Z(23.6), X(13.9), Z(24.9))
    .C(X(13.7), Z(25.4), X(13.9), Z(25.8), X(14.3), Z(26.0)), 0.5);
  出(p => p.M(X(15.8), Z(22.0)).C(X(15.2), Z(23.8), X(16.2), Z(25.4), X(15.4), Z(26.6))
    .C(X(15.0), Z(27.2), X(15.6), Z(27.6), X(16.2), Z(27.3)), 0.5);

  // ── 雙菱連珠星陣 ──
  const 連 = (a: [number, number], b: [number, number]) => {
    const [ax, az] = a, [bx, bz] = b;
    const dx = bx - ax, dz = bz - az, L = Math.hypot(dx, dz), t = 0.55 / L;
    線(p => p.M(X(ax + dx * t), Z(az + dz * t)).L(X(bx - dx * t), Z(bz - dz * t)), 0.42);
  };
  const 菱 = (頂: [number, number], 左: [number, number], 右: [number, number], 底: [number, number]) => {
    for (const s of [頂, 左, 右, 底]) 星(s[0], s[1]);
    連(頂, 左); 連(頂, 右); 連(左, 底); 連(右, 底);
  };
  菱([12.2, 27.9], [9.1, 28.6], [15.6, 29.2], [12.3, 29.7]);
  星(9.2, 30.7); 星(12.2, 30.8); 星(15.7, 30.9);
  菱([12.4, 31.9], [9.1, 32.3], [15.7, 32.6], [12.4, 33.3]);
};
