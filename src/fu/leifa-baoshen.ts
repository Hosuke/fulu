// ─────────────────────────────────────────────────────────────────────────────
// 元始保神符（道法會元卷十二・雲篆雷符之屬） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3843_卷12雲篆雷符-03.png（「元始保神符」題左之符）
// 判準：雙群雲篆——右群：の螺旋首＋蛇脊下垂綴人字交二重＋底橫撇；左群：
// 弓首曲＋長蛇三折＋底己曲。書材卷面未記，不施硃層。摹形從簡。
// 錨自 8 格裁圖（格2_baoshen）：X=(c-2.8)×3.3、Z=(r-1.7)×3.3+2。幅比 1.6。
// 逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 元始保神符: 符筆 = (bi) => {
  const u = bi.u, k = 3.3;
  const X = (c: number) => (c - 2.8) * k;
  const Z = (r: number) => (r - 1.7) * k + 2;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.7) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 右群：の螺旋首（入筆自右上，捲心內收）
  抖(p => p.M(X(4.95), Z(2.15))
    .C(X(4.2), Z(1.75), X(3.2), Z(1.95), X(2.95), Z(2.75))
    .C(X(2.75), Z(3.45), X(3.35), Z(4.1), X(4.15), Z(4.0))
    .C(X(4.75), Z(3.9), X(4.95), Z(3.3), X(4.55), Z(3.0))
    .C(X(4.25), Z(2.8), X(3.85), Z(3.0), X(3.9), Z(3.35)), 0.9);
  // 蛇脊下垂（自螺旋底出）
  抖(p => p.M(X(4.15), Z(4.05))
    .C(X(4.6), Z(4.6), X(4.6), Z(5.2), X(4.15), Z(5.65))
    .C(X(3.7), Z(6.1), X(3.75), Z(6.6), X(4.25), Z(6.95))
    .C(X(4.6), Z(7.3), X(4.5), Z(7.8), X(4.05), Z(8.2))
    .C(X(3.75), Z(8.5), X(3.8), Z(8.8), X(3.95), Z(8.95)), 0.88);
  // 人字交一
  抖(p => p.M(X(4.25), Z(7.0))
    .C(X(3.7), Z(7.5), X(3.1), Z(8.05), X(2.7), Z(8.6)), 0.81);
  抖(p => p.M(X(4.3), Z(7.05))
    .C(X(4.75), Z(7.4), X(5.15), Z(7.7), X(5.45), Z(8.0)), 0.81);
  // 人字交二
  抖(p => p.M(X(3.95), Z(9.0))
    .C(X(3.4), Z(9.5), X(2.8), Z(10.05), X(2.25), Z(10.6)), 0.81);
  抖(p => p.M(X(4.0), Z(9.05))
    .C(X(4.5), Z(9.5), X(5.05), Z(9.95), X(5.55), Z(10.35)), 0.81);
  // 底橫撇
  抖(p => p.M(X(2.5), Z(10.95))
    .C(X(3.2), Z(11.15), X(4.0), Z(11.15), X(4.6), Z(10.95)), 0.9);

  // 左群：弓首曲（鉤環顯著）
  抖(p => p.M(X(0.95), Z(1.8))
    .C(X(1.7), Z(1.9), X(1.85), Z(2.55), X(1.3), Z(2.85))
    .C(X(0.9), Z(3.05), X(0.85), Z(3.5), X(1.25), Z(3.8))
    .C(X(1.6), Z(4.1), X(1.55), Z(4.7), X(1.35), Z(5.2)), 0.85);
  // 長蛇三折承底己曲
  抖(p => p.M(X(0.7), Z(6.0))
    .C(X(1.35), Z(6.5), X(1.45), Z(7.1), X(0.85), Z(7.55))
    .C(X(0.35), Z(7.9), X(0.45), Z(8.5), X(1.05), Z(8.85))
    .C(X(1.6), Z(9.15), X(1.6), Z(9.7), X(1.05), Z(10.0))
    .C(X(0.5), Z(10.3), X(0.25), Z(10.65), X(0.45), Z(11.0))
    .C(X(0.7), Z(11.4), X(1.4), Z(11.35), X(1.65), Z(10.95))
    .C(X(1.8), Z(10.6), X(1.5), Z(10.3), X(1.2), Z(10.5)), 0.88);
};
