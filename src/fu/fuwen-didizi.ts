// ─────────────────────────────────────────────────────────────────────────────
// 複文・地地子子子（雙地三子疊） · 親筆
// 據圖版：參攷-local/複文/道藏太平經卷104首_興上除害複文169_三家本冊24頁524.png
// （正統道藏原式，三家本影印冊24頁524；興上除害複文第一百六十九）
// 判準：上雙地並列（土旁乜身右曳）・下三子橫列（末子曳長迴鉤）——
// 單格 8x 放大親核（複_五格板）。釋讀待核，名從形貌。幅比 0.95（z 0..22.8）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 複文地地子: 符筆 = (bi) => {
  const u = bi.u;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.85) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.9) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 地（左右各一）
  const 地 = (cx: number) => {
    // 土旁
    線(p => p.M(cx - 4.6, 4.2).L(cx - 2.6, 4.05), 0.8);
    線(p => p.M(cx - 3.6, 2.9).L(cx - 3.65, 6.6), 0.8);
    線(p => p.M(cx - 4.9, 6.8).L(cx - 2.3, 6.6), 0.8);
    // 也身（雙豎乜迴右曳）
    線(p => p.M(cx - 1.4, 3.4).L(cx - 1.45, 6.1), 0.75);
    線(p => p.M(cx + 0.1, 3.2).L(cx + 0.05, 5.6), 0.75);
    出(p => p.M(cx - 2.0, 4.6).C(cx - 0.8, 4.4, cx + 0.6, 4.3, cx + 1.5, 4.3)
      .C(cx + 2.0, 4.35, cx + 2.2, 4.7, cx + 2.1, 5.2)
      .C(cx + 1.9, 6.2, cx + 0.6, 6.7, cx - 0.7, 6.8)
      .C(cx + 0.7, 7.0, cx + 1.9, 6.9, cx + 2.7, 6.5), 0.8);
  };
  地(-5.2); 地(5.2);

  // 子（三橫列，末子曳長迴鉤）
  const 子 = (cx: number, 曳 = 0) => {
    線(p => p.M(cx - 2.0, 13.6).C(cx - 1.2, 13.9, cx - 0.2, 13.9, cx + 1.6, 13.4), 0.85);
    出(p => p.M(cx + 0.2, 13.6).C(cx - 0.1, 15.4, cx - 0.1, 17.2, cx + 0.1, 18.8)
      .C(cx + 0.2, 19.8 + 曳, cx - 0.6, 20.4 + 曳, cx - 1.8 - 曳 * 0.5, 20.2 + 曳), 0.9);
    線(p => p.M(cx - 2.4, 16.2).L(cx + 2.0, 15.9), 0.85);
  };
  子(-7.2); 子(-0.2); 子(6.8, 1.0);
};
