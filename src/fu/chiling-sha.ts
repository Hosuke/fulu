// 天地水年月日時七煞神君等）；本繪依畫序親書楷體成字（同敕字式之裁）。
// 罡煞入符核之制見總綱一・1.4（張智雄已核）。幅比 1.2（z 0..28.8）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 煞字式: 符筆 = (bi) => {
  const u = bi.u;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.95) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.05) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // ── ⺈＋彐（左上）──
  出(p => p.M(-3.4, 3.2).C(-4.6, 4.2, -5.8, 5.0, -7.0, 5.6));       // 撇
  橫(p => p.M(-6.6, 6.6).C(-4.8, 6.4, -3.0, 6.3, -1.6, 6.5).L(-1.6, 9.2), 0.9); // 橫折
  橫(p => p.M(-7.6, 9.4).L(-1.7, 9.2), 0.9);                         // 彐中橫
  橫(p => p.M(-7.6, 9.5).L(-7.6, 12.2).L(-1.6, 12.0), 0.9);          // 彐下折
  // 豎提（左下）
  出(p => p.M(-6.2, 13.4).L(-6.3, 17.6).C(-5.6, 17.2, -4.8, 16.8, -4.0, 16.5), 0.95);
  // 點
  出(p => p.M(-3.2, 13.8).C(-2.6, 14.6, -2.1, 15.4, -1.7, 16.2), 0.9);

  // ── 攵部（右）──
  出(p => p.M(3.8, 2.8).C(3.0, 4.2, 2.2, 5.4, 1.2, 6.5));            // 短撇
  橫(p => p.M(1.0, 7.6).C(3.0, 7.3, 5.2, 7.2, 7.4, 7.4));            // 橫
  出(p => p.M(6.2, 7.6).C(4.8, 10.8, 3.0, 14.2, 0.6, 17.2));         // 長撇
  出(p => p.M(2.6, 11.0).C(4.0, 13.2, 5.6, 15.4, 7.4, 17.4), 1.15);  // 捺

  // ── 灬（四點底——七煞神君之位）──
  出(p => p.M(-7.8, 21.0).C(-7.4, 22.0, -7.1, 23.0, -6.9, 24.0), 0.95);
  出(p => p.M(-3.6, 21.4).C(-3.3, 22.4, -3.1, 23.3, -3.0, 24.2), 0.95);
  出(p => p.M(0.8, 21.4).C(1.1, 22.4, 1.3, 23.3, 1.4, 24.2), 0.95);
  出(p => p.M(5.2, 21.0).C(5.8, 22.0, 6.3, 23.0, 6.7, 24.0), 0.95);
};
