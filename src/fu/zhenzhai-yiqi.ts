// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭疫氣百雜鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p185_符葉五.jpg（中段右四）
// 判準：星紋符頭（桿・圈叢二行）＋左密目梯七箱・中椎椎雙杙柱・右柱𡉵古朱
// 字形疊承皃曲（摹形從簡）。錨自 12 格裁圖（宅185_B yiqi）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅疫氣: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿＋圈叢二行
  線(p => p.M(X(0.9), Z(1.3)).L(X(6.5), Z(1.2))); 圈(0.6, 1.3); 圈(6.8, 1.2);
  for (const [x, z] of [[0.9, 2.4], [1.9, 2.5], [2.9, 2.4], [3.9, 2.5], [4.9, 2.4], [6.2, 2.3],
                        [1.0, 3.5], [2.0, 3.6], [3.0, 3.5], [4.0, 3.6], [6.3, 3.4]] as const)
    圈(x, z);

  // 左密目梯七箱
  線(p => p.M(X(0.8), Z(4.4)).L(X(0.8), Z(17.0)));
  線(p => p.M(X(2.9), Z(4.35)).L(X(2.9), Z(16.95)));
  for (let i = 0; i <= 7; i++)
    線(p => p.M(X(0.8), Z(4.4 + i * 1.8)).L(X(2.9), Z(4.35 + i * 1.8)));

  // 中椎椎雙杙柱（木＋隹之形摹簡：干杙加橫梳）
  const 椎 = (z0: number) => {
    線(p => p.M(X(3.9), Z(z0)).L(X(3.87), Z(z0 + 2.6)), 0.36);
    線(p => p.M(X(3.3), Z(z0 + 0.5)).L(X(4.5), Z(z0 + 0.47)), 0.32);
    線(p => p.M(X(3.4), Z(z0 + 1.2)).L(X(4.4), Z(z0 + 1.17)), 0.3);
    線(p => p.M(X(3.4), Z(z0 + 1.9)).L(X(4.4), Z(z0 + 1.87)), 0.3);
  };
  椎(4.4); 椎(7.4);

  // 右柱：𡉵古朱字形疊承皃曲（摹形從簡）
  線(p => p.M(X(5.3), Z(4.6)).L(X(5.28), Z(6.2)), 0.34);
  線(p => p.M(X(4.9), Z(5.1)).L(X(5.8), Z(5.07)), 0.32);
  線(p => p.M(X(4.8), Z(6.2)).L(X(5.9), Z(6.17)), 0.34);
  線(p => p.M(X(5.3), Z(7.0)).L(X(5.28), Z(8.4)), 0.34);
  線(p => p.M(X(4.9), Z(7.4)).L(X(5.8), Z(7.37)), 0.32);
  線(p => p.M(X(4.8), Z(8.4)).L(X(4.8), Z(9.4)).L(X(5.9), Z(9.37))
    .L(X(5.9), Z(8.37)).L(X(4.75), Z(8.4)), 0.32);
  // 朱形
  出(p => p.M(X(5.1), Z(10.1)).C(X(4.8), Z(10.5), X(4.6), Z(10.8), X(4.4), Z(11.0)), 0.32);
  線(p => p.M(X(4.6), Z(11.2)).L(X(6.0), Z(11.17)), 0.34);
  線(p => p.M(X(5.3), Z(10.4)).L(X(5.28), Z(13.0)), 0.34);
  出(p => p.M(X(5.25), Z(11.6)).C(X(4.9), Z(12.3), X(4.6), Z(12.8), X(4.3), Z(13.2)), 0.32);
  出(p => p.M(X(5.35), Z(11.6)).C(X(5.7), Z(12.3), X(6.0), Z(12.8), X(6.3), Z(13.2)), 0.32);
  // 皃曲
  線(p => p.M(X(4.7), Z(14.0))
    .C(X(4.5), Z(14.7), X(4.7), Z(15.3), X(5.3), Z(15.35))
    .C(X(5.9), Z(15.4), X(6.1), Z(14.9), X(5.8), Z(14.6)), 0.36);

  // 底桁
  線(p => p.M(X(0.9), Z(17.7)).L(X(6.2), Z(17.6)), 0.42);
};
