// 幅比 0.61（高≈14.6 指）。式：臥目之形——一體橫陳右角尖揚，體含斜梭白竅；
// 左端曲卷如尾，下緣一灣內蝕；左下別出離帆一葉。
// 二輪（2026-07-12）：體增豐、右角伸長、白灣加深、梭竅放大、離帆增體。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 恆嶽真形: 符筆 = (bi) => {
  // 主體一塊（順時針自左上小角起）
  bi.塊(t => t.M(-8.2, 6.2)
    .C(-6.1, 5.3, -4.1, 4.9, -2.6, 4.3)     // 頂緣東行
    .C(-0.7, 3.3, 1.0, 2.6, 2.4, 2.5)       // 頂脊
    .C(4.6, 2.35, 6.7, 2.6, 8.2, 2.8)       // 向右角
    .C(9.0, 2.9, 9.6, 3.0, 10.0, 3.1)       // 右角尖
    .C(8.6, 4.6, 7.4, 5.6, 6.3, 6.4)        // 角下緣回鋒（深凹）
    .C(4.8, 7.9, 3.3, 9.6, 2.2, 10.6)       // 體右下走（腹深）
    .C(1.3, 11.3, 0.75, 11.9, 0.45, 12.7)   // 底尖（長）
    .C(-0.1, 11.4, -0.65, 10.95, -1.4, 10.9) // 內凹回鋒
    .C(-2.5, 9.3, -4.5, 9.3, -5.7, 10.9)    // 下緣白灣（深蝕）
    .C(-6.3, 12.1, -7.4, 12.7, -8.1, 12.1)  // 左下圓卷（増豐）
    .C(-9.05, 11.2, -9.4, 9.9, -9.0, 8.7)   // 左端弧
    .C(-8.8, 7.9, -8.35, 7.5, -7.75, 7.4)   // 腰際內凹
    .C(-7.95, 7.0, -8.1, 6.6, -8.2, 6.2)    // 回左上小角
    .close());

  // 斜梭白竅（鑿：兩尖之梭，軸傾東北，體豐）
  bi.鑿(t => t.M(-0.6, 7.4)
    .C(0.7, 5.35, 3.0, 4.05, 4.75, 4.45)
    .C(3.85, 6.5, 1.7, 8.05, -0.6, 7.4)
    .close());

  // 左下離帆（塊：尖上微卷、底左出鋒）
  bi.塊(t => t.M(-4.5, 10.4)
    .C(-4.15, 11.6, -3.35, 12.85, -2.4, 13.7)
    .C(-4.2, 14.15, -6.3, 14.05, -7.6, 13.6)
    .C(-6.2, 12.8, -5.2, 11.7, -4.5, 10.4)
    .close());
};
