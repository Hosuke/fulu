// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭除家鬼克害人口 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p183_符葉三.jpg（上段右四）
// 判準：星紋符頭（斷桿綴圈・己曲・圈鏈）＋大迴帶四折＋昇馬形雙構（左日箱
// 廾足・右馬形口首桁身四蹄曲尾）＋鬼形雙蟠尾＋右軌。摹形從簡註。
// 錨自 12 格裁圖（宅183_A jiagui）：X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。
// 幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅家鬼: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：斷桿綴圈＋己曲＋圈鏈
  線(p => p.M(X(0.8), Z(1.0)).L(X(3.3), Z(0.95))); 圈(0.5, 1.0); 圈(3.6, 0.95);
  線(p => p.M(X(4.6), Z(0.92)).L(X(6.6), Z(0.88))); 圈(4.3, 0.92); 圈(6.9, 0.85);
  線(p => p.M(X(2.0), Z(1.8)).C(X(1.6), Z(2.2), X(1.9), Z(2.6), X(2.5), Z(2.5))
    .C(X(3.0), Z(2.4), X(2.9), Z(1.9), X(2.4), Z(2.0)), 0.34);
  連([3.4, 2.1], [4.5, 2.0]); 圈(3.4, 2.1); 圈(4.5, 2.0);

  // 大迴帶四折
  線(p => p.M(X(0.9), Z(3.0)).L(X(6.6), Z(2.95))
    .C(X(7.1), Z(2.95), X(7.1), Z(3.95), X(6.6), Z(4.0))
    .L(X(1.6), Z(4.05)).C(X(1.1), Z(4.05), X(1.1), Z(5.05), X(1.6), Z(5.1))
    .L(X(6.6), Z(5.05)).C(X(7.1), Z(5.05), X(7.1), Z(6.05), X(6.6), Z(6.1))
    .L(X(1.6), Z(6.15)).C(X(1.1), Z(6.15), X(1.1), Z(7.15), X(1.6), Z(7.2))
    .L(X(7.0), Z(7.15)));

  // 昇形（左）：日箱＋廾足
  線(p => p.M(X(1.2), Z(8.1)).L(X(1.2), Z(10.2)).L(X(3.0), Z(10.15))
    .L(X(3.0), Z(8.05)).L(X(1.15), Z(8.1)));
  線(p => p.M(X(1.3), Z(9.15)).L(X(2.9), Z(9.1)), 0.34);
  線(p => p.M(X(1.6), Z(10.4)).L(X(1.55), Z(12.6)));
  線(p => p.M(X(2.6), Z(10.35)).L(X(2.6), Z(12.5)));
  線(p => p.M(X(0.9), Z(11.3)).L(X(3.3), Z(11.25)), 0.34);

  // 馬形（右）：口首＋桁身＋四蹄曲＋尾鉤
  線(p => p.M(X(3.8), Z(8.2)).L(X(3.8), Z(9.6)).L(X(5.3), Z(9.55))
    .L(X(5.3), Z(8.15)).L(X(3.75), Z(8.2)));
  線(p => p.M(X(3.9), Z(10.1)).L(X(6.3), Z(10.05)), 0.42);
  線(p => p.M(X(3.9), Z(10.9)).L(X(6.2), Z(10.85)), 0.36);
  線(p => p.M(X(6.3), Z(10.1)).L(X(6.35), Z(12.3)));
  for (const x0 of [4.0, 4.7, 5.4] as const)
    線(p => p.M(X(x0), Z(11.3)).C(X(x0 - 0.3), Z(11.9), X(x0 - 0.2), Z(12.5), X(x0 + 0.1), Z(12.9)), 0.34);
  線(p => p.M(X(6.35), Z(12.4)).C(X(6.5), Z(13.0), X(6.3), Z(13.4), X(5.9), Z(13.5)), 0.34);

  // 鬼形雙蟠尾
  const 鬼蟠 = (cx: number) => {
    線(p => p.M(X(cx - 0.7), Z(13.8)).L(X(cx - 0.7), Z(15.2)).L(X(cx + 0.7), Z(15.15))
      .L(X(cx + 0.7), Z(13.75)).L(X(cx - 0.75), Z(13.8)));
    線(p => p.M(X(cx - 0.6), Z(14.5)).L(X(cx + 0.6), Z(14.45)), 0.32);
    線(p => p.M(X(cx - 0.3), Z(15.4))
      .C(X(cx - 0.8), Z(16.2), X(cx - 0.5), Z(17.0), X(cx + 0.2), Z(17.2))
      .C(X(cx + 0.7), Z(17.3), X(cx + 0.9), Z(16.9), X(cx + 0.6), Z(16.6))
      .C(X(cx + 0.4), Z(16.4), X(cx + 0.2), Z(16.5), X(cx + 0.2), Z(16.7)), 0.36);
  };
  鬼蟠(2.0); 鬼蟠(4.6);

  // 右軌
  線(p => p.M(X(7.2), Z(7.3)).L(X(7.2), Z(16.5))
    .C(X(7.2), Z(17.2), X(7.0), Z(17.7), X(6.6), Z(18.0)));
};
