// ─────────────────────────────────────────────────────────────────────────────
// 太上符（道法會元卷一百二・「太上」二字符頭七符・六） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3931_卷102太上二字符頭七符-p07.png（右起第六）
// 判準：太上符頭通制（頂小鉤・大橫・左右長弧腳下垂入身・上字・敕曲核心
// ——十字桁貫豎承大与曲）＋迴摺帶・左迴摺右王聖框・底摺尾。「右七符用皁紙」書材註記；行持說明
// 與咒記位不錄；七符之六在葉（右起第一葉緣截斷僅存左緣，俟原影補〔待核〕）。
// 摹形從簡。錨自 8 格裁圖（格102_五 ts5）：X=(c-3.95)×2.5、
// Z=(r-1.9)×2.5+1.0。幅比 3。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 太上符六: 符筆 = (bi) => {
  const u = bi.u, k = 2.5;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 1.9) * k + 1.0;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.58) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 太上冠：頂小鉤・大橫・左右長弧腳・上字
  出(p => p.M(X(3.75), Z(2.1)).C(X(3.85), Z(2.5), X(3.95), Z(2.8), X(4.0), Z(3.0)), 0.5);
  線(p => p.M(X(0.55), Z(3.5)).L(X(7.25), Z(3.42)), 0.62);
  出(p => p.M(X(3.85), Z(3.55)).C(X(2.6), Z(4.7), X(1.3), Z(5.5), X(0.8), Z(6.5)).L(X(0.78), Z(9.8)), 0.6);
  出(p => p.M(X(4.05), Z(3.55)).C(X(5.3), Z(4.7), X(6.6), Z(5.5), X(7.1), Z(6.5)).L(X(7.08), Z(8.4)), 0.6);
  線(p => p.M(X(3.95), Z(4.0)).L(X(3.93), Z(5.9)), 0.55);
  // 冠腳彎合
  出(p => p.M(X(0.78), Z(9.8)).C(X(0.9), Z(10.3), X(1.7), Z(10.55), X(3.0), Z(10.5)), 0.58);
  出(p => p.M(X(7.08), Z(8.4)).C(X(7.0), Z(9.1), X(6.6), Z(9.5), X(6.1), Z(9.6)), 0.58);
  線(p => p.M(X(4.05), Z(4.8)).L(X(5.1), Z(4.76)), 0.5);
  線(p => p.M(X(2.5), Z(6.0)).L(X(5.6), Z(5.94)), 0.55);
  // 敕曲核心
  線(p => p.M(X(1.7), Z(8.3)).L(X(4.6), Z(8.24)), 0.55);
  線(p => p.M(X(2.95), Z(7.6)).L(X(2.93), Z(10.2)), 0.55);
  出(p => p.M(X(2.95), Z(9.5)).L(X(2.2), Z(10.3)), 0.45);
  出(p => p.M(X(3.0), Z(9.5)).L(X(3.7), Z(10.2)), 0.45);
  線(p => p.M(X(4.6), Z(7.8)).C(X(5.9), Z(7.7), X(6.3), Z(8.5), X(5.3), Z(8.8)).C(X(4.4), Z(9.1), X(4.5), Z(9.8), X(5.5), Z(9.9)).C(X(6.1), Z(9.96), X(6.3), Z(10.3), X(6.2), Z(10.6)), 0.55);
  // 迴摺帶
  線(p => p.M(X(6.2), Z(11.0)).C(X(1.4), Z(11.1), X(1.1), Z(11.9), X(2.3), Z(12.0)).L(X(5.2), Z(11.9)).C(X(6.3), Z(11.95), X(6.3), Z(12.8), X(5.2), Z(12.9)).L(X(1.9), Z(13.0)), 0.6);
  // 左迴摺・右王聖框
  線(p => p.M(X(1.9), Z(13.1)).L(X(1.88), Z(17.4)).C(X(1.86), Z(18.2), X(2.6), Z(18.4), X(3.3), Z(18.3)), 0.55);
  線(p => p.M(X(3.9), Z(14.0)).L(X(3.9), Z(17.2)).L(X(6.4), Z(17.14)).L(X(6.4), Z(13.94)).L(X(3.85), Z(14.0)), 0.55);
  線(p => p.M(X(4.5), Z(14.7)).L(X(5.9), Z(14.66)), 0.48);
  線(p => p.M(X(5.2), Z(14.1)).L(X(5.18), Z(16.4)), 0.48);
  線(p => p.M(X(4.5), Z(15.6)).L(X(5.9), Z(15.56)), 0.48);
  線(p => p.M(X(4.4), Z(16.5)).L(X(6.0), Z(16.46)), 0.48);
  // 底摺尾
  線(p => p.M(X(5.9), Z(18.9)).C(X(1.7), Z(19.0), X(1.4), Z(19.8), X(2.6), Z(19.9)).L(X(5.1), Z(19.8)).C(X(6.1), Z(19.85), X(6.1), Z(20.6), X(5.1), Z(20.7)).L(X(2.3), Z(20.8)), 0.6);
  出(p => p.M(X(2.3), Z(20.85)).C(X(2.0), Z(21.4), X(2.2), Z(22.0), X(2.8), Z(22.3)), 0.58);
};
