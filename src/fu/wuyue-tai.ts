// 判準：古今圖書集成（1726）明碑一系摹刻；錨自 24 指格網逐點讀出（格_tai.png）。
// 幅比 0.96（高≈23 指）。式：燕尾冠＋左二逗＋右ㄋ波帶＋中軀分二足＋內核五枚。
// 二輪（2026-07-12）：加筆寬近木刻之肥，冠増體，頸足相連，叉部交股。
// ─────────────────────────────────────────────────────────────────────────────
import { 卵 } from '../fubi.js';
import type { 符筆 } from './index.js';

export const 泰嶽真形: 符筆 = (bi) => {
  const u = bi.u;
  const 揮 = (寬指: number, 起: '藏' | '尖', 收: '回' | '出',
              書: Parameters<typeof bi.一筆>[1]) =>
    bi.一筆({ 寬: 寬指 * u, 起, 收 }, 書);

  // 燕尾冠（塊）：左尖 (-2.7,1.8)，中脊 (0.7,3.1)，右尖 (4.5,3.2)，腹垂 z4.95
  bi.塊(t => t.M(-2.7, 1.8)
    .C(-1.5, 2.9, -0.3, 3.35, 0.7, 3.35)
    .C(2.0, 3.3, 3.3, 2.95, 4.5, 3.15)
    .C(3.3, 4.0, 2.1, 4.5, 1.1, 4.75)
    .C(0.2, 4.95, -0.8, 4.9, -1.5, 4.5)
    .C(-2.15, 3.9, -2.55, 2.7, -2.7, 1.8)
    .close());

  // 頸（連冠底至叉部——末探至叉心，與足腿柱三筆墨面相疊）
  揮(1.6, '藏', '回', p => p.M(0.3, 4.4).C(0.0, 5.3, -0.35, 6.3, -0.45, 7.5));

  // 左上逗一（淚滴）；左上逗二（圓首出尾）
  揮(1.5, '尖', '回', p => p.M(-4.0, 3.3).C(-4.6, 3.9, -5.1, 4.6, -5.35, 5.4));
  揮(1.6, '藏', '出', p => p.M(-6.5, 6.9)
    .C(-6.85, 7.7, -6.4, 8.5, -5.55, 8.55)
    .C(-4.95, 8.6, -4.4, 8.35, -4.0, 7.8));

  // 左足（自叉右上入，西南走，與右腿交股）
  揮(2.3, '藏', '回', p => p.M(0.6, 7.1)
    .C(-1.0, 8.5, -2.9, 9.5, -4.2, 11.0)
    .C(-5.3, 12.3, -6.0, 13.7, -6.6, 15.3)
    .C(-7.1, 16.6, -7.8, 17.3, -8.6, 18.0));
  // 足笏（橫踏，左尖右鈍）
  揮(2.0, '尖', '回', p => p.M(-10.6, 17.5)
    .C(-9.6, 18.3, -8.2, 18.9, -6.8, 19.3)
    .C(-6.0, 19.5, -5.2, 19.65, -4.3, 19.7));

  // 右腿與尾鉤（自叉左上入，東南走，收出上揚）
  揮(2.3, '藏', '出', p => p.M(-1.3, 7.3)
    .C(0.4, 8.8, 2.1, 10.1, 3.2, 11.7)
    .C(3.9, 13.4, 4.3, 15.2, 4.8, 16.9)
    .C(5.3, 18.7, 6.3, 19.9, 7.7, 19.9)
    .C(8.9, 19.9, 9.9, 18.9, 10.5, 17.7)
    .C(10.7, 17.3, 10.85, 16.9, 11.0, 16.5));

  // 中柱（自交股處垂，圓收——起點沒入叉心墨面）
  揮(2.0, '藏', '回', p => p.M(-0.1, 8.3)
    .C(0.6, 10.0, 0.7, 11.5, 0.45, 13.0)
    .C(0.1, 14.8, -0.35, 16.6, -0.5, 18.4)
    .C(-0.58, 19.5, -0.55, 20.4, -0.45, 21.2));

  // 右上ㄋ波帶（尖起出收）
  揮(2.0, '尖', '出', p => p.M(4.1, 7.0)
    .C(4.5, 6.0, 5.5, 5.4, 6.5, 5.8)
    .C(7.5, 6.2, 7.6, 7.2, 6.9, 7.9)
    .C(6.6, 8.2, 6.2, 8.45, 5.95, 8.6)
    .C(7.1, 8.9, 7.8, 9.7, 8.1, 10.7)
    .C(8.4, 12.0, 7.9, 13.0, 6.6, 14.1));

  // 內核五枚（穴口之遺意——明碑一系已成墨核）
  bi.塊(t => 卵(t, -2.6, 12.5, 1.0, 0.95));
  bi.塊(t => 卵(t, -3.35, 15.0, 0.85, 1.15, 0.3));
  bi.塊(t => 卵(t, 1.6, 12.9, 1.2, 1.05));
  bi.塊(t => 卵(t, 1.35, 15.2, 1.1, 1.1));
  bi.塊(t => 卵(t, 1.0, 18.4, 0.95, 1.7));
};
