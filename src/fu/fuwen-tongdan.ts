// ─────────────────────────────────────────────────────────────────────────────
// 複文・同同單（雙同覆單疊） · 親筆
// 據圖版：參攷-local/複文/道藏太平經卷104首_興上除害複文169_三家本冊24頁524.png
// （正統道藏原式，三家本影印冊24頁524；興上除害複文第一百六十九）
// 判準：上雙同並列（冂內⿱一口）・下單身（曰曰疊・長橫・貫豎十字）——
// 8x 親核（複_五格板）。釋讀待核，名從形貌。幅比 1.15（z 0..27.6）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 複文同單: 符筆 = (bi) => {
  const u = bi.u;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.85) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.9) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 同（左右各一）：冂內⿱一口
  const 同 = (cx: number) => {
    出(p => p.M(cx - 3.1, 2.6).L(cx - 3.2, 8.3), 0.85);
    出(p => p.M(cx - 3.1, 2.7).L(cx + 3.0, 2.5).L(cx + 3.1, 8.2), 0.85);
    線(p => p.M(cx - 1.8, 4.2).L(cx + 1.8, 4.05), 0.75);
    線(p => p.M(cx - 1.5, 5.6).L(cx - 1.55, 7.4), 0.7);
    線(p => p.M(cx - 1.5, 5.7).L(cx + 1.5, 5.55).L(cx + 1.55, 7.3), 0.7);
    線(p => p.M(cx - 1.5, 7.4).L(cx + 1.55, 7.3), 0.7);
  };
  同(-4.2); 同(4.2);

  // 單身：曰曰疊・長橫・貫豎
  線(p => p.M(-4.8, 10.6).L(-4.85, 13.6), 0.8);
  線(p => p.M(-4.8, 10.7).L(4.8, 10.5).L(4.85, 13.5), 0.8);
  線(p => p.M(-4.7, 12.1).L(4.7, 11.95), 0.72);
  線(p => p.M(-4.8, 13.6).L(4.85, 13.5), 0.75);
  線(p => p.M(-5.3, 15.0).L(-5.35, 18.2), 0.8);
  線(p => p.M(-5.3, 15.1).L(5.3, 14.9).L(5.35, 18.1), 0.8);
  線(p => p.M(-5.2, 16.6).L(5.2, 16.4), 0.72);
  線(p => p.M(-5.3, 18.2).L(5.35, 18.1), 0.75);
  // 長橫與貫豎（十）
  線(p => p.M(-6.6, 20.4).C(-3.0, 20.0, 3.0, 20.0, 6.6, 20.4), 0.9);
  出(p => p.M(0.05, 10.8).L(-0.05, 23.4)
    .C(-0.05, 24.2, -0.3, 24.7, -0.8, 25.0), 0.9);
  線(p => p.M(-3.2, 22.8).L(3.2, 22.6), 0.8);
};
