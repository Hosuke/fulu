// 鎮宅靈符・厭疾病盜賊虛耗之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p186_符葉六_跋末神降忌食.jpg（上段右三）
// 判準：星紋符頭（桿三・左長杙豎・星叢放射八芒・右緣圈列）＋滿幅桁＋左目目
// 箱・右豎撇捺曲（⺁乂摹形）＋口口對＋底桁二。錨自 12 格裁圖（宅186_A xuhao）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅虛耗: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：桿三
  線(p => p.M(X(1.0), Z(1.2)).L(X(6.0), Z(1.1))); 圈(0.7, 1.2); 圈(6.3, 1.1);
  線(p => p.M(X(1.0), Z(2.2)).L(X(6.0), Z(2.1))); 圈(0.7, 2.2); 圈(6.3, 2.1); 圈(3.4, 2.15);
  線(p => p.M(X(1.0), Z(3.2)).L(X(6.0), Z(3.1))); 圈(0.7, 3.2); 圈(6.3, 3.1); 圈(3.4, 3.15);
  // 左長杙豎
  線(p => p.M(X(0.8), Z(4.2)).L(X(0.8), Z(9.0)), 0.4);
  // 星叢放射八芒
  圈(3.4, 6.4);
  const 芒: [number, number][] = [
    [3.4, 4.6], [4.8, 5.1], [5.5, 6.4], [4.7, 7.7],
    [3.4, 8.3], [2.1, 7.7], [1.5, 6.4], [2.2, 5.1],
  ];
  for (const m of 芒) { 連([3.4, 6.4], m); 圈(m[0], m[1]); }
  // 右緣圈列
  圈(6.75, 2.6); 圈(6.7, 4.1); 圈(6.8, 5.3); 圈(6.9, 6.5); 圈(6.8, 7.7); 圈(6.3, 8.2);

  // 滿幅桁
  線(p => p.M(X(0.8), Z(9.4)).L(X(6.8), Z(9.3)), 0.44);

  // 左目目箱（二格）
  線(p => p.M(X(0.9), Z(10.2)).L(X(0.9), Z(15.2)).L(X(3.4), Z(15.15))
    .L(X(3.4), Z(10.15)).L(X(0.85), Z(10.2)));
  線(p => p.M(X(1.0), Z(11.8)).L(X(3.3), Z(11.77)), 0.32);
  線(p => p.M(X(1.0), Z(13.5)).L(X(3.3), Z(13.47)), 0.32);

  // 右豎撇捺曲（⺁乂摹形從簡）
  線(p => p.M(X(4.3), Z(10.1)).L(X(4.32), Z(14.9)), 0.4);
  線(p => p.M(X(4.4), Z(10.15)).L(X(6.6), Z(10.1)), 0.38);
  出(p => p.M(X(6.6), Z(10.2))
    .C(X(6.1), Z(11.8), X(5.4), Z(13.4), X(4.5), Z(14.9)), 0.4);
  出(p => p.M(X(5.0), Z(11.6))
    .C(X(5.5), Z(12.8), X(6.0), Z(13.9), X(6.4), Z(14.8)), 0.38);

  // 口口對
  線(p => p.M(X(1.0), Z(15.8)).L(X(1.0), Z(17.3)).L(X(3.0), Z(17.25))
    .L(X(3.0), Z(15.75)).L(X(0.95), Z(15.8)));
  線(p => p.M(X(1.1), Z(16.55)).L(X(2.9), Z(16.52)), 0.3);
  線(p => p.M(X(4.2), Z(15.75)).L(X(4.2), Z(17.25)).L(X(6.2), Z(17.2))
    .L(X(6.2), Z(15.7)).L(X(4.15), Z(15.75)));
  線(p => p.M(X(4.3), Z(16.5)).L(X(6.1), Z(16.47)), 0.3);

  // 底寬目箱
  線(p => p.M(X(0.9), Z(17.9)).L(X(0.9), Z(19.15)).L(X(6.6), Z(19.1))
    .L(X(6.6), Z(17.85)).L(X(0.85), Z(17.9)), 0.4);
  線(p => p.M(X(1.0), Z(18.5)).L(X(6.5), Z(18.47)), 0.34);
};
