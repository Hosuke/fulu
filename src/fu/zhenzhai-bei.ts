// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭除北方土氣耗損之鬼 · 親筆（五方土氣組・北）
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p182_符葉二.jpg（下排左一）
// 判準：二圈桿符頭＋左折線星（<形三圈）＋右散圈柱＋頂匣・雙塔梯身承滿幅帶＋
// 鬼形三渦足（田匣承巳渦）挾外欄。錨自 24 指格網（宅_bei.png），X=x、Z=z-4。
// 幅比 2.0。釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅北方: 符筆 = (bi) => {
  const u = bi.u;
  const X = (x: number) => x;
  const Z = (z: number) => z - 4;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.34) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.42) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.26 * u;
    bi.A(X(x), Z(z), r); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.55, Z(z)).L(X(x1) - 0.55, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：二圈桿
  桿(-6.1, 5.4, 6.9);
  桿(-6.1, 5.3, 8.7);
  // 左折線星（<形：左圈射二斜圈）
  {
    const a: [number, number] = [-6.2, 11.2], b: [number, number] = [-2.5, 10.2], c: [number, number] = [-2.6, 12.9];
    for (const t of [b, c]) {
      const dx = t[0] - a[0], dz = t[1] - a[1], L = Math.hypot(dx, dz), s0 = 0.55 / L, s1 = 0.5 / L;
      線(p => p.M(X(a[0] + dx * s0), Z(a[1] + dz * s0)).L(X(t[0] - dx * s1), Z(t[1] - dz * s1)), 0.4);
    }
    圈(a[0], a[1], 0.44); 圈(b[0], b[1], 0.44); 圈(c[0], c[1], 0.44);
  }
  // 右散圈柱（大小相間）
  圈(1.8, 10.5, 0.4); 圈(5.0, 10.6, 0.46);
  圈(5.1, 12.9, 0.4); 圈(0.9, 13.4, 0.3);
  圈(5.2, 15.3, 0.46); 圈(0.8, 17.2, 0.3); 圈(5.3, 17.6, 0.46);

  // 頂匣（左傾長匣三桁）
  線(p => p.M(X(-5.8), Z(14.1)).L(X(0.2), Z(14.0)).L(X(0.2), Z(17.6)).L(X(-5.8), Z(17.7)).L(X(-5.8), Z(14.1)));
  線(p => p.M(X(-5.6), Z(15.3)).L(X(0.0), Z(15.2)), 0.38);
  線(p => p.M(X(-5.6), Z(16.5)).L(X(0.0), Z(16.4)), 0.38);

  // 雙塔梯身（左右各三匣），中縫豎
  const 匣 = (x0: number, x1: number, z0: number, z1: number, 桁: number[]) => {
    線(p => p.M(X(x0), Z(z0 - 0.2)).L(X(x0), Z(z1 + 0.2)));
    線(p => p.M(X(x1), Z(z0 - 0.2)).L(X(x1), Z(z1 + 0.2)));
    for (const z of [z0, ...桁, z1]) 線(p => p.M(X(x0) - 0.25, Z(z)).L(X(x1) + 0.25, Z(z)));
  };
  匣(-6.6, -1.5, 18.4, 21.4, [19.9]);
  匣(-6.6, -1.5, 22.4, 25.2, [23.8]);
  匣(-6.6, -1.5, 26.2, 28.4, [27.3]);
  匣(-0.6, 5.4, 18.2, 21.2, [19.7]);
  匣(-0.6, 5.5, 22.2, 25.0, [23.6]);
  匣(-0.6, 5.5, 26.0, 28.2, [27.1]);
  線(p => p.M(X(-1.05), Z(18.3)).L(X(-1.05), Z(28.3)), 0.38);

  // 滿幅帶（二桁閉匡）
  線(p => p.M(X(-7.0), Z(29.2)).L(X(6.0), Z(29.0)).L(X(6.0), Z(31.4)).L(X(-7.0), Z(31.6)).L(X(-7.0), Z(29.2)), 0.46);
  線(p => p.M(X(-6.8), Z(30.3)).L(X(5.8), Z(30.2)), 0.38);
  線(p => p.M(X(-0.4), Z(29.2)).L(X(-0.4), Z(31.5)), 0.36);

  // 鬼形三渦足（田匣承巳渦）＋左右外欄
  const 鬼尾 = (cx: number, z0: number) => {
    線(p => p.M(X(cx - 1.6), Z(z0)).L(X(cx + 1.6), Z(z0 - 0.1)).L(X(cx + 1.6), Z(z0 + 2.0))
      .L(X(cx - 1.6), Z(z0 + 2.1)).L(X(cx - 1.6), Z(z0)));
    線(p => p.M(X(cx - 1.5), Z(z0 + 1.0)).L(X(cx + 1.5), Z(z0 + 0.9)), 0.36);
    線(p => p.M(X(cx), Z(z0)).L(X(cx), Z(z0 + 2.0)), 0.36);
    線(p => p.M(X(cx + 1.1), Z(z0 + 2.2))
      .C(X(cx + 1.3), Z(z0 + 3.1), X(cx + 0.5), Z(z0 + 4.0), X(cx - 0.6), Z(z0 + 4.2))
      .C(X(cx - 1.5), Z(z0 + 4.4), X(cx - 1.7), Z(z0 + 3.5), X(cx - 0.9), Z(z0 + 3.2))
      .C(X(cx - 0.4), Z(z0 + 3.0), X(cx - 0.1), Z(z0 + 3.4), X(cx - 0.3), Z(z0 + 3.7)), 0.44);
  };
  鬼尾(-4.4, 33.4);
  鬼尾(-0.2, 33.2);
  鬼尾(3.6, 33.4);
  線(p => p.M(X(-6.8), Z(32.0)).C(X(-7.0), Z(34.4), X(-7.0), Z(36.8), X(-6.7), Z(39.2)), 0.44);
  線(p => p.M(X(5.9), Z(31.8)).C(X(6.1), Z(34.2), X(6.1), Z(36.6), X(5.8), Z(39.0)), 0.44);
};
