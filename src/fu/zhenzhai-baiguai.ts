// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭百恠口舌之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p185_符葉五.jpg（上段右三）
// 判準：星紋符頭（桿・斜圈鏈・左右圈柱・散圈叢）＋雙密目梯（各六箱，右柱
// 上箱帶月杙）＋∞蝶結足對＋川尾。錨自 12 格裁圖（宅185_A baiguai）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅百恠: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：桿＋斜圈鏈＋左右圈柱＋散圈叢
  線(p => p.M(X(0.9), Z(1.5)).L(X(6.7), Z(1.4))); 圈(0.6, 1.5); 圈(7.0, 1.4);
  連([0.7, 2.5], [2.5, 2.2]); 圈(0.7, 2.5); 圈(2.5, 2.2);
  for (const z of [3.6, 4.6, 5.6]) 圈(0.6, z);
  for (const z of [2.5, 3.5, 4.5, 5.5]) 圈(6.9, z);
  for (const [x, z] of [[2.7, 3.0], [3.6, 2.8], [4.5, 3.0], [2.8, 4.2], [3.8, 4.0],
                        [4.7, 4.2], [3.0, 5.4], [4.0, 5.2]] as const) 圈(x, z);

  // 雙密目梯（各六箱）＋右上月杙
  const 梯 = (x0: number, x1: number) => {
    線(p => p.M(X(x0), Z(6.3)).L(X(x0), Z(15.8)));
    線(p => p.M(X(x1), Z(6.25)).L(X(x1), Z(15.75)));
    for (let i = 0; i <= 6; i++)
      線(p => p.M(X(x0), Z(6.3 + i * 1.583)).L(X(x1), Z(6.25 + i * 1.583)));
  };
  梯(0.8, 3.3); 梯(4.4, 6.9);
  線(p => p.M(X(5.2), Z(6.5)).L(X(5.18), Z(7.7)), 0.32);
  線(p => p.M(X(6.0), Z(6.45)).L(X(5.98), Z(7.65)), 0.32);

  // ∞蝶結足對＋川尾
  const 蝶 = (cx: number) => 線(p => p.M(X(cx - 1.1), Z(16.3))
    .C(X(cx - 1.5), Z(17.2), X(cx - 0.6), Z(17.6), X(cx), Z(17.0))
    .C(X(cx + 0.6), Z(16.4), X(cx + 1.5), Z(16.8), X(cx + 1.1), Z(17.6))
    .C(X(cx + 0.8), Z(18.1), X(cx - 0.2), Z(18.0), X(cx - 0.4), Z(17.4))
    .C(X(cx - 0.6), Z(16.9), X(cx - 1.0), Z(16.8), X(cx - 1.3), Z(17.1)), 0.36);
  蝶(2.0); 蝶(5.6);
  線(p => p.M(X(1.4), Z(18.2)).C(X(1.3), Z(18.6), X(1.3), Z(18.9), X(1.35), Z(19.1)));
  線(p => p.M(X(6.3), Z(18.2)).C(X(6.4), Z(18.6), X(6.4), Z(18.9), X(6.35), Z(19.1)));
};
