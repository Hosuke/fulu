// 據圖版：參攷-local/複文/太平經符_Talismans_in_Taipingjing.jpg（右半左起第1行首格）
// 判準：雙見並立（覞形）——目匡含雙桁，儿足左斂右鉤。楷隸筆意。
// 幅比 0.95（z 0..22.8）。釋讀〔待核〕，名從構件形貌。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 複文見見: 符筆 = (bi) => {
  const u = bi.u;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.8) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 曳 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.95) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  for (const cx of [-4.6, 4.6]) {
    // 目匡：雙豎四桁
    橫(p => p.M(cx - 2.1, 3.5).L(cx - 2.1, 11.2));
    橫(p => p.M(cx + 2.1, 3.5).L(cx + 2.1, 11.2));
    橫(p => p.M(cx - 2.2, 3.7).L(cx + 2.2, 3.6));
    橫(p => p.M(cx - 2.1, 6.2).L(cx + 2.1, 6.1));
    橫(p => p.M(cx - 2.1, 8.7).L(cx + 2.1, 8.6));
    橫(p => p.M(cx - 2.2, 11.1).L(cx + 2.2, 11.0));
    // 儿足：左斂、右長鉤上挑
    曳(p => p.M(cx - 0.9, 11.3)
      .C(cx - 1.6, 13.4, cx - 2.6, 15.4, cx - 4.0, 17.2));
    bi.一筆({ 寬: 0.95 * u, 起: '藏', 收: '出' }, p => p.M(cx + 0.9, 11.3)
      .C(cx + 1.2, 13.4, cx + 1.6, 15.4, cx + 2.2, 17.0)
      .C(cx + 2.9, 18.0, cx + 4.0, 17.7, cx + 4.5, 16.3));
  }
};
