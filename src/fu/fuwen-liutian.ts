// 複文・天天天天天天（六天疊，三三兩行） · 親筆
// 據圖版：參攷-local/複文/道藏太平經卷104首_興上除害複文169_三家本冊24頁524.png
// （正統道藏原式，三家本影印冊24頁524；興上除害複文第一百六十九）
// 判準：六天三三兩行——8x 親核（複_五格板；初判四天，親核正為六）。
// 釋讀待核，名從形貌。幅比 0.9（z 0..21.6）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 複文六天: 符筆 = (bi) => {
  const u = bi.u;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.85) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.9) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  const 天 = (cx: number, cz: number) => {
    線(p => p.M(cx - 2.0, cz).C(cx - 0.9, cz - 0.25, cx + 0.7, cz - 0.25, cx + 2.0, cz), 0.8);
    線(p => p.M(cx - 2.7, cz + 1.7).C(cx - 1.2, cz + 1.45, cx + 1.0, cz + 1.45, cx + 2.7, cz + 1.7), 0.85);
    出(p => p.M(cx + 0.15, cz + 0.1).C(cx - 0.4, cz + 2.2, cx - 1.2, cz + 3.9, cx - 2.6, cz + 5.2), 0.9);
    出(p => p.M(cx - 0.4, cz + 2.6).C(cx + 0.5, cz + 3.6, cx + 1.5, cz + 4.5, cx + 2.6, cz + 5.1), 0.9);
  };
  for (const cx of [-7.4, 0, 7.4]) 天(cx, 3.2);
  for (const cx of [-7.4, 0, 7.4]) 天(cx, 12.6);
};
