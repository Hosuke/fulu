// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭除太歲疾疫之侵 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p184_符葉四.jpg（下段右四）
// 判準：星紋符頭（桿・右圈柱五・厶匣田王組土杙）＋寬目梯疊（八桁帶中杙
// 干記）＋底九鉤大曳。摹形從簡註。錨自 12 格裁圖（宅184_C taisui）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅太歲: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.42) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿＋右圈柱五
  線(p => p.M(X(0.9), Z(1.1)).L(X(6.6), Z(1.0))); 圈(6.95, 1.0);
  for (const z of [2.0, 3.0, 4.0, 5.0, 5.9]) 圈(7.0, z);

  // 厶匣田王組＋土杙
  線(p => p.M(X(1.2), Z(2.6)).L(X(1.2), Z(4.4)).L(X(3.2), Z(4.35))
    .L(X(3.2), Z(2.55)).L(X(1.15), Z(2.6)));
  線(p => p.M(X(2.4), Z(2.9)).C(X(2.0), Z(3.3), X(1.8), Z(3.7), X(1.7), Z(4.0)), 0.32);
  線(p => p.M(X(2.1), Z(3.5)).L(X(2.8), Z(3.9)), 0.32);
  線(p => p.M(X(3.7), Z(2.5)).L(X(3.7), Z(4.9)).L(X(5.9), Z(4.85))
    .L(X(5.9), Z(2.45)).L(X(3.65), Z(2.5)));
  線(p => p.M(X(3.8), Z(3.65)).L(X(5.8), Z(3.6)), 0.32);
  線(p => p.M(X(4.8), Z(2.55)).L(X(4.78), Z(4.85)), 0.32);
  線(p => p.M(X(1.5), Z(5.3)).L(X(1.48), Z(6.4)), 0.34);
  線(p => p.M(X(1.0), Z(5.8)).L(X(2.0), Z(5.77)), 0.34);
  線(p => p.M(X(0.9), Z(6.5)).L(X(2.1), Z(6.47)), 0.34);
  線(p => p.M(X(2.9), Z(5.4)).L(X(5.4), Z(5.35)), 0.36);

  // 寬目梯疊（八桁帶中杙干記）
  線(p => p.M(X(0.8), Z(7.2)).L(X(0.8), Z(16.8)));
  線(p => p.M(X(6.8), Z(7.15)).L(X(6.8), Z(16.75)));
  for (let i = 0; i <= 7; i++)
    線(p => p.M(X(0.8), Z(7.2 + i * 1.371)).L(X(6.8), Z(7.15 + i * 1.371)));
  線(p => p.M(X(3.8), Z(7.3)).L(X(3.77), Z(11.9)), 0.34);
  線(p => p.M(X(2.9), Z(12.4)).L(X(2.88), Z(15.5)), 0.34);
  線(p => p.M(X(4.7), Z(12.35)).L(X(4.68), Z(15.45)), 0.34);

  // 底九鉤大曳
  出(p => p.M(X(4.4), Z(17.2)).C(X(4.2), Z(17.9), X(4.0), Z(18.4), X(3.7), Z(18.7)), 0.42);
  出(p => p.M(X(4.1), Z(17.5)).L(X(5.6), Z(17.45))
    .C(X(6.1), Z(17.9), X(6.3), Z(18.3), X(6.2), Z(18.7))
    .C(X(6.15), Z(18.95), X(5.9), Z(19.0), X(5.6), Z(18.9)), 0.42);
};
