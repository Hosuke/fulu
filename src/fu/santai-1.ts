// 上清三台神符（一，右起首符） · 親筆
// 據圖版：參攷-local/紫微諱/樣頁/樣_1430_天樞院回車畢道正法卷上三台神符-p31.png
// （左緣三符柱之右一；p32「夫上清三台符者…用黃綾書此真符戴於頭上」——
// 行持說明與咒記位不錄；黃綾書＝書材註記）
// 判準：楷意疊書符柱——●右點首桿＋勹乙大鉤＋密亖帶十一桁（右緣豎）＋
// ●虛弧承∩實拱雙●足（右緣豎）＋目框四桁＋尸巳曲帶＋密亖五桁嵌己曲＋
// 波亖四桁＋王田帶＋弓咼曲群＋利形＋三●豎並行承中垂木橫皿曲＋長垂曲尾。
// 摹形從簡。錨自 6 格柱裁圖（格柱_柱一，48 行）：X=(c-2.8)×1.5、
// Z=(r-0.7)×1.5+1.2。幅比 3。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 三台神符一: 符筆 = (bi) => {
  const u = bi.u, k = 1.5;
  const X = (c: number) => (c - 2.8) * k;
  const Z = (r: number) => (r - 0.7) * k + 1.2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.3) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.32) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 點 = (x: number, z: number, r = 0.3) =>
    bi.塊(p => p.M(X(x - r), Z(z)).C(X(x - r), Z(z - r * 1.3), X(x + r), Z(z - r * 1.3), X(x + r), Z(z))
      .C(X(x + r), Z(z + r * 1.3), X(x - r), Z(z + r * 1.3), X(x - r), Z(z)).close());

  // ●右點首桿
  點(5.0, 1.0, 0.38); 線(p => p.M(X(0.25), Z(0.95)).L(X(4.55), Z(0.9)), 0.34);
  // 勹乙大鉤
  出(p => p.M(X(0.6), Z(1.5))
    .C(X(2.2), Z(1.35), X(3.7), Z(1.5), X(4.0), Z(2.0))
    .C(X(4.15), Z(2.35), X(3.6), Z(2.7), X(2.6), Z(2.85))
    .C(X(1.9), Z(2.95), X(1.3), Z(2.9), X(1.0), Z(2.7)), 0.34);

  // 密亖帶十一桁（右緣豎）
  for (const z of [3.1, 3.75, 4.4, 5.05, 5.65, 6.3, 6.95, 7.55, 8.2, 8.85, 9.5])
    線(p => p.M(X(0.25), Z(z)).L(X(5.15), Z(z - 0.04)), 0.3);
  線(p => p.M(X(5.25), Z(3.15)).L(X(5.23), Z(9.55)), 0.32);
  線(p => p.M(X(3.4), Z(4.4)).L(X(3.38), Z(5.65)), 0.28);
  線(p => p.M(X(3.5), Z(7.55)).L(X(3.48), Z(9.5)), 0.28);

  // ●虛弧承∩實拱雙●足（右緣豎）
  點(2.7, 10.85, 0.32);
  for (const [x, z] of [[1.2, 11.35], [1.9, 11.1], [3.4, 11.1], [4.1, 11.35]] as const)
    線(p => p.M(X(x), Z(z)).L(X(x + 0.32), Z(z - 0.06)), 0.26);
  線(p => p.M(X(1.0), Z(13.3))
    .C(X(0.95), Z(11.9), X(1.5), Z(11.25), X(2.3), Z(11.25))
    .C(X(3.1), Z(11.25), X(3.5), Z(11.9), X(3.45), Z(13.1)), 0.32);
  點(1.0, 13.35, 0.34); 點(3.45, 13.2, 0.34);
  線(p => p.M(X(5.15), Z(10.55)).L(X(5.13), Z(13.6)), 0.32);
  線(p => p.M(X(4.6), Z(10.5)).L(X(5.15), Z(10.55)), 0.3);

  // 目框四桁
  線(p => p.M(X(0.3), Z(14.0)).L(X(5.35), Z(13.95)), 0.32);
  線(p => p.M(X(0.6), Z(14.7)).L(X(0.6), Z(16.1)).L(X(5.2), Z(16.05))
    .L(X(5.2), Z(14.65)).L(X(0.55), Z(14.7)), 0.3);
  線(p => p.M(X(0.7), Z(15.4)).L(X(5.1), Z(15.35)), 0.28);

  // 尸巳曲帶
  線(p => p.M(X(0.4), Z(16.6)).L(X(5.15), Z(16.55)), 0.32);
  線(p => p.M(X(5.1), Z(16.55)).L(X(5.08), Z(18.35)), 0.3);
  線(p => p.M(X(1.2), Z(17.1))
    .C(X(2.8), Z(17.0), X(3.1), Z(17.4), X(2.0), Z(17.55))
    .C(X(1.1), Z(17.65), X(1.2), Z(18.05), X(2.4), Z(18.1))
    .C(X(2.9), Z(18.12), X(3.1), Z(18.25), X(3.1), Z(18.35)), 0.3);

  // 密亖五桁嵌己曲（左尸鉤）
  for (const z of [18.7, 19.3, 19.9, 20.5, 21.1])
    線(p => p.M(X(0.35), Z(z)).L(X(5.25), Z(z - 0.04)), 0.3);
  出(p => p.M(X(0.45), Z(19.95)).C(X(0.4), Z(20.5), X(0.35), Z(21.0), X(0.3), Z(21.5)), 0.3);
  線(p => p.M(X(2.4), Z(19.6))
    .C(X(3.4), Z(19.55), X(3.5), Z(19.85), X(2.8), Z(19.95))
    .C(X(2.2), Z(20.05), X(2.3), Z(20.3), X(3.2), Z(20.35)), 0.28);

  // 波亖四桁（左豎）
  出(p => p.M(X(0.35), Z(22.0))
    .C(X(1.6), Z(21.8), X(2.8), Z(22.2), X(4.0), Z(22.0))
    .C(X(4.5), Z(21.9), X(4.9), Z(22.05), X(5.2), Z(21.95)), 0.3);
  for (const z of [22.7, 23.4, 24.0])
    線(p => p.M(X(0.35), Z(z)).L(X(5.2), Z(z - 0.04)), 0.3);
  線(p => p.M(X(0.5), Z(22.0)).L(X(0.48), Z(24.0)), 0.3);

  // 王田帶
  線(p => p.M(X(0.7), Z(24.7)).L(X(2.4), Z(24.66)), 0.3);
  線(p => p.M(X(0.7), Z(25.5)).L(X(2.4), Z(25.46)), 0.3);
  線(p => p.M(X(0.7), Z(26.3)).L(X(2.4), Z(26.26)), 0.3);
  線(p => p.M(X(1.55), Z(24.7)).L(X(1.53), Z(26.3)), 0.3);
  線(p => p.M(X(2.9), Z(24.55)).L(X(2.9), Z(26.4)).L(X(5.0), Z(26.35))
    .L(X(5.0), Z(24.5)).L(X(2.85), Z(24.55)), 0.3);
  線(p => p.M(X(3.95), Z(24.6)).L(X(3.93), Z(26.35)), 0.28);
  線(p => p.M(X(3.0), Z(25.45)).L(X(4.9), Z(25.4)), 0.28);

  // 弓咼曲群
  線(p => p.M(X(0.5), Z(27.8))
    .C(X(1.9), Z(27.7), X(2.1), Z(28.15), X(1.2), Z(28.3))
    .C(X(0.4), Z(28.45), X(0.5), Z(28.9), X(1.5), Z(28.95))
    .C(X(2.0), Z(28.98), X(2.1), Z(29.3), X(1.8), Z(29.6)), 0.3);
  線(p => p.M(X(2.7), Z(27.75)).L(X(2.7), Z(29.3)).L(X(4.9), Z(29.25))
    .L(X(4.9), Z(27.7)).L(X(2.65), Z(27.75)), 0.3);
  線(p => p.M(X(3.3), Z(28.2))
    .C(X(4.3), Z(28.15), X(4.4), Z(28.5), X(3.7), Z(28.6))
    .C(X(3.2), Z(28.68), X(3.3), Z(28.95), X(4.1), Z(29.0)), 0.28);
  出(p => p.M(X(4.85), Z(29.3)).C(X(4.9), Z(29.7), X(4.8), Z(30.0), X(4.6), Z(30.2)), 0.3);

  // 利形（禾＋刂）
  線(p => p.M(X(0.6), Z(30.9)).L(X(2.5), Z(30.85)), 0.3);
  出(p => p.M(X(1.55), Z(30.5)).L(X(1.53), Z(33.3)), 0.32);
  出(p => p.M(X(1.5), Z(31.3)).L(X(0.75), Z(32.1)), 0.28);
  出(p => p.M(X(1.6), Z(31.3)).L(X(2.3), Z(32.05)), 0.28);
  線(p => p.M(X(3.3), Z(30.6)).L(X(3.28), Z(32.9)), 0.3);
  出(p => p.M(X(4.5), Z(30.5)).L(X(4.48), Z(33.1))
    .C(X(4.46), Z(33.4), X(4.25), Z(33.5), X(4.0), Z(33.4)), 0.3);

  // 三●豎並行
  for (const cx of [1.2, 2.65, 4.15]) {
    點(cx, 34.0, 0.28);
    出(p => p.M(X(cx), Z(34.3)).L(X(cx - 0.02), Z(36.4)), 0.3);
  }
  // 中垂承木橫皿曲
  出(p => p.M(X(2.65), Z(36.5)).L(X(2.63), Z(40.2)), 0.34);
  線(p => p.M(X(1.2), Z(37.3)).L(X(4.1), Z(37.25)), 0.3);
  出(p => p.M(X(2.6), Z(37.6)).C(X(2.0), Z(38.3), X(1.5), Z(38.8), X(1.05), Z(39.15)), 0.28);
  出(p => p.M(X(2.7), Z(37.6)).C(X(3.3), Z(38.3), X(3.8), Z(38.8), X(4.25), Z(39.1)), 0.28);
  線(p => p.M(X(1.3), Z(39.7))
    .C(X(1.2), Z(40.3), X(1.6), Z(40.6), X(2.2), Z(40.55)), 0.28);

  // 長垂曲尾
  出(p => p.M(X(2.7), Z(40.6)).L(X(2.68), Z(45.3))
    .C(X(2.66), Z(45.9), X(2.4), Z(46.2), X(2.0), Z(46.3)), 0.36);
  點(1.5, 42.3, 0.2); 點(3.6, 43.6, 0.2);
};
