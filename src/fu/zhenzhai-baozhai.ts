// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・保宅舍門戶人口清吉 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p183_符葉三.jpg（下段右四）
// 判準：星紋符頭（桿・波桿綴中圈・大凵盆內圈對）＋迴帶二折＋雙目疊夾
// 中杙貫底＋弓迴足。錨自 12 格裁圖（宅183_B baozhai）：X=(c-3.95)×2.7、
// Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅保宅: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿＋波桿綴中圈＋大凵盆內圈對
  線(p => p.M(X(1.2), Z(1.0)).L(X(6.9), Z(0.9))); 圈(0.9, 1.0); 圈(7.2, 0.9);
  線(p => p.M(X(1.2), Z(2.25)).C(X(2.2), Z(2.05), X(3.2), Z(2.0), X(4.0), Z(2.05)), 0.36);
  線(p => p.M(X(4.7), Z(2.05)).C(X(5.6), Z(2.1), X(6.5), Z(2.15), X(7.0), Z(2.2)), 0.36);
  圈(0.9, 2.3); 圈(4.35, 2.0); 圈(7.3, 2.25);
  // 大凵盆
  線(p => p.M(X(0.95), Z(3.5))
    .C(X(1.1), Z(4.8), X(1.3), Z(5.7), X(1.9), Z(6.0))
    .L(X(5.7), Z(5.9))
    .C(X(6.4), Z(5.75), X(6.7), Z(4.7), X(6.85), Z(3.4)), 0.42);
  圈(4.2, 3.9); 圈(4.3, 5.0);

  // 迴帶二折
  線(p => p.M(X(1.1), Z(7.2)).L(X(6.2), Z(7.15))
    .C(X(6.7), Z(7.15), X(6.7), Z(8.1), X(6.2), Z(8.15))
    .L(X(1.9), Z(8.2)).C(X(1.4), Z(8.2), X(1.4), Z(9.15), X(1.9), Z(9.2))
    .L(X(6.5), Z(9.15)));

  // 雙目疊夾中杙貫底
  const 梯 = (x0: number, x1: number) => {
    線(p => p.M(X(x0), Z(9.8)).L(X(x0), Z(14.0)));
    線(p => p.M(X(x1), Z(9.75)).L(X(x1), Z(13.95)));
    for (let i = 0; i <= 2; i++)
      線(p => p.M(X(x0), Z(9.8 + i * 2.1)).L(X(x1), Z(9.75 + i * 2.1)));
  };
  梯(1.2, 3.5); 梯(4.9, 7.2);
  線(p => p.M(X(4.25), Z(9.8)).L(X(4.2), Z(17.5)), 0.4);

  // 弓迴足
  線(p => p.M(X(1.3), Z(14.5)).L(X(5.7), Z(14.45))
    .C(X(6.2), Z(14.45), X(6.2), Z(15.4), X(5.7), Z(15.45))
    .L(X(2.1), Z(15.5)).C(X(1.6), Z(15.5), X(1.6), Z(16.45), X(2.1), Z(16.5))
    .L(X(6.4), Z(16.45)));
  線(p => p.M(X(1.5), Z(17.0)).C(X(1.3), Z(17.7), X(1.2), Z(18.3), X(1.2), Z(18.8)));
  線(p => p.M(X(6.5), Z(16.9)).C(X(6.7), Z(17.6), X(6.8), Z(18.2), X(6.8), Z(18.7)));
};
