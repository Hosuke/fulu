// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭夢寐驚人鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p185_符葉五.jpg（下段右二）
// 判準：星紋符頭（短桿對・人字圈鏈・散圈）＋鬼鬼鬼三聯曲（口首匕尾）＋
// 寬目田箱疊＋底桁二。摹形從簡註。錨自 12 格裁圖（宅185_B mengmei）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅夢寐: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：短桿對＋人字圈鏈＋散圈
  線(p => p.M(X(1.1), Z(1.4)).L(X(2.4), Z(1.35))); 圈(0.8, 1.4); 圈(2.7, 1.35);
  線(p => p.M(X(4.4), Z(1.35)).L(X(5.7), Z(1.3))); 圈(4.1, 1.35); 圈(6.0, 1.3);
  圈(3.4, 2.6); 連([3.4, 2.6], [1.8, 3.6]); 連([3.4, 2.6], [5.0, 3.6]);
  圈(1.8, 3.6); 圈(5.0, 3.6);
  圈(0.6, 2.7); 圈(6.5, 2.6); 圈(0.7, 4.5); 圈(6.4, 4.4); 圈(2.7, 4.9); 圈(4.1, 4.8);

  // 鬼鬼鬼三聯曲（口首匕尾——摹形）
  const 鬼曲 = (cx: number) => {
    線(p => p.M(X(cx - 0.6), Z(5.9)).L(X(cx - 0.6), Z(6.9)).L(X(cx + 0.6), Z(6.87))
      .L(X(cx + 0.6), Z(5.87)).L(X(cx - 0.65), Z(5.9)), 0.32);
    線(p => p.M(X(cx - 0.4), Z(7.1))
      .C(X(cx - 0.7), Z(7.7), X(cx - 0.5), Z(8.2), X(cx + 0.1), Z(8.3))
      .C(X(cx + 0.6), Z(8.4), X(cx + 0.8), Z(8.0), X(cx + 0.5), Z(7.75)), 0.34);
  };
  鬼曲(1.6); 鬼曲(3.6); 鬼曲(5.6);

  // 寬目田箱疊
  線(p => p.M(X(0.9), Z(9.2)).L(X(0.9), Z(13.9)));
  線(p => p.M(X(6.5), Z(9.15)).L(X(6.5), Z(13.85)));
  for (const z of [9.2, 10.75, 12.3, 13.9]) 線(p => p.M(X(0.9), Z(z)).L(X(6.5), Z(z - 0.05)));
  線(p => p.M(X(2.8), Z(9.3)).L(X(2.78), Z(13.8)), 0.34);
  線(p => p.M(X(4.6), Z(9.25)).L(X(4.58), Z(13.75)), 0.34);
  // 中箱兒杙
  線(p => p.M(X(3.2), Z(11.2)).L(X(4.2), Z(11.17)), 0.3);
  線(p => p.M(X(3.7), Z(10.8)).L(X(3.68), Z(12.2)), 0.3);

  // 底桁二
  線(p => p.M(X(1.0), Z(15.0)).L(X(6.4), Z(14.9)), 0.42);
  線(p => p.M(X(1.0), Z(16.2)).L(X(6.4), Z(16.1)), 0.42);
  線(p => p.M(X(1.2), Z(16.9)).C(X(1.1), Z(17.6), X(1.1), Z(18.2), X(1.2), Z(18.7)));
  線(p => p.M(X(6.2), Z(16.8)).C(X(6.3), Z(17.5), X(6.3), Z(18.1), X(6.2), Z(18.6)));
};
