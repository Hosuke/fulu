// ─────────────────────────────────────────────────────────────────────────────
// 起風符（道法會元卷九十四・雨頭十五類符・一） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3923_卷94十五類符雨頭一覽-p26.png（右起第一）
// 判準：雨字符頭通制（頂桁・冠框・內左三橫右三弧・中豎長垂・●墨點・左右
// 長腳）＋右斤形（橫撇長腳）＋中与曲承弓弓 S 垂。符下咒音記位不錄。摹形從簡。
// 錨自 8 格裁圖（格94_五 qifeng）：X=(c-3.75)×2.55、Z=(r-4.6)×2.55+1.0。
// 幅比 3。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 起風符: 符筆 = (bi) => {
  const u = bi.u, k = 2.55;
  const X = (c: number) => (c - 3.75) * k;
  const Z = (r: number) => (r - 4.6) * k + 1.0;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.34) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.36) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 點 = (x: number, z: number, r = 0.4) =>
    bi.塊(p => p.M(X(x - r), Z(z)).C(X(x - r), Z(z - r * 1.3), X(x + r), Z(z - r * 1.3), X(x + r), Z(z))
      .C(X(x + r), Z(z + r * 1.3), X(x - r), Z(z + r * 1.3), X(x - r), Z(z)).close());

  // 雨冠：頂桁・左長腳・右框豎・內三橫三弧・中豎長垂
  線(p => p.M(X(0.15), Z(4.85)).L(X(7.3), Z(4.78)), 0.84);
  出(p => p.M(X(0.4), Z(4.90)).L(X(0.38), Z(20.97)).C(X(0.37), Z(21.95), X(0.25), Z(22.59), X(0.05), Z(23.08)), 0.76);
  線(p => p.M(X(6.85), Z(4.90)).L(X(6.83), Z(11.42)), 0.72);
  線(p => p.M(X(0.9), Z(6.00)).L(X(2.7), Z(5.96)), 0.57);
  線(p => p.M(X(0.9), Z(6.60)).L(X(2.7), Z(6.56)), 0.57);
  線(p => p.M(X(0.9), Z(7.20)).L(X(2.7), Z(7.16)), 0.57);
  線(p => p.M(X(4.3), Z(6.00)).C(X(5.15), Z(5.82), X(5.65), Z(5.90), X(6.0), Z(6.05)), 0.57);
  線(p => p.M(X(4.3), Z(6.60)).C(X(5.15), Z(6.42), X(5.65), Z(6.50), X(6.0), Z(6.65)), 0.57);
  線(p => p.M(X(4.3), Z(7.20)).C(X(5.15), Z(7.02), X(5.65), Z(7.10), X(6.0), Z(7.25)), 0.57);
  線(p => p.M(X(3.6), Z(5.00)).L(X(3.58), Z(11.58)), 0.72);
  // 右斤形（橫・撇・長腳豎）
  線(p => p.M(X(4.5), Z(8.15)).L(X(6.5), Z(8.08)), 0.68);
  出(p => p.M(X(4.55), Z(8.20)).C(X(4.3), Z(9.31), X(4.1), Z(10.28), X(3.95), Z(11.09)), 0.65);
  出(p => p.M(X(5.9), Z(8.20)).L(X(5.88), Z(24.38)), 0.76);
  // 中与曲承弓弓 S
  線(p => p.M(X(3.55), Z(11.74)).C(X(2.3), Z(12.23), X(2.2), Z(13.36), X(3.3), Z(13.52)).C(X(4.3), Z(13.68), X(4.3), Z(14.66), X(3.2), Z(14.98)), 0.68);
  出(p => p.M(X(3.2), Z(15.14)).C(X(2.4), Z(15.95), X(2.5), Z(17.09), X(3.3), Z(17.25)).C(X(4.1), Z(17.41), X(4.1), Z(18.54), X(3.2), Z(18.87)).C(X(2.4), Z(19.19), X(2.5), Z(20.33), X(3.2), Z(20.65)).L(X(3.16), Z(22.11)), 0.68);
  點(3.2, 23.00);
};
