// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・盜賊不侵萬事稱意 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p183_符葉三.jpg（上段右三）
// 判準：星紋符頭（桿綴中圈・人字鏈・乂交圈鏈・側圈）＋尖冠桁＋三柱目梯疊
// （左右各五箱・中柱生杙）＋底匚。錨自 12 格裁圖（宅183_A buqin）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅不侵: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：桿＋人字鏈＋乂交圈鏈＋側圈
  線(p => p.M(X(0.9), Z(1.0)).L(X(7.0), Z(0.9))); 圈(0.6, 1.0); 圈(7.3, 0.9);
  圈(2.6, 1.4); 連([2.6, 1.4], [4.0, 2.0]); 圈(4.0, 2.0);
  圈(1.5, 3.4); 圈(4.4, 3.3); 圈(1.6, 4.9); 圈(4.5, 4.8);
  連([1.5, 3.4], [4.5, 4.8]); 連([4.4, 3.3], [1.6, 4.9]);
  圈(0.5, 2.8); 圈(7.2, 2.6); 圈(0.5, 4.3); 圈(7.3, 4.2);

  // 尖冠桁
  線(p => p.M(X(1.6), Z(5.9)).L(X(3.4), Z(5.3)).L(X(5.2), Z(5.85)), 0.36);
  線(p => p.M(X(0.8), Z(6.3)).L(X(7.1), Z(6.2)), 0.42);

  // 三柱目梯疊
  const 梯 = (x0: number, x1: number, z0: number, 層: number, 步: number) => {
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z0 + 層 * 步)));
    線(p => p.M(X(x1), Z(z0 - 0.05)).L(X(x1), Z(z0 + 層 * 步 - 0.05)));
    for (let i = 0; i <= 層; i++)
      線(p => p.M(X(x0), Z(z0 + i * 步)).L(X(x1), Z(z0 + i * 步 - 0.05)));
  };
  梯(0.7, 2.6, 6.9, 5, 2.05);
  梯(5.2, 7.1, 6.9, 5, 2.05);
  // 中柱：生杙＋箱四
  線(p => p.M(X(3.9), Z(6.6)).L(X(3.85), Z(9.0)), 0.34);
  線(p => p.M(X(3.1), Z(7.5)).L(X(4.7), Z(7.45)), 0.34);
  梯(2.9, 4.9, 9.0, 4, 2.0);

  // 底匚
  線(p => p.M(X(2.9), Z(17.3)).L(X(2.9), Z(18.3)).L(X(4.9), Z(18.25))
    .L(X(4.9), Z(17.25)));
};
