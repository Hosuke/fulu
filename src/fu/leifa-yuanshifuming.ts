// ─────────────────────────────────────────────────────────────────────────────
// 元始符命（道法會元卷十二・雲篆雷符之屬） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3843_卷12雲篆雷符-03.png（「元始符命」題左，
// 符分「符」「命」二段，各繫小圈）
// 判準：符段——雙鉤首（左緊右壯）・小圈・左右雙蛇長波（右波幅至緣，摹形從簡）；
// 命段——人弧承彎鉤（左短點）・小圈・中蛇四折・右蛇三折・左蛇三折・中短尾。
// 次行「右符急速立應」依右符之序當指此符〔待核〕。書材卷面未記，不施硃層。
// 錨自 8 格裁圖（格2_fuming）：X=(c-5.5)×2.8、Z=(r-4.3)×2.8+2。幅比 3。
// 逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 元始符命: 符筆 = (bi) => {
  const u = bi.u, k = 2.8;
  const X = (c: number) => (c - 5.5) * k;
  const Z = (r: number) => (r - 4.3) * k + 2;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.7) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.3) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.32 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // ── 符段 ──
  // 左strand：緊雙鉤首
  抖(p => p.M(X(3.3), Z(4.55))
    .C(X(3.95), Z(4.6), X(4.15), Z(5.1), X(3.7), Z(5.4))
    .C(X(3.35), Z(5.6), X(3.5), Z(6.0), X(3.95), Z(6.15))
    .C(X(4.35), Z(6.3), X(4.5), Z(6.6), X(4.3), Z(6.8)), 0.72);
  // 左strand：長波四折（切返緊蟠）
  抖(p => p.M(X(4.55), Z(7.3))
    .C(X(3.4), Z(7.55), X(3.15), Z(8.35), X(4.15), Z(8.75))
    .C(X(5.15), Z(9.15), X(5.1), Z(9.95), X(3.95), Z(10.15))
    .C(X(2.95), Z(10.35), X(2.95), Z(11.2), X(3.95), Z(11.4))
    .C(X(4.9), Z(11.6), X(4.85), Z(12.45), X(4.0), Z(12.6))
    .C(X(3.2), Z(12.75), X(3.0), Z(13.7), X(2.8), Z(14.6)), 0.77);
  // 右strand：壯鉤首
  抖(p => p.M(X(6.05), Z(4.5))
    .C(X(6.75), Z(4.35), X(7.15), Z(4.7), X(7.0), Z(5.15))
    .C(X(6.9), Z(5.5), X(6.5), Z(5.65), X(6.35), Z(5.95)), 0.79);
  圈(6.05, 6.65);
  // 右strand：長波四折（波幅至緣）
  抖(p => p.M(X(6.8), Z(7.2))
    .C(X(5.7), Z(7.45), X(5.5), Z(8.25), X(6.5), Z(8.6))
    .C(X(7.7), Z(9.0), X(7.65), Z(9.75), X(6.4), Z(10.0))
    .C(X(5.5), Z(10.2), X(5.5), Z(11.0), X(6.5), Z(11.25))
    .C(X(7.65), Z(11.55), X(7.6), Z(12.3), X(6.5), Z(12.5))
    .C(X(5.85), Z(12.7), X(6.3), Z(13.6), X(6.85), Z(14.5)), 0.79);

  // ── 命段 ──
  // 左短點
  抖(p => p.M(X(3.1), Z(18.7))
    .C(X(3.45), Z(18.9), X(3.8), Z(18.95), X(4.05), Z(18.8)), 0.7);
  // 人弧
  抖(p => p.M(X(4.15), Z(18.8))
    .C(X(4.55), Z(18.15), X(5.7), Z(18.0), X(6.35), Z(18.45))
    .C(X(6.85), Z(18.8), X(7.2), Z(19.15), X(7.4), Z(19.5)), 0.77);
  // 彎鉤承中蛇四折（一筆連）
  抖(p => p.M(X(5.5), Z(18.75))
    .C(X(5.0), Z(19.3), X(4.6), Z(19.9), X(5.3), Z(20.25))
    .C(X(6.2), Z(20.65), X(6.25), Z(21.3), X(5.25), Z(21.6))
    .C(X(4.4), Z(21.85), X(4.45), Z(22.6), X(5.4), Z(22.9))
    .C(X(6.35), Z(23.2), X(6.35), Z(23.9), X(5.4), Z(24.15))
    .C(X(4.65), Z(24.35), X(4.7), Z(25.05), X(5.25), Z(25.45))
    .C(X(5.6), Z(25.85), X(5.45), Z(26.35), X(5.1), Z(26.6)), 0.79);
  圈(3.45, 21.7);
  // 右蛇三折
  抖(p => p.M(X(7.05), Z(20.3))
    .C(X(7.5), Z(20.85), X(7.55), Z(21.5), X(7.05), Z(22.0))
    .C(X(6.6), Z(22.45), X(6.65), Z(23.05), X(7.15), Z(23.5))
    .C(X(7.55), Z(23.9), X(7.45), Z(24.5), X(7.1), Z(25.3)), 0.72);
  // 左蛇三折
  抖(p => p.M(X(3.85), Z(24.75))
    .C(X(4.35), Z(25.25), X(4.35), Z(25.9), X(3.8), Z(26.35))
    .C(X(3.35), Z(26.7), X(3.4), Z(27.3), X(3.85), Z(27.7))
    .C(X(4.15), Z(28.0), X(4.1), Z(28.4), X(3.8), Z(28.65)), 0.72);
  // 中短尾
  抖(p => p.M(X(5.55), Z(26.2))
    .C(X(5.75), Z(26.8), X(5.6), Z(27.5), X(5.15), Z(27.95))
    .C(X(4.9), Z(28.2), X(4.95), Z(28.55), X(5.1), Z(28.75)), 0.7);
};
