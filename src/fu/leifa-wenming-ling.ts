// ─────────────────────────────────────────────────────────────────────────────
// 道法會元卷十二・雷霆聞命符（霊首） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3843_卷12雷霆聞命清微令符-07.png（「靈」題）
// 原文：「右符雷霆聞命，急如星火，焚於烈火香上」（發用記位）；「密用召將
// 心祝大應・玉明三會同符」記位不錄。組凡三道（雨冠首・霊首・霆首），此其二。
// 判準：題字不摹（題位錄譜）——符形：雙波冠→左S降曲・中綴點・右雙S曲→
// 底人足雙撇。承組硃層之制。幅比 1.1（z 0..26.4）。
// 錨自 12 格裁圖（格_wling）：X=(col-5.8)×2.2、Z=(row-0.8)×2.2+2。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 聞命霊首: 符筆 = (bi) => {
  const u = bi.u, k = 2.2;
  const X = (c: number) => (c - 5.8) * k;
  const Z = (r: number) => (r - 0.8) * k + 2;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.68) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  bi.硃(() => {
    // 雙波冠
    抖(p => p.M(X(2.6), Z(1.5))
      .C(X(3.4), Z(1.1), X(4.4), Z(1.0), X(5.4), Z(1.2))
      .C(X(6.6), Z(1.4), X(7.6), Z(1.3), X(8.6), Z(1.0)), 0.7);
    抖(p => p.M(X(2.9), Z(2.3))
      .C(X(3.8), Z(2.0), X(4.8), Z(2.0), X(5.7), Z(2.3))
      .C(X(6.8), Z(2.6), X(7.8), Z(2.5), X(8.8), Z(2.1)), 0.66);
    // 左S降曲（腰曲入足）
    抖(p => p.M(X(2.7), Z(3.0))
      .C(X(2.3), Z(3.9), X(2.5), Z(4.7), X(3.3), Z(5.1))
      .C(X(4.1), Z(5.5), X(4.3), Z(6.2), X(3.8), Z(6.8))
      .C(X(3.4), Z(7.3), X(3.2), Z(7.9), X(3.2), Z(8.5)), 0.68);
    // 中綴點
    bi.dot(X(5.3), Z(4.35), 0.55);
    // 中小曲（點下鉤桁）
    抖(p => p.M(X(4.4), Z(5.9))
      .C(X(5.2), Z(6.3), X(6.0), Z(6.3), X(6.6), Z(5.9)), 0.6);
    // 右雙S曲
    抖(p => p.M(X(7.6), Z(3.0))
      .C(X(8.4), Z(3.5), X(8.5), Z(4.2), X(7.8), Z(4.7))
      .C(X(7.1), Z(5.2), X(7.1), Z(5.9), X(7.8), Z(6.3))
      .C(X(8.4), Z(6.7), X(8.5), Z(7.3), X(8.0), Z(7.8)), 0.68);
    // 底人足雙撇
    抖(p => p.M(X(5.9), Z(7.2))
      .C(X(5.4), Z(8.0), X(4.9), Z(8.7), X(4.3), Z(9.3)), 0.66);
    抖(p => p.M(X(6.4), Z(7.3))
      .C(X(6.9), Z(8.0), X(7.4), Z(8.7), X(7.9), Z(9.4)), 0.66);
  });
};
