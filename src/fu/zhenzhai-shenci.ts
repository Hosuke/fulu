// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭神祠精魅鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p184_符葉四.jpg（上段右一）
// 判準：星紋符頭（桿・人字大鏈）＋雙柱密梯（左右各六七箱，左帶士杙・
// 中箱藏螺）＋底己曲。摹形從簡註。錨自 12 格裁圖（宅184_A shenci）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅神祠: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：桿＋人字大鏈
  線(p => p.M(X(0.9), Z(1.2)).L(X(6.7), Z(1.1))); 圈(0.6, 1.2); 圈(7.0, 1.1);
  圈(3.9, 2.2); 連([3.9, 2.2], [0.6, 3.4]); 連([3.9, 2.2], [7.0, 3.3]);
  圈(0.6, 3.4); 圈(7.0, 3.3);

  // 左柱密梯（六箱帶士杙）
  線(p => p.M(X(1.3), Z(4.3)).L(X(1.25), Z(5.3)), 0.34);
  線(p => p.M(X(0.8), Z(4.8)).L(X(1.8), Z(4.77)), 0.34);
  const 梯 = (x0: number, x1: number, z0: number, 層: number, 步: number) => {
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z0 + 層 * 步)));
    線(p => p.M(X(x1), Z(z0 - 0.05)).L(X(x1), Z(z0 + 層 * 步 - 0.05)));
    for (let i = 0; i <= 層; i++)
      線(p => p.M(X(x0), Z(z0 + i * 步)).L(X(x1), Z(z0 + i * 步 - 0.05)));
  };
  梯(0.7, 3.4, 5.6, 4, 1.7);
  // 中箱藏螺
  線(p => p.M(X(0.8), Z(13.0)).L(X(0.8), Z(15.5)).L(X(3.3), Z(15.45))
    .L(X(3.3), Z(12.95)).L(X(0.75), Z(13.0)));
  線(p => p.M(X(1.5), Z(13.5))
    .C(X(1.0), Z(14.1), X(1.3), Z(14.9), X(2.0), Z(14.9))
    .C(X(2.6), Z(14.9), X(2.8), Z(14.3), X(2.4), Z(14.0))
    .C(X(2.1), Z(13.8), X(1.9), Z(14.0), X(2.0), Z(14.2)), 0.36);
  梯(0.8, 3.4, 16.0, 2, 1.7);

  // 右柱密梯（七箱帶王杙）
  梯(3.9, 7.1, 4.2, 7, 2.13);
  線(p => p.M(X(5.4), Z(6.0)).L(X(5.37), Z(8.0)), 0.32);
  線(p => p.M(X(5.5), Z(12.5)).L(X(5.47), Z(14.5)), 0.32);

  // 底己曲
  線(p => p.M(X(4.6), Z(17.7))
    .C(X(4.3), Z(18.2), X(4.5), Z(18.6), X(5.1), Z(18.6))
    .C(X(5.6), Z(18.6), X(5.8), Z(18.2), X(5.5), Z(18.0)), 0.36);
};
