// 入山佩帶符・二 · 親筆
// 據圖版：參攷-local/入山符/抱朴子登涉_入山佩帶符_p046.png（四庫繕本，PD；
// 主坊自全卷 PDF 抽葉）。組文同符一（此三符…辟虎狼也）。材色原文未言，不入硃層。
// 判準：尸匡（頂桁左角垂長欄、右肩短欄曲垂）；匡內波桁四道遞降，左小欄；
// 中柱・右柱長垂（右柱綴三波桁），三足下地微撇。幅比 2.15（Z=z）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 佩帶符二: 符筆 = (bi) => {
  const u = bi.u;
  const X = (x: number) => x;
  const Z = (z: number) => z;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);

  // 尸匡：頂桁（左角下垂長欄至足）＋右肩曲垂
  線(p => p.M(X(-10.2), Z(2.6))
    .C(X(-5.0), Z(2.2), X(1.0), Z(2.2), X(6.8), Z(2.6)), 0.6);
  線(p => p.M(X(-10.2), Z(2.6))
    .L(X(-10.3), Z(36.0))
    .C(X(-10.4), Z(41.0), X(-10.8), Z(45.5), X(-11.4), Z(49.4)), 0.6);
  線(p => p.M(X(7.0), Z(2.0))
    .C(X(7.4), Z(5.6), X(7.6), Z(9.4), X(8.6), Z(13.6)), 0.55);

  // 匡內波桁四道（遞降）＋右內短豎＋左小欄
  線(p => p.M(X(-8.2), Z(5.4)).C(X(-4.0), Z(7.0), X(0.5), Z(7.0), X(4.4), Z(5.2)));
  線(p => p.M(X(-8.4), Z(9.0)).C(X(-4.0), Z(10.4), X(0.5), Z(10.2), X(5.6), Z(8.4)));
  線(p => p.M(X(4.0), Z(5.8)).C(X(3.7), Z(8.4), X(3.8), Z(10.8), X(4.4), Z(13.0)), 0.5);
  線(p => p.M(X(-7.8), Z(12.6)).C(X(-4.0), Z(13.8), X(0.0), Z(13.6), X(3.6), Z(12.2)));
  線(p => p.M(X(-8.4), Z(11.8)).C(X(-8.8), Z(13.6), X(-8.8), Z(15.6), X(-8.2), Z(17.4)), 0.5);
  線(p => p.M(X(-8.2), Z(16.4)).C(X(-5.0), Z(17.2), X(-1.6), Z(17.0), X(1.4), Z(15.8)));

  // 中柱（長垂至足，末微左撇）
  線(p => p.M(X(-2.8), Z(16.4))
    .L(X(-2.9), Z(38.0))
    .C(X(-2.95), Z(42.5), X(-3.3), Z(46.5), X(-4.0), Z(49.8)), 0.6);

  // 右柱（綴三波桁，長垂至足，末微右撇）
  線(p => p.M(X(-1.2), Z(19.4)).C(X(1.0), Z(19.8), X(3.4), Z(19.6), X(5.8), Z(18.4)));
  線(p => p.M(X(-1.8), Z(23.4)).C(X(1.0), Z(23.8), X(4.5), Z(23.4), X(8.6), Z(21.2)));
  線(p => p.M(X(-2.0), Z(31.8)).C(X(1.5), Z(33.0), X(5.5), Z(32.4), X(9.6), Z(30.2)));
  線(p => p.M(X(3.9), Z(19.2))
    .L(X(4.1), Z(35.0))
    .C(X(4.2), Z(40.0), X(4.6), Z(44.5), X(5.6), Z(48.6)), 0.6);
};
