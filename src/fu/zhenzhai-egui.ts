// 鎮宅靈符・厭惡鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p182_符葉二.jpg（上段右四）
// 判準：星紋符頭（桿二・圈杙柱四——各三圈以豎相貫）＋密桁帶（頂T杙）＋
// 山迴脚右軌。錨自 12 格裁圖（宅182_A egui）：X=(c-3.95)×2.7、
// Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅惡鬼: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.32 * k, Z(z)).L(X(x1) - 0.32 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：桿二＋圈杙柱四
  桿(0.6, 7.4, 1.0);
  桿(0.6, 7.4, 2.2);
  const 杙 = (x: number) => {
    for (const z of [3.0, 4.5, 6.0]) 圈(x, z);
    線(p => p.M(X(x), Z(3.4)).L(X(x), Z(4.1)));
    線(p => p.M(X(x), Z(4.9)).L(X(x), Z(5.6)));
  };
  杙(1.5); 杙(3.1); 杙(4.7); 杙(6.3);

  // 密桁帶（頂T杙＋桁七＋左軌）
  線(p => p.M(X(3.9), Z(7.0)).L(X(3.85), Z(8.2)));
  線(p => p.M(X(0.7), Z(7.4)).L(X(0.7), Z(13.3)));
  for (let i = 0; i < 7; i++)
    線(p => p.M(X(0.8), Z(7.5 + i * 0.83)).L(X(7.1), Z(7.4 + i * 0.83)));

  // 山迴脚（boustro三折）＋右軌
  線(p => p.M(X(7.2), Z(7.4)).L(X(7.2), Z(17.8)));
  線(p => p.M(X(0.9), Z(13.9)).L(X(4.9), Z(13.85))
    .C(X(5.4), Z(13.85), X(5.4), Z(14.75), X(4.9), Z(14.8))
    .L(X(1.7), Z(14.85)).C(X(1.2), Z(14.85), X(1.2), Z(15.75), X(1.7), Z(15.8))
    .L(X(4.8), Z(15.75)).C(X(5.3), Z(15.75), X(5.3), Z(16.6), X(4.8), Z(16.65))
    .L(X(1.6), Z(16.7)).C(X(1.1), Z(16.75), X(1.0), Z(17.3), X(1.05), Z(17.9)));
  線(p => p.M(X(1.05), Z(18.1)).C(X(1.0), Z(18.6), X(1.0), Z(19.0), X(1.05), Z(19.3)));
  線(p => p.M(X(7.2), Z(18.0)).C(X(7.25), Z(18.5), X(7.25), Z(18.9), X(7.2), Z(19.2)));
};
