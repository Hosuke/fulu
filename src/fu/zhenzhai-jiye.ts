// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭雞夜鳴百恠之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p181_符葉一.jpg（下段右三）
// 判準：星紋符頭（桿二・斜圈長鏈・散圈叢）＋頂尸桁＋左目梯・右言箱疊＋
// 底尸平箱・田箱承鬼尾小蟠＋張脚。錨自 12 格裁圖（宅181_B x3）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅雞夜: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.32 * k, Z(z)).L(X(x1) - 0.32 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：桿二＋斜圈長鏈＋散圈叢
  桿(0.5, 7.2, 1.0);
  桿(0.5, 7.1, 2.5);
  線(p => p.M(X(2.2), Z(3.58)).L(X(6.1), Z(4.42)));
  圈(1.9, 3.5); 圈(6.4, 4.5);
  for (const [x, z] of [[0.8, 3.5], [2.0, 4.7], [0.8, 5.9], [3.5, 5.2], [5.2, 5.6],
                        [6.9, 5.5], [2.5, 6.9], [4.2, 7.2], [6.4, 7.3]] as const) 圈(x, z);

  // 頂尸桁
  線(p => p.M(X(0.6), Z(8.2)).L(X(7.2), Z(8.0)), 0.42);
  線(p => p.M(X(0.6), Z(8.25)).L(X(0.6), Z(9.4)));

  // 左目梯（三箱）
  線(p => p.M(X(0.6), Z(8.7)).L(X(0.6), Z(13.6)));
  線(p => p.M(X(3.4), Z(8.65)).L(X(3.4), Z(13.55)));
  for (let i = 0; i <= 3; i++)
    線(p => p.M(X(0.6), Z(8.7 + i * 1.63)).L(X(3.4), Z(8.65 + i * 1.63)));

  // 右言箱疊（桁三承口，下承目箱）
  for (const z of [8.6, 9.5, 10.4]) 線(p => p.M(X(4.5), Z(z)).L(X(7.2), Z(z - 0.06)));
  線(p => p.M(X(5.0), Z(10.8)).L(X(5.0), Z(12.0)).L(X(6.7), Z(11.95))
    .L(X(6.7), Z(10.75)).L(X(4.95), Z(10.8)));
  線(p => p.M(X(4.6), Z(12.2)).L(X(4.6), Z(14.2)).L(X(7.0), Z(14.15))
    .L(X(7.0), Z(12.15)).L(X(4.55), Z(12.2)));
  線(p => p.M(X(4.7), Z(13.2)).L(X(6.9), Z(13.15)));

  // 底尸平箱（左）
  線(p => p.M(X(0.7), Z(14.0)).L(X(0.7), Z(16.0)).L(X(3.3), Z(15.95))
    .L(X(3.3), Z(13.95)).L(X(0.65), Z(14.0)));
  線(p => p.M(X(0.8), Z(15.0)).L(X(3.2), Z(14.95)));
  線(p => p.M(X(1.95), Z(14.05)).L(X(1.9), Z(15.9)));

  // 田箱承鬼尾小蟠（右）
  線(p => p.M(X(4.7), Z(14.4)).L(X(4.7), Z(16.3)).L(X(6.7), Z(16.25))
    .L(X(6.7), Z(14.35)).L(X(4.65), Z(14.4)));
  線(p => p.M(X(4.8), Z(15.35)).L(X(6.6), Z(15.3)));
  線(p => p.M(X(5.7), Z(14.45)).L(X(5.7), Z(16.2)));
  // 鬼尾小蟠（右下螺曲）
  線(p => p.M(X(6.2), Z(16.28))
    .C(X(6.6), Z(17.0), X(6.8), Z(17.6), X(6.4), Z(18.0))
    .C(X(6.0), Z(18.4), X(5.7), Z(18.1), X(5.9), Z(17.7))
    .C(X(6.0), Z(17.5), X(6.2), Z(17.5), X(6.3), Z(17.7)));

  // 張脚
  線(p => p.M(X(1.0), Z(16.5)).C(X(0.9), Z(17.3), X(0.9), Z(18.0), X(1.0), Z(18.5)));
  線(p => p.M(X(3.9), Z(16.5)).C(X(3.9), Z(17.2), X(3.9), Z(17.8), X(3.9), Z(18.3)));
};
