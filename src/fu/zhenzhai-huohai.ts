// 鎮宅靈符・厭除禍害之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p181_符葉一.jpg（上排右二）
// 判準：星紋符頭（二橫圈桿＋折線星山二疊＋自由圈列）＋方格疊書符身（三層梯帶）＋
// 曲腳（六垂波腳）。錨自 24 指格網讀出（宅_huohai.png），仿射入紙（k=1.41）。
// 幅比 2.2。逐筆釋讀〔待核〕；右側星折線原印偏淡，依殘跡結構補全注〔簡〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅禍害: 符筆 = (bi) => {
  const u = bi.u, k = 1.41, cx0 = -3.5, z00 = 6.0;
  const X = (x: number) => (x - cx0) * k;
  const Z = (z: number) => (z - z00) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.4) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.32 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.5 * k, Z(z)).L(X(x1) - 0.5 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：二橫圈桿
  桿(-9.7, 2.5, 6.6);
  桿(-9.7, 2.3, 8.3);

  // 星山二疊（左實右淡〔簡〕）＋自由圈列
  const 段 = (a: [number, number], b: [number, number]) => {
    // 兩端止於圈緣（r=0.4），勿入孔（Codex 審 2026-07-12）
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };
  const 折 = (a: [number, number], b: [number, number], c: [number, number]) => {
    段(a, b); 段(b, c);
    圈(a[0], a[1]); 圈(b[0], b[1]); 圈(c[0], c[1]);
  };
  折([-9.5, 11.9], [-7.2, 10.2], [-5.0, 12.3]);
  折([-2.6, 10.6], [-0.6, 9.8], [1.4, 11.3]);
  for (const [fx, fz] of [[-8.9, 15.3], [-6.4, 14.9], [-3.9, 15.2], [-1.5, 15.0],
                          [1.6, 15.1], [-0.5, 13.7]] as const) 圈(fx, fz, 0.4);

  // 符身三層梯帶
  // 上層：左右二匣各四桁（左匣左欄、右匣右欄）
  for (const z of [16.3, 17.4, 18.5, 19.4]) 線(p => p.M(X(-10.0), Z(z)).L(X(-3.3), Z(z)));
  線(p => p.M(X(-10.0), Z(16.2)).L(X(-10.0), Z(19.5)));
  for (const z of [16.2, 17.3, 18.4, 19.3]) 線(p => p.M(X(-2.6), Z(z)).L(X(3.3), Z(z)));
  線(p => p.M(X(3.3), Z(16.1)).L(X(3.3), Z(19.4)));

  // 中層：全幅帶匣三桁（雙欄）
  for (const z of [20.6, 21.7, 22.8]) 線(p => p.M(X(-9.9), Z(z)).L(X(3.2), Z(z)));
  線(p => p.M(X(-9.9), Z(20.5)).L(X(-9.9), Z(22.9)));
  線(p => p.M(X(3.2), Z(20.5)).L(X(3.2), Z(22.9)));

  // 下層：左右二匣各三桁＋中豎，下承全幅閉帶
  for (const z of [24.6, 25.7, 26.9]) 線(p => p.M(X(-9.8), Z(z)).L(X(-3.0), Z(z)));
  線(p => p.M(X(-9.8), Z(24.5)).L(X(-9.8), Z(27.0)));
  for (const z of [24.4, 25.5, 26.6]) 線(p => p.M(X(-1.6), Z(z)).L(X(3.2), Z(z)));
  線(p => p.M(X(3.2), Z(24.3)).L(X(3.2), Z(26.7)));
  線(p => p.M(X(-2.3), Z(24.3)).L(X(-2.3), Z(27.3)));
  線(p => p.M(X(-10.0), Z(28.3)).L(X(3.3), Z(28.3)));
  線(p => p.M(X(-10.0), Z(29.5)).L(X(3.3), Z(29.5)));
  線(p => p.M(X(-10.0), Z(28.2)).L(X(-10.0), Z(29.6)));
  線(p => p.M(X(3.3), Z(28.2)).L(X(3.3), Z(29.6)));

  // 符腳：六垂波腳（S 波相間，收出）
  const 腳x = [-8.9, -6.9, -4.9, -2.7, -0.6, 1.7];
  腳x.forEach((x, i) => {
    const 相 = i % 2 === 0 ? 1 : -1;
    bi.一筆({ 寬: 0.5 * u, 起: '藏', 收: '出' }, p => p
      .M(X(x), Z(29.9))
      .C(X(x - 0.7 * 相), Z(32.0), X(x + 0.7 * 相), Z(34.0), X(x - 0.2 * 相), Z(36.0))
      .C(X(x - 0.8 * 相), Z(37.4), X(x - 1.1), Z(38.6), X(x - 1.7), Z(39.6)));
  });
};
