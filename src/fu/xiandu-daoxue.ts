// ─────────────────────────────────────────────────────────────────────────────
// 玉清大梵禱雪玉符（紫微仙都九符・六） · 親筆
// 據圖版：參攷-local/紫微諱/樣頁/樣_94_雷霆玉經紫微仙都九符禱雪借潮符-p53.png
// （左圖；題「玉清大梵禱雪玉符」在其右）
// 判準：密摺蟠曲雲篆（摺＝橫程）——頭三疊帶（中簇綴波豎）＋左厂臂摺帶二重
// ＋右滿幅帶（波桁・垂莖）＋滿幅帶（厂臂）＋波帶雙峰＋滿幅三摺＋左右對疊帶
// 二重＋皿箱區（頂桁中杙・大皿框點陣四組三點・中豎雙底桁・隅飄左足）。
// 摺帶連綿摹形從簡。錨自 10 格裁圖（格_daoxue）：X=(c-4.95)×2.3、
// Z=(r-0.05)×2.3+0.9。幅比 2.4。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 禱雪玉符: 符筆 = (bi) => {
  const u = bi.u, k = 2.3;
  const X = (c: number) => (c - 4.95) * k;
  const Z = (r: number) => (r - 0.05) * k + 0.9;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 摺 = (x0: number, x1: number, rT: number, rB: number, n: number, 左起 = true, 寬 = 0.4) =>
    抖(pa => {
      const p = n > 1 ? (rB - rT) / (n - 1) : 0;
      let dir = 左起 ? 1 : -1, y = rT;
      pa.M(X(dir > 0 ? x0 : x1), Z(y));
      for (let i = 0; i < n; i++) {
        const xe = dir > 0 ? x1 : x0;
        if (i < n - 1) {
          pa.L(X(xe - dir * p * 0.35), Z(y));
          pa.C(X(xe + dir * p * 1.05), Z(y - p * 0.05), X(xe + dir * p * 1.05), Z(y + p * 1.05), X(xe - dir * p * 0.35), Z(y + p));
          y += p; dir = -dir;
        } else pa.L(X(xe), Z(y));
      }
    }, 寬);
  const 點 = (x: number, z: number) => {
    const c = bi.ctx; c.save();
    bi.塊(pth => pth.M(X(x - 0.13), Z(z)).C(X(x - 0.13), Z(z - 0.17), X(x + 0.13), Z(z - 0.17), X(x + 0.13), Z(z))
      .C(X(x + 0.13), Z(z + 0.17), X(x - 0.13), Z(z + 0.17), X(x - 0.13), Z(z)).close());
    c.restore();
  };

  // 頭三疊帶（中簇綴波豎）
  摺(0.4, 3.2, 0.15, 2.9, 6);
  摺(3.9, 6.0, 0.2, 3.4, 7);
  抖(p => p.M(X(6.25), Z(0.8)).C(X(6.5), Z(1.15), X(6.3), Z(1.5), X(6.45), Z(1.8)), 0.36);
  摺(6.7, 9.5, 0.2, 2.9, 6);

  // 左厂臂摺帶二重
  抖(p => p.M(X(0.6), Z(3.3)).C(X(0.32), Z(3.9), X(0.3), Z(4.9), X(0.55), Z(5.5)), 0.4);
  摺(0.65, 3.1, 3.4, 5.4, 4);
  摺(0.4, 3.2, 5.7, 8.1, 5);

  // 右滿幅帶（波桁承垂莖）
  摺(3.4, 9.6, 2.95, 4.9, 4);
  線(p => p.M(X(5.6), Z(5.4)).L(X(5.58), Z(6.5)), 0.38);
  摺(3.4, 6.0, 5.6, 7.9, 5);
  摺(6.15, 9.5, 5.8, 7.8, 4);

  // 滿幅帶（左厂臂）
  抖(p => p.M(X(0.65), Z(8.4)).C(X(0.38), Z(8.9), X(0.36), Z(9.5), X(0.6), Z(10.0)), 0.4);
  摺(0.7, 9.6, 8.4, 9.9, 4);

  // 波帶雙峰
  抖(p => p.M(X(0.4), Z(10.3))
    .L(X(4.6), Z(10.25))
    .C(X(5.0), Z(9.95), X(5.4), Z(9.95), X(5.7), Z(10.25))
    .C(X(6.0), Z(10.5), X(6.4), Z(10.5), X(6.7), Z(10.2))
    .C(X(7.0), Z(9.95), X(7.4), Z(9.95), X(7.7), Z(10.25))
    .L(X(9.6), Z(10.2)), 0.4);
  摺(0.4, 9.6, 10.9, 11.0, 1);

  // 滿幅三摺
  摺(0.4, 9.6, 11.5, 13.0, 3);

  // 左右對疊帶二重
  摺(0.6, 3.2, 13.4, 15.8, 5);
  抖(p => p.M(X(9.6), Z(12.8)).C(X(9.7), Z(13.2), X(9.65), Z(13.6), X(9.5), Z(13.9)), 0.38);
  摺(5.6, 9.5, 13.3, 15.9, 5);
  摺(0.5, 3.3, 16.1, 18.4, 5);
  摺(5.7, 9.4, 16.0, 18.3, 4);
  抖(p => p.M(X(6.6), Z(17.75)).C(X(7.1), Z(17.55), X(7.5), Z(17.95), X(8.0), Z(17.8)), 0.36);

  // 皿箱區：頂桁中杙
  線(p => p.M(X(0.6), Z(18.8)).L(X(9.3), Z(18.72)), 0.44);
  線(p => p.M(X(4.95), Z(18.85)).L(X(4.93), Z(19.55)), 0.4);
  // 大皿框（隅飄）
  線(p => p.M(X(0.95), Z(19.6)).L(X(9.45), Z(19.52)), 0.42);
  抖(p => p.M(X(1.05), Z(19.62)).L(X(0.55), Z(19.25)), 0.34);
  抖(p => p.M(X(9.35), Z(19.55)).L(X(9.75), Z(19.2)), 0.34);
  線(p => p.M(X(1.05), Z(19.62)).L(X(1.03), Z(23.15)), 0.4);
  線(p => p.M(X(9.4), Z(19.55)).L(X(9.38), Z(23.1)), 0.4);
  // 左外臂
  抖(p => p.M(X(0.5), Z(19.7)).C(X(0.3), Z(20.6), X(0.3), Z(21.8), X(0.55), Z(22.6))
    .C(X(0.65), Z(23.1), X(0.9), Z(23.5), X(1.2), Z(23.75)), 0.4);
  // 點陣四組（各三點）＋中豎
  for (const cx of [2.3, 3.7, 6.2, 7.6]) { 點(cx, 20.3); 點(cx, 20.85); 點(cx, 21.4); }
  線(p => p.M(X(5.0), Z(19.65)).L(X(4.98), Z(22.45)), 0.4);
  // 雙底桁＋底邊
  線(p => p.M(X(1.15), Z(22.55)).L(X(9.3), Z(22.48)), 0.38);
  線(p => p.M(X(1.15), Z(23.05)).L(X(9.3), Z(22.98)), 0.38);
  線(p => p.M(X(1.1), Z(23.6)).L(X(9.25), Z(23.55)), 0.4);
  抖(p => p.M(X(9.35), Z(23.1)).L(X(9.7), Z(23.5)), 0.34);
};
