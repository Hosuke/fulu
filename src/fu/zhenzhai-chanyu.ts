// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭產育男女胎衣血腥不淨鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p185_符葉五.jpg（下段右四）
// 判準：星紋符頭（桿三・圈行・了了八皃曲行）＋左密目梯七箱・右柱𠃊出鬼
// 形疊承惢曲（摹形從簡）＋底桁。錨自 12 格裁圖（宅185_B chanyu）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅產育: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿三＋圈行
  線(p => p.M(X(0.9), Z(1.2)).L(X(6.5), Z(1.1))); 圈(0.6, 1.2); 圈(6.8, 1.1);
  線(p => p.M(X(0.9), Z(2.2)).L(X(6.5), Z(2.1))); 圈(0.6, 2.2); 圈(6.8, 2.1);
  線(p => p.M(X(0.9), Z(3.2)).L(X(6.5), Z(3.1))); 圈(0.6, 3.2); 圈(6.8, 3.1);
  for (const [x, z] of [[0.7, 4.3], [2.2, 4.4], [3.7, 4.3], [6.7, 4.2]] as const) 圈(x, z);

  // 了了八皃曲行（摹形）
  出(p => p.M(X(1.4), Z(5.2)).C(X(1.7), Z(5.5), X(1.7), Z(6.0), X(1.4), Z(6.4)), 0.34);
  出(p => p.M(X(2.3), Z(5.15)).C(X(2.6), Z(5.5), X(2.6), Z(5.95), X(2.3), Z(6.35)), 0.34);
  出(p => p.M(X(3.3), Z(5.3)).C(X(3.1), Z(5.8), X(2.9), Z(6.2), X(2.7), Z(6.5)), 0.32);
  出(p => p.M(X(3.5), Z(5.3)).C(X(3.7), Z(5.8), X(3.9), Z(6.2), X(4.1), Z(6.5)), 0.32);
  線(p => p.M(X(4.6), Z(5.4))
    .C(X(4.4), Z(6.0), X(4.6), Z(6.5), X(5.2), Z(6.55))
    .C(X(5.7), Z(6.6), X(5.9), Z(6.2), X(5.6), Z(5.95)), 0.34);

  // 左密目梯七箱
  線(p => p.M(X(0.8), Z(7.4)).L(X(0.8), Z(18.2)));
  線(p => p.M(X(3.2), Z(7.35)).L(X(3.2), Z(18.15)));
  for (let i = 0; i <= 7; i++)
    線(p => p.M(X(0.8), Z(7.4 + i * 1.543)).L(X(3.2), Z(7.35 + i * 1.543)));

  // 右柱：𠃊出鬼形疊承惢曲（摹形從簡）
  線(p => p.M(X(4.2), Z(7.6)).L(X(4.2), Z(9.0)).L(X(5.9), Z(8.95)), 0.36);
  線(p => p.M(X(5.0), Z(7.4)).L(X(4.98), Z(8.95)), 0.34);
  線(p => p.M(X(4.3), Z(9.7)).L(X(6.0), Z(9.65)), 0.34);
  線(p => p.M(X(5.15), Z(9.4)).L(X(5.13), Z(10.6)), 0.34);
  // 鬼形
  線(p => p.M(X(4.4), Z(11.2)).L(X(4.4), Z(12.5)).L(X(6.0), Z(12.45))
    .L(X(6.0), Z(11.15)).L(X(4.35), Z(11.2)), 0.32);
  線(p => p.M(X(5.2), Z(11.25)).L(X(5.18), Z(12.45)), 0.3);
  出(p => p.M(X(4.6), Z(12.7)).C(X(4.4), Z(13.3), X(4.5), Z(13.8), X(4.9), Z(14.1)), 0.34);
  出(p => p.M(X(5.7), Z(12.7)).C(X(5.9), Z(13.3), X(5.8), Z(13.8), X(5.4), Z(14.15)), 0.34);
  // 惢曲（三小圈曲簇）
  const 惢 = (cx: number, cz: number) => 線(p => p.M(X(cx + 0.35), Z(cz))
    .C(X(cx - 0.25), Z(cz + 0.25), X(cx - 0.25), Z(cz + 0.7), X(cx + 0.15), Z(cz + 0.8))
    .C(X(cx + 0.5), Z(cz + 0.9), X(cx + 0.6), Z(cz + 0.45), X(cx + 0.25), Z(cz + 0.35)), 0.3);
  惢(4.7, 14.8); 惢(5.6, 14.9); 惢(5.1, 15.9);
  出(p => p.M(X(5.1), Z(16.8)).C(X(5.0), Z(17.4), X(5.1), Z(17.9), X(5.3), Z(18.3)), 0.34);

  // 底桁
  線(p => p.M(X(0.9), Z(18.8)).L(X(3.2), Z(18.75)), 0.4);
};
