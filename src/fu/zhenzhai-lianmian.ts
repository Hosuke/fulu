// 鎮宅靈符・厭除疾病連綿之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p186_符葉六_跋末神降忌食.jpg（上段右一）
// 判準：星紋符頭（桿三・星叢放射八芒・右散圈三）＋大厂框（頂桁＋左長垂）內
// 三箱遞縮（日十・日・口）＋底短桁。錨自 12 格裁圖（宅186_A lianmian）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅連綿: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：桿三
  線(p => p.M(X(1.0), Z(1.3)).L(X(6.4), Z(1.2))); 圈(0.7, 1.3); 圈(6.7, 1.2);
  線(p => p.M(X(1.0), Z(2.3)).L(X(6.4), Z(2.2))); 圈(0.7, 2.3); 圈(6.7, 2.2);
  線(p => p.M(X(1.0), Z(3.3)).L(X(6.4), Z(3.2))); 圈(0.7, 3.3); 圈(6.7, 3.2);

  // 星叢放射八芒（心圈＋八圈芒）
  圈(3.3, 6.6);
  const 芒: [number, number][] = [
    [3.3, 4.7], [4.7, 5.2], [5.4, 6.5], [4.6, 7.9],
    [3.3, 8.4], [1.9, 7.9], [1.2, 6.6], [2.0, 5.2],
  ];
  for (const m of 芒) { 連([3.3, 6.6], m); 圈(m[0], m[1]); }
  // 右散圈三
  圈(6.7, 5.6); 圈(6.8, 7.2); 圈(6.9, 8.8);

  // 大厂框：頂桁＋左長垂（末鉤右）
  線(p => p.M(X(0.7), Z(9.4)).L(X(6.9), Z(9.3)), 0.44);
  線(p => p.M(X(0.75), Z(9.45)).L(X(0.7), Z(18.7))
    .C(X(0.7), Z(19.1), X(1.0), Z(19.2), X(1.5), Z(19.15)), 0.42);

  // 帶一：田日對＋中杙
  線(p => p.M(X(1.3), Z(10.0)).L(X(1.3), Z(11.7)).L(X(3.4), Z(11.65))
    .L(X(3.4), Z(9.95)).L(X(1.25), Z(10.0)));
  線(p => p.M(X(1.4), Z(10.85)).L(X(3.3), Z(10.82)), 0.32);
  線(p => p.M(X(2.35), Z(10.05)).L(X(2.33), Z(11.65)), 0.32);
  線(p => p.M(X(3.95), Z(10.0)).L(X(3.95), Z(11.7)).L(X(6.2), Z(11.65))
    .L(X(6.2), Z(9.95)).L(X(3.9), Z(10.0)));
  線(p => p.M(X(4.05), Z(10.85)).L(X(6.1), Z(10.82)), 0.32);
  線(p => p.M(X(3.68), Z(10.1)).L(X(3.66), Z(11.6)), 0.3);
  // 帶二：寬日（上半雙豎）
  線(p => p.M(X(1.3), Z(12.2)).L(X(1.3), Z(13.9)).L(X(6.2), Z(13.85))
    .L(X(6.2), Z(12.15)).L(X(1.25), Z(12.2)));
  線(p => p.M(X(1.4), Z(13.05)).L(X(6.1), Z(13.02)), 0.32);
  線(p => p.M(X(2.7), Z(12.3)).L(X(2.68), Z(13.0)), 0.3);
  線(p => p.M(X(4.4), Z(12.25)).L(X(4.38), Z(12.95)), 0.3);
  // 帶三：日日對
  線(p => p.M(X(1.2), Z(14.4)).L(X(1.2), Z(16.1)).L(X(3.4), Z(16.05))
    .L(X(3.4), Z(14.35)).L(X(1.15), Z(14.4)));
  線(p => p.M(X(1.3), Z(15.25)).L(X(3.3), Z(15.22)), 0.32);
  線(p => p.M(X(3.95), Z(14.4)).L(X(3.95), Z(16.1)).L(X(6.1), Z(16.05))
    .L(X(6.1), Z(14.35)).L(X(3.9), Z(14.4)));
  線(p => p.M(X(4.05), Z(15.25)).L(X(6.0), Z(15.22)), 0.32);
  // 帶四：日居中
  線(p => p.M(X(1.7), Z(16.6)).L(X(1.7), Z(18.3)).L(X(5.5), Z(18.25))
    .L(X(5.5), Z(16.55)).L(X(1.65), Z(16.6)));
  線(p => p.M(X(1.8), Z(17.45)).L(X(5.4), Z(17.42)), 0.32);

  // 底短桁
  線(p => p.M(X(1.5), Z(18.9)).L(X(5.5), Z(18.85)), 0.4);
};
