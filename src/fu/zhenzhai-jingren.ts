// 鎮宅靈符・厭驚恐人之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p186_符葉六_跋末神降忌食.jpg（上段右四）
// 判準：圈桿符頭（桿二・散圈二列・左斜連圈對）＋左柱（亖桁承目箱四疊）・
// 右柱（巳曲・舛小形・目箱・水曲・功形——摹形從簡）。錨自 12 格裁圖
// （宅186_A jingren）：X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。
// 逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅驚人: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：桿二
  線(p => p.M(X(1.0), Z(1.2)).L(X(6.3), Z(1.1))); 圈(0.7, 1.2); 圈(6.6, 1.1);
  線(p => p.M(X(1.5), Z(2.4)).L(X(5.4), Z(2.3))); 圈(1.2, 2.4); 圈(5.7, 2.3);
  // 散圈二列
  圈(1.2, 3.7); 圈(2.4, 3.8); 圈(3.6, 3.7); 圈(4.8, 3.8); 圈(6.5, 2.5);
  線(p => p.M(X(5.0), Z(5.2)).L(X(6.2), Z(5.15))); 圈(4.7, 5.2); 圈(6.5, 5.15);
  // 左斜連圈對
  圈(1.0, 5.1); 連([1.0, 5.1], [2.3, 5.7]); 圈(2.3, 5.7);

  // 左柱：亖桁三承目箱四疊
  線(p => p.M(X(0.7), Z(6.5)).L(X(3.4), Z(6.45)), 0.4);
  線(p => p.M(X(0.7), Z(7.2)).L(X(3.4), Z(7.15)), 0.4);
  線(p => p.M(X(0.7), Z(7.9)).L(X(3.4), Z(7.85)), 0.4);
  const 目 = (x0: number, x1: number, z0: number, z1: number) => {
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z1)).L(X(x1), Z(z1 - 0.05))
      .L(X(x1), Z(z0 - 0.05)).L(X(x0 - 0.05), Z(z0)));
    線(p => p.M(X(x0 + 0.1), Z((z0 + z1) / 2)).L(X(x1 - 0.1), Z((z0 + z1) / 2 - 0.03)), 0.32);
  };
  目(0.8, 3.4, 8.5, 10.7); 目(0.8, 3.4, 11.1, 13.3);
  目(0.8, 3.4, 13.7, 15.9); 目(0.8, 3.4, 16.3, 18.5);
  出(p => p.M(X(1.2), Z(18.5)).C(X(1.1), Z(18.8), X(1.0), Z(19.1), X(0.85), Z(19.3)), 0.34);
  出(p => p.M(X(3.0), Z(18.45)).C(X(3.1), Z(18.75), X(3.2), Z(19.05), X(3.35), Z(19.25)), 0.34);

  // 右柱：橫承長乙曲
  線(p => p.M(X(4.4), Z(6.6)).L(X(6.3), Z(6.55)), 0.36);
  出(p => p.M(X(5.9), Z(6.75))
    .C(X(4.9), Z(7.3), X(4.5), Z(7.9), X(5.2), Z(8.3))
    .C(X(5.9), Z(8.7), X(5.7), Z(9.3), X(4.8), Z(9.5)), 0.38);
  // 舛小形（摹形：夕鉤＋㐄鉤）
  出(p => p.M(X(4.7), Z(10.2)).C(X(4.5), Z(10.8), X(4.6), Z(11.3), X(5.0), Z(11.5)), 0.34);
  線(p => p.M(X(4.6), Z(10.7)).L(X(5.3), Z(10.65)), 0.3);
  線(p => p.M(X(5.7), Z(10.1)).L(X(6.4), Z(10.05)), 0.3);
  出(p => p.M(X(6.0), Z(10.0)).C(X(6.0), Z(10.6), X(6.1), Z(11.2), X(6.3), Z(11.6)), 0.34);
  // 目箱
  目(4.6, 6.7, 12.4, 15.2);
  // 水曲（豎鉤＋左右挑）
  出(p => p.M(X(5.6), Z(15.5)).C(X(5.6), Z(16.2), X(5.6), Z(16.9), X(5.3), Z(17.3)), 0.36);
  出(p => p.M(X(5.2), Z(15.9)).L(X(4.6), Z(16.6)), 0.32);
  出(p => p.M(X(5.9), Z(15.9)).L(X(6.5), Z(16.6)), 0.32);
  // 功形（工桁＋力鉤——摹形）
  線(p => p.M(X(4.4), Z(17.8)).L(X(5.3), Z(17.75)), 0.32);
  線(p => p.M(X(4.85), Z(17.8)).L(X(4.83), Z(18.9)), 0.32);
  線(p => p.M(X(4.4), Z(18.95)).L(X(5.3), Z(18.9)), 0.32);
  線(p => p.M(X(5.6), Z(17.9)).L(X(6.5), Z(17.85)), 0.32);
  出(p => p.M(X(6.2), Z(17.9)).C(X(6.2), Z(18.5), X(6.0), Z(18.9), X(5.6), Z(19.1)), 0.34);
};
