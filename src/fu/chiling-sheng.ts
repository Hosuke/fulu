// 判準：原刻拆畫圖——聖字逐筆繫：乾・元・亨・利・貞（邪不干正）・六甲・六丁・
// 天丁・力士・五雷・使者・風伯・雨師（尾誦「執符而行急急如律令」記位 p28）；
// 本繪依畫序親書楷體成字（耳口上、王下，同敕字式之裁）。幅比 1.2（z 0..28.8）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 聖字式: 符筆 = (bi) => {
  const u = bi.u;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.95) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.05) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // ── 耳部（左上）──
  橫(p => p.M(-9.0, 4.8).L(-1.8, 4.6));
  橫(p => p.M(-7.6, 4.9).L(-7.7, 12.2), 0.85);
  橫(p => p.M(-3.6, 4.8).L(-3.65, 12.0), 0.85);
  橫(p => p.M(-7.5, 7.4).L(-3.7, 7.3), 0.75);
  橫(p => p.M(-7.5, 9.8).L(-3.7, 9.7), 0.75);
  橫(p => p.M(-9.6, 12.4).L(-0.8, 12.1));

  // ── 口部（右上）──
  橫(p => p.M(1.8, 6.2).L(1.75, 11.9), 0.85);
  橫(p => p.M(1.8, 6.3).L(7.6, 6.0).L(7.7, 11.7), 0.85);
  橫(p => p.M(1.8, 11.9).L(7.7, 11.7), 0.85);

  // ── 王部（下）──
  橫(p => p.M(-5.8, 15.6).L(5.6, 15.3));
  橫(p => p.M(-0.1, 15.5).L(-0.15, 23.7), 1.0);
  橫(p => p.M(-5.2, 19.5).L(5.0, 19.2), 0.9);
  出(p => p.M(-7.4, 24.3).C(-3.0, 23.9, 3.0, 23.8, 7.3, 24.1), 1.15);
};
