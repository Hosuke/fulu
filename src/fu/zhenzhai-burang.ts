// 鎮宅靈符・厭不禳牛畜生產鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p186_符葉六_跋末神降忌食.jpg（下段右一）
// 判準：圈桿符頭（桿一・右緣圈列）＋亖桁帶嵌卵形＋密箱長陣（滿幅目箱・左口
// 右尢曲・左㔾㔾右目箱・口口對・中目小對・底皿箱——摹形從簡，符最密）。
// 錨自 12 格裁圖（宅186_B burang）：X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。
// 幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅不禳: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };

  // 符頭：桿二
  線(p => p.M(X(0.8), Z(0.95)).L(X(6.3), Z(0.85))); 圈(0.5, 0.96); 圈(6.6, 0.85);
  線(p => p.M(X(0.8), Z(1.8)).L(X(6.3), Z(1.7))); 圈(0.5, 1.81);
  // 右緣圈列
  圈(6.7, 1.6); 圈(6.8, 2.4); 圈(6.9, 3.2); 圈(6.8, 4.0);

  // 目卵箱（寬目箱中嵌卵形）
  const 目 = (x0: number, x1: number, z0: number, z1: number) => {
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z1)).L(X(x1), Z(z1 - 0.05))
      .L(X(x1), Z(z0 - 0.05)).L(X(x0 - 0.05), Z(z0)));
    線(p => p.M(X(x0 + 0.1), Z((z0 + z1) / 2)).L(X(x1 - 0.1), Z((z0 + z1) / 2 - 0.03)), 0.32);
  };
  const 卵 = (cx: number, z0: number, z1: number) => 線(p => p.M(X(cx), Z(z0))
    .C(X(cx - 0.32), Z(z0 + (z1 - z0) * 0.28), X(cx - 0.32), Z(z1 - (z1 - z0) * 0.28), X(cx), Z(z1))
    .C(X(cx + 0.32), Z(z1 - (z1 - z0) * 0.28), X(cx + 0.32), Z(z0 + (z1 - z0) * 0.28), X(cx), Z(z0)), 0.32);
  目(0.7, 6.4, 2.3, 4.5);
  卵(3.4, 2.7, 4.1);

  // 滿幅目箱（中綴短杙）
  目(0.7, 6.3, 5.0, 7.3);
  線(p => p.M(X(3.5), Z(5.1)).L(X(3.48), Z(6.1)), 0.3);

  // 左目箱・右兀曲
  目(0.7, 4.3, 7.8, 10.1);
  線(p => p.M(X(4.8), Z(8.0)).L(X(6.5), Z(7.95)), 0.36);
  出(p => p.M(X(5.15), Z(8.05)).L(X(5.1), Z(10.0)), 0.36);
  出(p => p.M(X(5.9), Z(8.0))
    .C(X(5.95), Z(8.8), X(6.0), Z(9.5), X(6.5), Z(9.8)), 0.36);

  // 左𠕒箱（內豎二）・右目箱
  線(p => p.M(X(0.7), Z(10.6)).L(X(0.7), Z(12.9)).L(X(3.1), Z(12.85))
    .L(X(3.1), Z(10.55)).L(X(0.65), Z(10.6)), 0.34);
  線(p => p.M(X(1.5), Z(10.65)).L(X(1.48), Z(12.8)), 0.3);
  線(p => p.M(X(2.3), Z(10.6)).L(X(2.28), Z(12.75)), 0.3);
  目(3.6, 6.4, 10.6, 12.9);

  // 目目對嵌卵（上層）
  目(0.7, 3.0, 13.4, 15.7); 目(3.6, 6.4, 13.4, 15.7);
  卵(3.3, 13.8, 15.2);

  // 目目對嵌卵（下層）
  目(0.7, 3.0, 16.2, 18.9); 目(3.6, 6.4, 16.2, 18.9);
  卵(3.3, 16.7, 18.2);
};
