// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭除官刑獄鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p184_符葉四.jpg（中段右一）
// 判準：星紋符頭（桿・圈杙柱二・V字圈樹・右圈柱）＋寬目匣＋目田層＋
// 底寬目中杙。錨自 12 格裁圖（宅184_A guanxing）：X=(c-3.95)×2.7、
// Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅官刑: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：桿＋圈杙柱二＋V字圈樹＋右圈柱
  線(p => p.M(X(1.0), Z(1.0)).L(X(6.7), Z(0.95))); 圈(0.7, 1.0); 圈(7.0, 0.95);
  圈(1.5, 2.3); 連([1.5, 2.3], [1.5, 4.0]); 圈(1.5, 4.0);
  圈(3.3, 2.2); 圈(4.6, 2.15); 圈(3.95, 3.1);
  連([3.3, 2.2], [3.95, 3.1]); 連([4.6, 2.15], [3.95, 3.1]);
  連([3.95, 3.1], [3.95, 4.2]); 圈(3.95, 4.2);
  圈(5.8, 2.2); 連([5.8, 2.2], [5.8, 4.0]); 圈(5.8, 4.0);
  圈(7.1, 2.1); 圈(7.15, 3.3); 圈(7.2, 4.5);

  // 寬目匣
  線(p => p.M(X(0.9), Z(5.0)).L(X(0.9), Z(8.0)).L(X(7.2), Z(7.95))
    .L(X(7.2), Z(4.95)).L(X(0.85), Z(5.0)));
  線(p => p.M(X(1.0), Z(6.5)).L(X(7.1), Z(6.45)));
  線(p => p.M(X(4.05), Z(5.05)).L(X(4.0), Z(7.95)), 0.34);

  // 目田層
  const 箱 = (x0: number, x1: number, z0: number, z1: number) => {
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z1)).L(X(x1), Z(z1 - 0.05))
      .L(X(x1), Z(z0 - 0.05)).L(X(x0 - 0.05), Z(z0)));
    線(p => p.M(X(x0 + 0.1), Z((z0 + z1) / 2)).L(X(x1 - 0.1), Z((z0 + z1) / 2 - 0.04)), 0.32);
  };
  箱(1.0, 3.4, 8.5, 11.0);
  箱(4.2, 7.1, 8.5, 11.0);
  箱(1.0, 3.4, 11.5, 13.0);
  箱(4.2, 7.1, 11.5, 13.0);

  // 底寬目中杙
  線(p => p.M(X(1.0), Z(13.6)).L(X(1.0), Z(18.8)));
  線(p => p.M(X(7.1), Z(13.55)).L(X(7.1), Z(18.75)));
  for (const z of [13.6, 15.3, 17.0, 18.8]) 線(p => p.M(X(1.0), Z(z)).L(X(7.1), Z(z - 0.05)));
  線(p => p.M(X(4.05), Z(13.65)).L(X(4.0), Z(18.75)), 0.34);
};
