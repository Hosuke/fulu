// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・龍神非佛 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p183_符葉三.jpg（中段右三）
// 判準：星紋符頭（波桿人字鏈・左圈柱密六・散圈）＋大匣右軌（頂反匣乂文・
// 雙目梯四箱・中杙）＋底小曲。主治句考據卷原作〔句待核〕——原葉題
// 「龍神非佛」今親見（升定，記考據卷）。錨自 12 格裁圖（宅183_A longshen）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅龍神: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：波桿＋人字鏈＋左圈柱＋散圈
  線(p => p.M(X(1.3), Z(1.3)).C(X(2.6), Z(1.1), X(4.0), Z(1.1), X(5.2), Z(1.25))
    .C(X(5.9), Z(1.3), X(6.5), Z(1.3), X(6.9), Z(1.25)));
  圈(1.0, 1.35); 圈(7.2, 1.2);
  圈(2.4, 1.9); 連([2.4, 1.9], [4.6, 2.1]); 圈(4.6, 2.1); 連([4.6, 2.1], [6.7, 1.8]); 圈(6.7, 1.8);
  for (const z of [1.9, 2.7, 3.5, 4.3, 5.1]) 圈(0.6, z);
  for (const [x, z] of [[2.2, 3.2], [3.4, 3.4], [4.8, 3.3], [2.4, 4.6], [3.6, 4.8],
                        [5.0, 4.7], [6.4, 4.5]] as const) 圈(x, z);

  // 大匣右軌：頂反匣乂文
  線(p => p.M(X(7.3), Z(5.8)).L(X(7.3), Z(17.8)));
  線(p => p.M(X(0.9), Z(6.0)).L(X(7.1), Z(5.9)), 0.42);
  線(p => p.M(X(1.0), Z(6.1)).L(X(1.0), Z(9.0)).L(X(4.0), Z(8.95))
    .L(X(4.0), Z(6.05)));
  線(p => p.M(X(1.4), Z(6.7)).C(X(2.2), Z(7.5), X(3.0), Z(8.1), X(3.7), Z(8.5)), 0.34);
  線(p => p.M(X(3.6), Z(6.8)).C(X(2.9), Z(7.5), X(2.1), Z(8.2), X(1.4), Z(8.6)), 0.34);
  線(p => p.M(X(4.5), Z(6.4)).L(X(6.9), Z(6.35)), 0.36);
  線(p => p.M(X(4.5), Z(7.5)).L(X(6.9), Z(7.45)), 0.36);

  // 雙目梯四箱＋中杙
  const 梯 = (x0: number, x1: number, z0: number) => {
    線(p => p.M(X(x0), Z(z0)).L(X(x0), Z(z0 + 8.2)));
    線(p => p.M(X(x1), Z(z0 - 0.05)).L(X(x1), Z(z0 + 8.15)));
    for (let i = 0; i <= 4; i++)
      線(p => p.M(X(x0), Z(z0 + i * 2.05)).L(X(x1), Z(z0 + i * 2.05 - 0.05)));
  };
  梯(1.0, 3.0, 9.5);
  梯(4.4, 6.4, 9.5);
  線(p => p.M(X(3.7), Z(9.7)).L(X(3.65), Z(17.5)), 0.34);

  // 底小曲
  線(p => p.M(X(1.4), Z(18.0)).C(X(1.2), Z(18.5), X(1.2), Z(18.9), X(1.35), Z(19.2)));
  線(p => p.M(X(6.0), Z(17.95)).C(X(6.2), Z(18.5), X(6.2), Z(18.9), X(6.05), Z(19.15)));
};
