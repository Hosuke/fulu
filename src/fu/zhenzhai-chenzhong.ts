// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭病患沈重減福短壽之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p186_符葉六_跋末神降忌食.jpg（中段右一）
// 判準：符頭（圈桿長・長撇斜垂・散圈・人字圈鏈）＋亭蓋（雙線）＋大箱（內因
// 大形・亖桁三・女干形——摹形從簡）。錨自 12 格裁圖（宅186_A chenzhong）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅沈重: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.4) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：圈桿長（騎圈四）
  線(p => p.M(X(0.9), Z(1.2)).L(X(6.6), Z(1.1)));
  圈(0.6, 1.2); 圈(2.7, 1.17); 圈(5.2, 1.13); 圈(6.9, 1.1);
  // 長撇斜垂（自桿右下曳，末小圈）
  出(p => p.M(X(5.5), Z(1.42))
    .C(X(3.7), Z(2.1), X(2.0), Z(2.9), X(1.0), Z(3.5)), 0.42);
  圈(0.75, 3.7, 0.22);
  // 散圈右
  圈(6.7, 2.4); 圈(6.8, 3.7);
  // 人字圈鏈（闊）
  圈(3.2, 5.0); 連([3.2, 5.0], [1.1, 6.6]); 連([3.2, 5.0], [5.3, 6.6]);
  圈(1.1, 6.6); 圈(5.3, 6.6); 圈(6.6, 6.5);

  // 亭蓋（雙線扁梯）
  線(p => p.M(X(1.5), Z(7.7)).L(X(6.1), Z(7.55)), 0.4);
  線(p => p.M(X(0.8), Z(8.6)).L(X(6.9), Z(8.45)), 0.4);
  線(p => p.M(X(1.5), Z(7.7)).L(X(0.8), Z(8.6)), 0.4);
  線(p => p.M(X(6.1), Z(7.55)).L(X(6.9), Z(8.45)), 0.4);

  // 大箱
  線(p => p.M(X(1.1), Z(9.4)).L(X(1.1), Z(19.0)).L(X(6.6), Z(18.9))
    .L(X(6.6), Z(9.3)).L(X(1.05), Z(9.4)), 0.4);
  // 內大形
  線(p => p.M(X(1.7), Z(10.1)).L(X(5.9), Z(10.05)), 0.34);
  出(p => p.M(X(3.8), Z(10.1)).C(X(3.4), Z(10.7), X(2.6), Z(11.2), X(1.8), Z(11.6)), 0.36);
  出(p => p.M(X(3.8), Z(10.1)).C(X(4.2), Z(10.7), X(5.0), Z(11.2), X(5.8), Z(11.6)), 0.36);
  // 亖桁五
  線(p => p.M(X(1.5), Z(12.1)).L(X(6.2), Z(12.05)), 0.34);
  線(p => p.M(X(1.5), Z(12.8)).L(X(6.2), Z(12.75)), 0.34);
  線(p => p.M(X(1.5), Z(13.5)).L(X(6.2), Z(13.45)), 0.34);
  線(p => p.M(X(1.5), Z(14.2)).L(X(6.2), Z(14.15)), 0.34);
  線(p => p.M(X(1.5), Z(14.9)).L(X(6.2), Z(14.85)), 0.34);
  // 攵千形（摹形從簡）
  出(p => p.M(X(2.5), Z(15.5)).C(X(2.2), Z(15.9), X(1.8), Z(16.2), X(1.4), Z(16.5)), 0.34);
  線(p => p.M(X(1.5), Z(16.7)).L(X(3.1), Z(16.6)), 0.32);
  出(p => p.M(X(3.0), Z(16.6)).C(X(2.6), Z(17.3), X(2.0), Z(18.0), X(1.4), Z(18.6)), 0.36);
  出(p => p.M(X(1.8), Z(17.1)).C(X(2.3), Z(17.6), X(2.8), Z(18.1), X(3.2), Z(18.5)), 0.36);
  線(p => p.M(X(3.9), Z(16.1)).L(X(6.2), Z(16.05)), 0.34);
  線(p => p.M(X(5.05), Z(15.5)).L(X(5.03), Z(18.7)), 0.36);
};
