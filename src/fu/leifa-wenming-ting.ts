// ─────────────────────────────────────────────────────────────────────────────
// 道法會元卷十二・雷霆聞命符（霆首） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3843_卷12雷霆聞命清微令符-07.png（「霆」題）
// 原文：「右符雷霆聞命，急如星火，焚於烈火香上」；組凡三道，此其三。
// 判準：題字不摹——符形：波桁入右口曲（環開下）→右降曲S折→中波桁鉤挑・
// 下波桁→右緣長垂S尾右曳→左波劃二。承組硃層之制。幅比 1.2（z 0..28.8）。
// 錨自 12 格裁圖（格_wting）：X=(col-8.0)×2.0、Z=(row-3.6)×2.0+2。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 聞命霆首: 符筆 = (bi) => {
  const u = bi.u, k = 2.0;
  const X = (c: number) => (c - 8.0) * k;
  const Z = (r: number) => (r - 3.6) * k + 2;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.68) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  bi.硃(() => {
    // 波桁入右口曲（環開下）
    抖(p => p.M(X(4.6), Z(4.6))
      .C(X(5.6), Z(4.2), X(7.0), Z(4.2), X(8.4), Z(4.4))
      .C(X(9.4), Z(4.1), X(10.2), Z(4.2), X(10.4), Z(4.8))
      .C(X(10.5), Z(5.3), X(10.1), Z(5.7), X(9.7), Z(5.6)), 0.72);
    // 右降曲S折
    抖(p => p.M(X(9.6), Z(6.0))
      .C(X(9.3), Z(6.6), X(9.4), Z(7.2), X(10.2), Z(7.6))
      .C(X(10.8), Z(7.9), X(10.9), Z(8.5), X(10.5), Z(9.0)), 0.7);
    // 中波桁鉤挑
    抖(p => p.M(X(5.0), Z(6.3))
      .C(X(5.8), Z(6.9), X(6.8), Z(7.2), X(7.7), Z(7.1))
      .C(X(8.1), Z(7.0), X(8.3), Z(6.7), X(8.2), Z(6.4)), 0.68);
    // 下波桁（會右緣）
    抖(p => p.M(X(5.1), Z(8.7))
      .C(X(6.3), Z(9.3), X(7.7), Z(9.6), X(8.9), Z(9.5))
      .C(X(9.4), Z(9.6), X(9.9), Z(9.5), X(10.2), Z(9.3)), 0.7);
    // 右緣長垂S尾右曳
    抖(p => p.M(X(10.2), Z(9.4))
      .C(X(10.4), Z(10.5), X(10.3), Z(11.5), X(10.4), Z(12.5))
      .C(X(10.5), Z(13.4), X(10.8), Z(14.2), X(11.3), Z(14.8))
      .C(X(11.7), Z(15.3), X(12.1), Z(15.6), X(12.5), Z(15.8)), 0.72);
    // 左波劃二
    抖(p => p.M(X(5.2), Z(11.5))
      .C(X(6.3), Z(11.8), X(7.5), Z(11.9), X(8.6), Z(11.7)), 0.62);
    抖(p => p.M(X(4.5), Z(13.7))
      .C(X(5.9), Z(14.0), X(7.5), Z(14.1), X(9.0), Z(13.9)), 0.62);
  });
};
