// ─────────────────────────────────────────────────────────────────────────────
// 祈晴符（道法會元卷九十四・雨頭十五類符・七） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3923_卷94十五類符雨頭一覽-p26.png（右起第七）
// 判準：雨字符頭之制（頂桁・冠框・內左三弧右三橫・中豎・●墨點・左右長腳）＋光形・左日右昜・甫框（頂橫中豎內橫）。
// 符下咒音記位不錄。摹形從簡。錨自 8 格裁圖（格94_後五 qiqing）：
// X=(c-3.6)×2.55、Z=(r-5.0)×2.55+1.0。幅比 3。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 祈晴符: 符筆 = (bi) => {
  const u = bi.u, k = 2.55;
  const X = (c: number) => (c - 3.6) * k;
  const Z = (r: number) => (r - 5.0) * k + 1.0;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.62) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.65) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 點 = (x: number, z: number, r = 0.42) =>
    bi.塊(p => p.M(X(x - r), Z(z)).C(X(x - r), Z(z - r * 1.3), X(x + r), Z(z - r * 1.3), X(x + r), Z(z))
      .C(X(x + r), Z(z + r * 1.3), X(x - r), Z(z + r * 1.3), X(x - r), Z(z)).close());

  // 雨冠
  線(p => p.M(X(0.2), Z(5.05)).L(X(7.0), Z(4.98)), 0.8);
  出(p => p.M(X(0.35), Z(5.10)).L(X(0.33), Z(18.19)).C(X(0.32), Z(18.73), X(0.22), Z(19.14), X(0.08), Z(19.41)), 0.7);
  出(p => p.M(X(6.75), Z(5.10)).L(X(6.73), Z(19.81)).C(X(6.75), Z(20.35), X(6.85), Z(20.76), X(7.0), Z(21.02)), 0.7);
  線(p => p.M(X(0.9), Z(5.90)).C(X(1.70), Z(5.74), X(2.10), Z(5.82), X(2.5), Z(5.94)), 0.55);
  線(p => p.M(X(0.9), Z(6.50)).C(X(1.70), Z(6.34), X(2.10), Z(6.42), X(2.5), Z(6.54)), 0.55);
  線(p => p.M(X(0.9), Z(7.10)).C(X(1.70), Z(6.94), X(2.10), Z(7.02), X(2.5), Z(7.14)), 0.55);
  線(p => p.M(X(4.3), Z(5.90)).L(X(5.9), Z(5.86)), 0.55);
  線(p => p.M(X(4.3), Z(6.50)).L(X(5.9), Z(6.46)), 0.55);
  線(p => p.M(X(4.3), Z(7.10)).L(X(5.9), Z(7.06)), 0.55);
  線(p => p.M(X(3.5), Z(5.15)).L(X(3.48), Z(7.60)), 0.6);
  // 光形
  出(p => p.M(X(3.9), Z(7.90)).L(X(3.82), Z(8.61)), 0.55);
  出(p => p.M(X(4.9), Z(7.85)).L(X(4.98), Z(8.54)), 0.55);
  線(p => p.M(X(3.6), Z(9.01)).L(X(5.3), Z(8.94)), 0.55);
  出(p => p.M(X(4.1), Z(9.28)).C(X(3.9), Z(9.96), X(3.7), Z(10.50), X(3.5), Z(10.90)), 0.55);
  出(p => p.M(X(4.7), Z(9.28)).C(X(4.9), Z(9.96), X(5.1), Z(10.50), X(5.3), Z(10.83)), 0.55);
  // 左日
  線(p => p.M(X(1.0), Z(9.96)).L(X(1.0), Z(12.66)).L(X(2.6), Z(12.60)).L(X(2.6), Z(9.90)).L(X(0.95), Z(9.96)), 0.55);
  線(p => p.M(X(1.1), Z(11.30)).L(X(2.5), Z(11.25)), 0.5);
  // 右昜
  線(p => p.M(X(3.5), Z(11.44)).L(X(3.5), Z(13.60)).L(X(5.3), Z(13.55)).L(X(5.3), Z(11.39)).L(X(3.45), Z(11.44)), 0.55);
  線(p => p.M(X(3.6), Z(12.52)).L(X(5.2), Z(12.47)), 0.5);
  線(p => p.M(X(3.5), Z(14.01)).L(X(5.4), Z(13.94)), 0.5);
  出(p => p.M(X(3.8), Z(14.27)).C(X(3.7), Z(14.82), X(3.55), Z(15.22), X(3.4), Z(15.49)), 0.5);
  出(p => p.M(X(4.4), Z(14.34)).C(X(4.3), Z(14.88), X(4.15), Z(15.29), X(4.0), Z(15.62)), 0.5);
  出(p => p.M(X(5.0), Z(14.27)).C(X(4.9), Z(14.82), X(4.75), Z(15.22), X(4.6), Z(15.62)), 0.5);
  // 甫框
  線(p => p.M(X(1.0), Z(16.16)).L(X(6.1), Z(16.08)), 0.6);
  線(p => p.M(X(1.5), Z(16.70)).L(X(1.5), Z(20.48)).L(X(5.7), Z(20.40)).L(X(5.7), Z(16.62)).L(X(1.45), Z(16.70)), 0.6);
  線(p => p.M(X(3.6), Z(15.90)).L(X(3.58), Z(20.89)), 0.6);
  線(p => p.M(X(1.6), Z(18.46)).L(X(5.6), Z(18.39)), 0.5);
  點(3.3, 23.19);
};
