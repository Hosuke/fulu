// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・比符大招官職 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p182_符葉二.jpg（下段右一）
// 判準：星紋符頭（桿二・个字圈樹二・側圈）＋桿＋寬迴帶（boustro圓折・
// 中十杙）＋底桁＋巛流足四。錨自 12 格裁圖（宅182_B bifu）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅比符: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.32 * k, Z(z)).L(X(x1) - 0.32 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：桿二＋个字圈樹二＋側圈
  桿(0.7, 7.3, 1.0);
  桿(0.7, 7.3, 2.5);
  const 樹 = (ax: number) => {
    圈(ax, 4.3);
    for (const t of [[ax - 0.9, 6.0], [ax, 6.3], [ax + 0.9, 6.0]] as const) {
      連([ax, 4.3], [t[0], t[1]]); 圈(t[0], t[1]);
    }
  };
  樹(2.0); 樹(5.2);
  圈(7.0, 4.5); 圈(7.2, 6.0);

  // 桿＋寬迴帶（boustro圓折五桁＋中十杙）
  桿(0.8, 7.3, 7.5);
  線(p => p.M(X(0.8), Z(8.4)).L(X(0.8), Z(14.6)));
  線(p => p.M(X(0.9), Z(8.5)).L(X(6.6), Z(8.45))
    .C(X(7.1), Z(8.45), X(7.1), Z(9.4), X(6.6), Z(9.45))
    .L(X(1.7), Z(9.5)).C(X(1.2), Z(9.5), X(1.2), Z(10.45), X(1.7), Z(10.5))
    .L(X(6.6), Z(10.45)).C(X(7.1), Z(10.45), X(7.1), Z(11.4), X(6.6), Z(11.45))
    .L(X(1.7), Z(11.5)).C(X(1.2), Z(11.5), X(1.2), Z(12.45), X(1.7), Z(12.5))
    .L(X(6.6), Z(12.45)).C(X(7.1), Z(12.45), X(7.1), Z(13.4), X(6.6), Z(13.45))
    .L(X(0.9), Z(13.5)));
  線(p => p.M(X(3.9), Z(8.5)).L(X(3.85), Z(10.5)), 0.34);
  線(p => p.M(X(4.0), Z(11.5)).L(X(3.95), Z(13.45)), 0.34);

  // 底桁＋巛流足四
  線(p => p.M(X(0.8), Z(15.0)).L(X(7.3), Z(14.9)), 0.42);
  const 流 = (x0: number) => 線(p => p.M(X(x0), Z(15.4))
    .C(X(x0 - 0.4), Z(16.3), X(x0 + 0.4), Z(17.2), X(x0), Z(18.1))
    .C(X(x0 - 0.3), Z(18.7), X(x0 - 0.4), Z(19.1), X(x0 - 0.5), Z(19.4)));
  流(1.5); 流(3.0); 流(4.6); 流(6.1);
};
