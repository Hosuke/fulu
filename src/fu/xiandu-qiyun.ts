// 玉清大梵起雲玉符（紫微仙都九符・三） · 親筆
// 據圖版：參攷-local/紫微諱/樣頁/樣_94_雷霆玉經紫微仙都九符致雨興電符-p52.png
// （右圖；題「玉清大梵起雲玉符」承 p51 葉末）
// 判準：密摺蟠曲雲篆（摺＝橫程）——頭三疊帶（左十摺・中六三三摺・右四三二
// 摺綴右緣豎摺）＋滿幅六摺（左緣丫剔・結點）＋三疊帶（左綴己結）＋滿幅波線
// ＋滿幅三摺承對疊帶（丫剔）＋冊帶（豎三夾對摺）＋底帶對摺己足。
// 摺帶連綿摹形從簡。錨自 10 格裁圖（格_qiyun2）：X=(c-4.9)×2.3、
// Z=(r-0.05)×2.3+0.8（k 2.4→2.3 收轉端極值入界）。幅比 2.6。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 起雲玉符: 符筆 = (bi) => {
  const u = bi.u, k = 2.3;
  const X = (c: number) => (c - 4.9) * k;
  const Z = (r: number) => (r - 0.05) * k + 0.8;
  const 抖 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 摺 = (x0: number, x1: number, rT: number, rB: number, n: number, 左起 = true, 寬 = 0.4) =>
    抖(pa => {
      const p = n > 1 ? (rB - rT) / (n - 1) : 0;
      let dir = 左起 ? 1 : -1, y = rT;
      pa.M(X(dir > 0 ? x0 : x1), Z(y));
      for (let i = 0; i < n; i++) {
        const xe = dir > 0 ? x1 : x0;
        if (i < n - 1) {
          pa.L(X(xe - dir * p * 0.35), Z(y));
          pa.C(X(xe + dir * p * 1.05), Z(y - p * 0.05), X(xe + dir * p * 1.05), Z(y + p * 1.05), X(xe - dir * p * 0.35), Z(y + p));
          y += p; dir = -dir;
        } else pa.L(X(xe), Z(y));
      }
    }, 寬);
  const 丫 = (x: number, r: number, 向 = 1) => {
    抖(p => p.M(X(x - 向 * 0.3), Z(r - 0.25)).L(X(x), Z(r)), 0.3);
    抖(p => p.M(X(x - 向 * 0.3), Z(r + 0.25)).L(X(x), Z(r)), 0.3);
  };

  // 頭・入鉤＋左疊帶十摺
  抖(p => p.M(X(1.35), Z(0.3)).C(X(1.7), Z(0.08), X(2.1), Z(0.08), X(2.25), Z(0.32)), 0.4);
  摺(0.15, 3.1, 0.4, 4.85, 10);
  // 頭・中疊帶（六＋三＋三）
  摺(3.6, 6.3, 0.35, 2.4, 6);
  摺(3.6, 5.9, 2.55, 3.6, 3);
  摺(4.6, 5.9, 3.75, 4.85, 3);
  // 頭・右疊帶（四＋三＋二）＋右緣豎摺
  摺(6.6, 9.6, 0.5, 2.0, 4);
  摺(6.6, 8.5, 2.15, 3.2, 3);
  摺(6.7, 7.8, 3.35, 4.0, 2);
  摺(8.6, 9.6, 2.15, 4.4, 5);

  // 滿幅六摺（左緣丫剔二・結點）
  摺(0.3, 9.6, 4.95, 8.55, 6);
  丫(0.32, 5.0); 丫(0.32, 7.7);
  抖(p => p.M(X(0.45), Z(7.3)).C(X(0.68), Z(7.25), X(0.72), Z(7.45), X(0.5), Z(7.48)), 0.36);

  // 三疊帶（左綴己結）
  摺(0.3, 2.9, 8.65, 10.6, 4);
  抖(p => p.M(X(2.15), Z(10.95))
    .C(X(0.9), Z(11.0), X(0.85), Z(11.5), X(1.6), Z(11.55))
    .C(X(2.2), Z(11.6), X(2.25), Z(11.85), X(1.5), Z(11.9)), 0.4);
  摺(3.3, 5.9, 8.75, 11.5, 6);
  摺(6.4, 9.5, 8.7, 11.6, 6);

  // 滿幅波線（左端小鉤）
  抖(p => p.M(X(0.4), Z(12.35))
    .C(X(0.55), Z(12.1), X(1.5), Z(12.0), X(2.6), Z(12.25))
    .C(X(4.0), Z(12.5), X(5.2), Z(12.0), X(6.6), Z(12.25))
    .C(X(7.8), Z(12.45), X(8.7), Z(12.05), X(9.6), Z(12.2)), 0.4);

  // 滿幅三摺承對疊帶（丫剔）
  摺(0.3, 9.6, 12.5, 13.4, 3);
  摺(0.3, 3.5, 13.55, 16.5, 6);
  摺(4.0, 9.5, 13.6, 16.5, 6);
  丫(4.05, 14.45); 丫(4.05, 15.35);

  // 冊帶（豎三夾對摺）
  線(p => p.M(X(0.35), Z(16.75)).L(X(0.33), Z(19.35)), 0.42);
  線(p => p.M(X(4.6), Z(16.75)).L(X(4.58), Z(19.35)), 0.42);
  線(p => p.M(X(9.5), Z(16.7)).L(X(9.48), Z(19.3)), 0.42);
  摺(0.5, 4.4, 17.05, 19.3, 4);
  摺(4.75, 9.35, 17.05, 19.3, 4);

  // 底帶（滿幅三摺＋對摺）
  摺(0.3, 9.5, 19.5, 21.2, 3);
  摺(0.3, 4.4, 21.35, 23.4, 4);
  摺(4.9, 9.4, 21.35, 23.4, 4);

  // 己足對
  抖(p => p.M(X(0.65), Z(23.55))
    .C(X(0.55), Z(24.2), X(0.45), Z(24.7), X(0.5), Z(25.0))
    .C(X(0.55), Z(25.38), X(0.95), Z(25.3), X(0.95), Z(24.95))
    .C(X(0.95), Z(24.7), X(0.7), Z(24.7), X(0.65), Z(24.9)), 0.4);
  抖(p => p.M(X(9.15), Z(23.5))
    .C(X(9.28), Z(24.15), X(9.4), Z(24.65), X(9.35), Z(24.95))
    .C(X(9.3), Z(25.33), X(8.9), Z(25.25), X(8.92), Z(24.9))
    .C(X(8.94), Z(24.65), X(9.18), Z(24.66), X(9.2), Z(24.86)), 0.4);
};
