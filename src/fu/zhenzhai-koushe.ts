// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭除口舌惡事侵害 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p181_符葉一.jpg（右上首符）
// 判準：星紋符頭（三橫圈桿＋星叢放射，圈皆空心）＋方格疊書符身（蛇帶疊摺）＋
// 曲腳（雙盤 S 迴）。錨自 24 指格網讀出（宅_koushe.png），仿射入紙（k=1.48）。
// 幅比 2.2。逐筆釋讀〔待核〕；密處摹形從簡注明。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅口舌: 符筆 = (bi) => {
  const u = bi.u, k = 1.48, cx0 = -5.6, z00 = 6.0;
  const X = (x: number) => (x - cx0) * k;
  const Z = (z: number) => (z - z00) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.42) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.32 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {   // 橫圈桿：兩端空圈
    線(p => p.M(X(x0) + 0.5 * k, Z(z)).L(X(x1) - 0.5 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：三橫圈桿
  桿(-12.2, 0.2, 6.6);
  桿(-12.2, 0.0, 8.3);
  桿(-12.2, -0.1, 10.1);

  // 星叢：中星放射四出（圈心＋短杙），外綴自由圈
  const 心: [number, number] = [-6.3, 15.6];
  for (const [tx, tz] of [[-6.3, 11.7], [-3.4, 12.6], [-9.0, 12.6], [-3.7, 15.4]] as const) {
    // 射線止於兩端圈緣（勿入孔——Codex 審 2026-07-12）
    const dx = tx - 心[0], dz = tz - 心[1], L = Math.hypot(dx, dz);
    const s0 = 0.55 / L, s1 = 0.42 / L;
    線(p => p.M(X(心[0] + dx * s0), Z(心[1] + dz * s0)).L(X(tx - dx * s1), Z(tz - dz * s1)));
    圈(tx, tz, 0.42);
  }
  圈(心[0], 心[1], 0.55);
  線(p => p.M(X(-6.3), Z(11.7 - 0.42)).L(X(-6.3), Z(11.0)));   // 頂杙（自圈緣起）
  for (const [fx, fz] of [[-11.9, 12.5], [-11.9, 15.4], [-10.3, 15.5],
                          [0.0, 11.7], [-0.1, 13.2], [-0.1, 14.7]] as const) 圈(fx, fz, 0.4);

  // 符身（蛇帶疊摺——摹形：密處以帶行代逐格，注〔簡〕）
  // 上層：左右二匣各三桁＋中王
  for (const z of [16.4, 17.5, 18.7]) 線(p => p.M(X(-12.0), Z(z)).L(X(-6.7), Z(z)));
  線(p => p.M(X(-12.0), Z(16.3)).L(X(-12.0), Z(18.8)));
  for (const z of [16.2, 17.3, 18.5]) 線(p => p.M(X(-3.4), Z(z)).L(X(0.4), Z(z)));
  線(p => p.M(X(-3.4), Z(16.1)).L(X(-3.4), Z(18.6)));
  線(p => p.M(X(-5.5), Z(16.0)).L(X(-5.5), Z(18.9)));   // 中豎（王骨）
  線(p => p.M(X(-6.5), Z(17.4)).L(X(-4.4), Z(17.3)));

  // 中層：亞匣（左）＋長桁二（右），下承全幅圓端蛇帶二折
  線(p => p.M(X(-11.6), Z(19.8)).L(X(-6.2), Z(19.8)));
  線(p => p.M(X(-11.6), Z(19.8)).L(X(-11.6), Z(21.6)).L(X(-6.2), Z(21.6)).L(X(-6.2), Z(19.8)));
  線(p => p.M(X(-9.9), Z(20.7)).C(X(-9.0), Z(20.2), X(-8.0), Z(20.2), X(-7.2), Z(20.7))); // 匣內雲曲〔簡〕
  for (const z of [20.2, 21.4] as const) 線(p => p.M(X(-5.8), Z(z)).L(X(0.6), Z(z)));
  // 圓端蛇帶（二長摺）
  線(p => p.M(X(0.3), Z(22.4))
    .L(X(-11.0), Z(22.5))
    .C(X(-12.0), Z(22.55), X(-12.0), Z(23.4), X(-11.0), Z(23.45))
    .L(X(0.0), Z(23.4))
    .C(X(0.8), Z(23.45), X(0.8), Z(24.3), X(0.0), Z(24.35))
    .L(X(-11.5), Z(24.4)));

  // 下層：全幅密桁五道＋左欄（二輪改——原刻密梯通幅），右緣長豎
  for (let i = 0; i < 5; i++) {
    const z = 25.3 + i * 1.1;
    線(p => p.M(X(-12.1), Z(z)).L(X(0.3), Z(z)));
  }
  線(p => p.M(X(-12.1), Z(25.2)).L(X(-12.1), Z(29.8)));
  線(p => p.M(X(-4.0), Z(28.6)).L(X(0.2), Z(28.6)).L(X(0.2), Z(30.9)).L(X(-4.2), Z(31.0)).L(X(-4.2), Z(28.6)));
  線(p => p.M(X(-2.0), Z(28.6)).L(X(-2.0), Z(31.0)));   // 亞中豎
  線(p => p.M(X(0.6), Z(24.6)).L(X(0.65), Z(38.6)));    // 右緣長豎（末微鉤）

  // 符腳：雙盤 S 迴（左右各一，一筆連書），尾出鋒
  bi.一筆({ 寬: 0.5 * u, 起: '藏', 收: '出' }, p => p
    .M(X(-5.2), Z(31.6))
    .C(X(-12.2), Z(31.7), X(-12.2), Z(33.5), X(-8.2), Z(33.4))
    .C(X(-5.2), Z(33.3), X(-5.2), Z(35.2), X(-8.6), Z(35.2))
    .C(X(-11.4), Z(35.2), X(-11.6), Z(36.8), X(-10.4), Z(37.3))
    .C(X(-11.2), Z(37.9), X(-11.7), Z(38.4), X(-11.9), Z(38.8)));
  bi.一筆({ 寬: 0.5 * u, 起: '藏', 收: '出' }, p => p
    .M(X(-0.4), Z(31.6))
    .C(X(-4.8), Z(31.8), X(-4.8), Z(33.6), X(-2.2), Z(33.5))
    .C(X(0.2), Z(33.4), X(0.2), Z(35.3), X(-2.6), Z(35.3))
    .C(X(-4.4), Z(35.3), X(-4.6), Z(36.6), X(-3.6), Z(37.2))
    .C(X(-3.2), Z(37.9), X(-3.0), Z(38.6), X(-2.9), Z(39.2)));
};
