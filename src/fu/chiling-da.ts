// 判準：原刻拆畫圖——大字三筆逐繫：一（真武符使大逞威靈・救民疾苦斷絕邪精・
// 永保長生眾神凛凛敕下奉行）・丿（滅跡除形・搜捉凶禍）・乀（收魂附體七魄・
// 安寧護身去禍）；本繪依畫序親書楷體成字（同敕字式之裁）。幅比 1.2（z 0..28.8）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 大字式: 符筆 = (bi) => {
  const u = bi.u;
  const 橫 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.05) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 1.15) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 一（真武符使——長橫微仰）
  橫(p => p.M(-8.2, 11.0).C(-4.0, 10.6, 2.0, 10.5, 8.0, 10.8));
  // 丿（滅跡除形搜捉凶禍——貫頂左撇）
  出(p => p.M(0.8, 4.2).C(0.0, 9.0, -1.6, 14.6, -4.4, 19.4)
    .C(-5.8, 21.6, -7.4, 23.4, -9.2, 24.8));
  // 乀（收魂附體安寧護身——右捺重出）
  出(p => p.M(-0.7, 12.6).C(1.6, 15.8, 4.2, 19.2, 6.6, 22.0)
    .C(7.5, 23.0, 8.5, 23.9, 9.4, 24.6), 1.25);
};
