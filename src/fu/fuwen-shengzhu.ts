// 複文・生生朱朱（雙生雙朱疊） · 親筆
// 據圖版：參攷-local/複文/道藏太平經卷104首_興上除害複文169_三家本冊24頁524.png
// （正統道藏原式，三家本影印冊24頁524；興上除害複文第一百六十九）
// 判準：上雙生並列・下雙朱並列（朱足弧張）——8x 親核（複_五格板）。
// 釋讀待核，名從形貌。幅比 1.0（z 0..24）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 複文生朱: 符筆 = (bi) => {
  const u = bi.u;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.85) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.9) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 生（雙）
  const 生 = (cx: number) => {
    出(p => p.M(cx - 0.9, 2.2).C(cx - 1.6, 2.9, cx - 2.3, 3.5, cx - 3.0, 4.0), 0.8);
    線(p => p.M(cx - 2.2, 3.9).L(cx + 2.2, 3.7), 0.8);
    線(p => p.M(cx - 2.6, 5.7).L(cx + 2.4, 5.5), 0.8);
    線(p => p.M(cx - 0.1, 3.8).L(cx - 0.15, 7.6), 0.85);
    線(p => p.M(cx - 2.9, 7.8).L(cx + 2.7, 7.6), 0.85);
  };
  生(-4.6); 生(4.6);

  // 朱（雙，足弧張）
  const 朱 = (cx: number) => {
    出(p => p.M(cx - 0.6, 12.4).C(cx - 1.3, 13.0, cx - 2.0, 13.5, cx - 2.7, 13.9), 0.75);
    線(p => p.M(cx - 2.6, 14.4).L(cx + 2.5, 14.2), 0.85);
    線(p => p.M(cx - 0.1, 13.0).L(cx - 0.15, 21.4), 0.9);
    出(p => p.M(cx - 0.3, 15.2).C(cx - 1.2, 17.2, cx - 2.2, 19.0, cx - 3.4, 20.4)
      .C(cx - 3.7, 20.8, cx - 4.0, 21.1, cx - 4.3, 21.3), 0.85);
    出(p => p.M(cx + 0.1, 15.2).C(cx + 1.0, 17.2, cx + 2.0, 19.0, cx + 3.2, 20.4)
      .C(cx + 3.5, 20.8, cx + 3.8, 21.1, cx + 4.1, 21.3), 0.85);
  };
  朱(-4.9); 朱(4.9);
};
