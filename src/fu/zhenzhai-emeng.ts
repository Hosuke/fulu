// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭惡夢經求無利息 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p182_符葉二.jpg（中段右四）
// 判準：星紋符頭（桿二・冂圈門——桿垂雙豎至圈、內圈對・側圈柱）＋厂桁＋
// 田田箱＋冂寬迴脚叉足。錨自 12 格裁圖（宅182_B emeng）：X=(c-3.95)×2.7、
// Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅惡夢: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 桿 = (x0: number, x1: number, z: number) => {
    線(p => p.M(X(x0) + 0.32 * k, Z(z)).L(X(x1) - 0.32 * k, Z(z)));
    圈(x0, z); 圈(x1, z);
  };

  // 符頭：桿二＋冂圈門＋側圈柱
  桿(0.7, 7.2, 1.0);
  桿(0.7, 7.2, 2.4);
  桿(2.4, 5.4, 3.6);
  線(p => p.M(X(2.4), Z(3.95)).L(X(2.35), Z(7.1)));
  線(p => p.M(X(5.5), Z(3.9)).L(X(5.55), Z(7.05)));
  圈(2.3, 7.5); 圈(5.6, 7.4);
  圈(3.4, 5.9); 圈(4.5, 5.8);
  for (const z of [3.5, 5.2, 6.8] as const) 圈(0.8, z);
  for (const z of [3.4, 5.0, 6.6] as const) 圈(7.1, z);

  // 厂桁
  線(p => p.M(X(0.9), Z(8.8)).L(X(7.1), Z(8.7)), 0.42);
  線(p => p.M(X(0.9), Z(8.85)).L(X(0.9), Z(9.7)));

  // 田田箱
  線(p => p.M(X(1.3), Z(10.3)).L(X(1.3), Z(13.2)).L(X(6.9), Z(13.15))
    .L(X(6.9), Z(10.25)).L(X(1.25), Z(10.3)));
  線(p => p.M(X(4.1), Z(10.35)).L(X(4.05), Z(13.15)));
  線(p => p.M(X(1.4), Z(11.7)).L(X(6.8), Z(11.65)));
  線(p => p.M(X(2.7), Z(10.35)).L(X(2.65), Z(13.15)), 0.34);
  線(p => p.M(X(5.5), Z(10.3)).L(X(5.45), Z(13.1)), 0.34);

  // 冂寬迴脚（boustro二折）＋叉足
  線(p => p.M(X(1.2), Z(13.4)).L(X(1.2), Z(17.9)));
  線(p => p.M(X(1.3), Z(14.0)).L(X(5.5), Z(13.95))
    .C(X(6.0), Z(13.95), X(6.0), Z(14.85), X(5.5), Z(14.9))
    .L(X(2.4), Z(14.95)).C(X(1.9), Z(14.95), X(1.9), Z(15.85), X(2.4), Z(15.9))
    .L(X(5.6), Z(15.85)).C(X(6.1), Z(15.85), X(6.2), Z(16.4), X(6.1), Z(17.0)));
  線(p => p.M(X(1.2), Z(18.1)).C(X(1.1), Z(18.6), X(1.1), Z(19.0), X(1.15), Z(19.3)));
  線(p => p.M(X(3.8), Z(17.2)).L(X(3.8), Z(19.1)));
  線(p => p.M(X(6.1), Z(17.3)).C(X(6.2), Z(18.0), X(6.2), Z(18.6), X(6.15), Z(19.1)));
};
