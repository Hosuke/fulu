// ─────────────────────────────────────────────────────────────────────────────
// 鎮宅靈符・厭不宜田蠶疾病之鬼 · 親筆
// 據圖版：參攷-local/鎮宅符/鎮宅靈符_三家本02_p185_符葉五.jpg（上段右一）
// 判準：星紋符頭（桿二・个字圈樹二・左箭楔・亞箱對）＋左目梯六箱・右柱
// 罒本罒白皃字形疊（摹形從簡）＋底桁。錨自 12 格裁圖（宅185_A butian）：
// X=(c-3.95)×2.7、Z=(r-0.7)×2.7+2。幅比 2.2。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 鎮宅不宜田: 符筆 = (bi) => {
  const u = bi.u, k = 2.7;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 0.7) * k + 2;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 圈 = (x: number, z: number, r = 0.28) => {
    const c = bi.ctx; c.save(); c.lineWidth = 0.3 * u;
    bi.A(X(x), Z(z), r * k); c.restore();
  };
  const 連 = (a: [number, number], b: [number, number]) => {
    const dx = b[0] - a[0], dz = b[1] - a[1], L = Math.hypot(dx, dz), t = 0.4 / L;
    線(p => p.M(X(a[0] + dx * t), Z(a[1] + dz * t)).L(X(b[0] - dx * t), Z(b[1] - dz * t)));
  };

  // 符頭：桿二＋个字圈樹二
  線(p => p.M(X(0.8), Z(1.5)).L(X(6.7), Z(1.4))); 圈(0.5, 1.5); 圈(7.0, 1.4);
  線(p => p.M(X(0.8), Z(2.15)).L(X(6.7), Z(2.05))); 圈(0.5, 2.15); 圈(7.0, 2.05);
  const 樹 = (ax: number) => {
    圈(ax, 2.9);
    for (const t of [[ax - 0.8, 4.0], [ax, 4.15], [ax + 0.8, 4.0]] as const) {
      連([ax, 2.9], [t[0], t[1]]); 圈(t[0], t[1]);
    }
  };
  樹(1.6); 樹(3.9);
  // 左箭楔＋亞箱對
  線(p => p.M(X(1.4), Z(4.9)).L(X(0.6), Z(5.2)).L(X(1.4), Z(5.5)), 0.4);
  線(p => p.M(X(1.7), Z(4.6)).L(X(1.7), Z(5.7)).L(X(3.2), Z(5.65))
    .L(X(3.2), Z(4.55)).L(X(1.65), Z(4.6)));
  線(p => p.M(X(3.7), Z(4.5)).L(X(3.7), Z(5.8)).L(X(5.9), Z(5.75))
    .L(X(5.9), Z(4.45)).L(X(3.65), Z(4.5)));
  線(p => p.M(X(4.8), Z(4.55)).L(X(4.78), Z(5.75)), 0.32);

  // 左目梯六箱
  線(p => p.M(X(0.9), Z(6.4)).L(X(0.9), Z(13.6)));
  線(p => p.M(X(3.4), Z(6.35)).L(X(3.4), Z(13.55)));
  for (let i = 0; i <= 6; i++)
    線(p => p.M(X(0.9), Z(6.4 + i * 1.2)).L(X(3.4), Z(6.35 + i * 1.2)));

  // 右柱：罒本罒白皃字形疊（摹形從簡）
  線(p => p.M(X(4.0), Z(6.4)).L(X(4.0), Z(7.8)).L(X(6.0), Z(7.75))
    .L(X(6.0), Z(6.35)).L(X(3.95), Z(6.4)));
  線(p => p.M(X(4.7), Z(6.45)).L(X(4.68), Z(7.75)), 0.3);
  線(p => p.M(X(5.3), Z(6.4)).L(X(5.28), Z(7.7)), 0.3);
  線(p => p.M(X(4.1), Z(8.5)).L(X(5.9), Z(8.45)), 0.36);
  線(p => p.M(X(5.0), Z(8.1)).L(X(4.98), Z(9.5)), 0.34);
  線(p => p.M(X(4.4), Z(9.0)).L(X(5.6), Z(9.4)), 0.3);
  線(p => p.M(X(4.0), Z(10.0)).L(X(4.0), Z(11.2)).L(X(6.0), Z(11.15))
    .L(X(6.0), Z(9.95)).L(X(3.95), Z(10.0)));
  線(p => p.M(X(4.1), Z(10.6)).L(X(5.9), Z(10.55)), 0.3);
  線(p => p.M(X(4.2), Z(11.8)).L(X(4.2), Z(13.0)).L(X(5.8), Z(12.95))
    .L(X(5.8), Z(11.75)).L(X(4.15), Z(11.8)));
  // 皃曲
  線(p => p.M(X(4.6), Z(13.4))
    .C(X(4.4), Z(14.0), X(4.6), Z(14.5), X(5.2), Z(14.55))
    .C(X(5.7), Z(14.6), X(5.9), Z(14.2), X(5.6), Z(13.95)), 0.36);

  // 底桁
  線(p => p.M(X(0.9), Z(14.2)).L(X(3.4), Z(14.15)), 0.4);
  線(p => p.M(X(0.9), Z(15.0)).L(X(3.4), Z(14.95)), 0.4);
};
