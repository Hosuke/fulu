// ─────────────────────────────────────────────────────────────────────────────
// 陳安世入山辟虎狼符・二 · 親筆
// 據圖版：四庫全卷 PDF p041 抽葉（參攷-local/入山符/，欽定四庫全書繕本，PD）
// 判準：五川入桁（五長豎曲流會於橫桁，左右外二豎越桁下鉤）→中脊長垂
// （中豎貫桁直落，中腰微斷再續，末梢右曳收鉤）→肋弓五對（脊左右各五弧，
// 上三對桁下密列，下二對中腰以下疏列）。丹書絹：全符硃層。
// 幅比 1.5（z 0..36）。錨：X=(col-12.6)×1.05、Z=(row-1.6)×1.05（格_chen2）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 陳安世符二: 符筆 = (bi) => {
  const u = bi.u;
  const k = 1.05;
  const X = (c: number) => (c - 12.6) * k;
  const Z = (r: number) => (r - 1.6) * k;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.58) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.58) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  bi.硃(() => {
    // 五川入桁
    出(p => p.M(X(6.7), Z(2.2)).C(X(7.6), Z(5.4), X(7.5), Z(8.4), X(7.4), Z(11.4))
      .C(X(7.35), Z(12.0), X(7.0), Z(12.4), X(6.6), Z(12.6)));          // 左一（越桁左鉤）
    線(p => p.M(X(9.3), Z(2.2)).C(X(10.3), Z(5.3), X(10.6), Z(8.3), X(10.7), Z(11.4)));
    線(p => p.M(X(15.2), Z(2.3)).C(X(14.5), Z(5.4), X(14.3), Z(8.3), X(14.2), Z(11.4)));
    出(p => p.M(X(17.6), Z(2.2)).C(X(17.9), Z(5.5), X(17.9), Z(8.5), X(17.9), Z(11.4))
      .C(X(17.9), Z(12.0), X(18.2), Z(12.4), X(18.6), Z(12.6)));        // 右一（越桁右鉤）
    線(p => p.M(X(7.7), Z(11.55)).L(X(17.3), Z(11.35)), 0.62);          // 橫桁

    // 中脊長垂（貫桁，中腰微斷再續——依原刻）
    線(p => p.M(X(12.4), Z(2.1)).C(X(12.5), Z(8.0), X(12.5), Z(14.0), X(12.6), Z(20.4)), 0.6);
    出(p => p.M(X(12.7), Z(20.9)).C(X(12.9), Z(24.5), X(13.1), Z(27.5), X(13.2), Z(29.4))
      .C(X(13.25), Z(31.0), X(12.9), Z(32.4), X(11.9), Z(33.3)), 0.6);

    // 肋弓五對（各弧收上挑）
    const 弓 = (x0: number, x1: number, z0: number, 沉: number) =>
      線(p => p.M(X(x0), Z(z0)).C(X(x0 + (x1 - x0) * 0.3), Z(z0 + 沉),
        X(x0 + (x1 - x0) * 0.72), Z(z0 + 沉), X(x1), Z(z0 - 0.25)), 0.6);
    弓(6.8, 11.2, 13.6, 0.95); 弓(13.5, 17.7, 13.3, 0.95);
    弓(6.8, 11.2, 15.7, 0.95); 弓(13.6, 17.9, 15.4, 0.95);
    弓(6.9, 11.3, 17.9, 0.95); 弓(13.7, 18.0, 17.6, 0.95);
    弓(6.5, 11.9, 21.1, 1.1);  弓(13.4, 18.6, 20.8, 1.1);
    弓(6.9, 12.1, 24.6, 1.1);  弓(13.9, 18.7, 24.3, 1.1);
  });
};
