// ─────────────────────────────────────────────────────────────────────────────
// 老君入山符・四 · 親筆
// 據圖版：參攷-local/入山符/抱朴子登涉_入山符_p034_符四.png（四庫繕本，PD）
// 判準：通體星陣（連珠）——左上短杙、橫貫接右柱（通天徹地，中綴三星）、
// 中央星頂射三歧（左右斜垂雙絲居中）、底列五星。丹書桃板——全符硃層。
// 幅比 1.5（z 0..36）。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 入山符四: 符筆 = (bi) => {
  const u = bi.u;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.38) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 星 = (x: number, z: number, r = 0.45) => bi.dot(x, z, r);

  bi.硃(() => {
    // 左上短杙：小蓋＋豎落星
    線(p => p.M(-6.6, 3.0).C(-6.4, 2.7, -6.1, 2.7, -5.9, 3.0));
    線(p => p.M(-6.25, 3.1).L(-6.3, 5.3));
    星(-6.3, 5.5);
    // 橫貫：左星至右柱
    線(p => p.M(-6.3, 5.5).L(5.0, 5.2));
    星(5.0, 5.2);
    // 右柱：頂杙 T，通天徹地，中綴二星，末承星
    線(p => p.M(4.6, 2.7).L(5.5, 2.7));
    線(p => p.M(5.05, 2.7).L(5.25, 33.9));
    星(5.1, 16.6); 星(5.2, 23.6, 0.6); 星(5.25, 34.1);

    // 中央星頂：三歧下射
    星(0.0, 10.4, 0.52);
    // 左歧：斜至星，折豎而下
    線(p => p.M(-0.1, 10.6).L(-4.3, 16.6));
    星(-4.3, 16.8);
    線(p => p.M(-4.3, 16.8).L(-4.35, 34.0));
    星(-4.35, 34.2);
    // 右歧：斜至星，折豎而下
    線(p => p.M(0.1, 10.6).L(3.4, 16.7));
    星(3.4, 16.9);
    線(p => p.M(3.4, 16.9).L(3.4, 34.0));
    星(3.4, 34.2);
    // 中央雙絲：微分而斂，垂至底星
    線(p => p.M(-0.15, 10.7)
      .C(-0.5, 14.0, -0.7, 17.5, -0.55, 21.0)
      .C(-0.5, 25.5, -0.65, 30.0, -0.8, 34.0));
    線(p => p.M(0.15, 10.7)
      .C(0.5, 14.5, 0.6, 18.0, 0.5, 21.5)
      .C(0.45, 26.0, 0.7, 30.0, 0.9, 34.0));
    星(-0.8, 34.2); 星(0.9, 34.2);
  });
};
