// ─────────────────────────────────────────────────────────────────────────────
// 中嶽嵩山真形圖 · 親筆
// 據圖版：參攷-local/五嶽真形圖/Gujin_Tushu_Jicheng_pic010_五嶽真形圖.png（嵩嶽）
// 判準：古今圖書集成（1726）明碑一系摹刻；錨自 24 指格網逐點讀出（格_song.png）。
// 幅比 0.79（高≈19 指）。式：方壇之匡——二柱飛簷、頂欄焰三舌、底承廣笏；
// 匡內左厚月抱卵、右上仰月、右下圓璧含竅。
// 二輪（2026-07-12）：柱・欄・月・璧全面加肥近木刻；柱底接笏。
// ─────────────────────────────────────────────────────────────────────────────
import { 卵 } from '../fubi.js';
import type { 符筆 } from './index.js';

export const 嵩嶽真形: 符筆 = (bi) => {
  const u = bi.u;
  const 揮 = (寬指: number, 起: '藏' | '尖', 收: '回' | '出',
              書: Parameters<typeof bi.一筆>[1]) =>
    bi.一筆({ 寬: 寬指 * u, 起, 收 }, 書);

  // 左柱（塊）：頂出飛簷（尖左），柱身厚實，下接底笏
  bi.塊(t => t.M(-10.4, 1.75)
    .C(-9.2, 2.6, -8.6, 3.9, -8.35, 5.4)
    .C(-8.1, 8.0, -8.1, 11.0, -8.45, 14.3)
    .L(-6.75, 14.3)
    .C(-7.0, 11.0, -7.0, 8.2, -6.8, 5.8)
    .C(-6.7, 4.6, -6.95, 3.4, -7.55, 2.75)
    .C(-8.1, 2.1, -9.2, 1.85, -10.4, 1.75)
    .close());

  // 右柱（塊）：頂出飛簷（尖右）
  bi.塊(t => t.M(10.1, 1.65)
    .C(9.0, 2.5, 8.5, 3.8, 8.3, 5.3)
    .C(8.15, 8.0, 8.2, 11.0, 8.55, 14.3)
    .L(6.4, 14.3)
    .C(6.6, 11.0, 6.5, 8.2, 6.25, 5.7)
    .C(6.15, 4.5, 6.4, 3.3, 7.0, 2.6)
    .C(7.9, 2.0, 9.0, 1.75, 10.1, 1.65)
    .close());

  // 頂欄（厚橫）＋焰三舌
  揮(2.0, '藏', '回', p => p.M(-7.6, 4.0).C(-2.5, 3.6, 2.5, 3.55, 7.7, 3.9));
  揮(2.2, '尖', '回', p => p.M(-4.7, 1.2).C(-4.9, 2.1, -4.8, 3.0, -4.5, 3.9));
  揮(2.2, '尖', '回', p => p.M(0.9, 0.8).C(0.65, 1.7, 0.65, 2.6, 0.8, 3.7));
  揮(2.2, '尖', '回', p => p.M(4.4, 1.3).C(4.15, 2.1, 4.1, 3.0, 4.15, 3.8));

  // 底欄廣笏（塊）：兩端出鋒下垂
  bi.塊(t => t.M(-10.1, 13.95)
    .C(-3.0, 13.5, 3.0, 13.5, 9.8, 14.05)
    .C(10.35, 14.8, 10.45, 15.9, 10.1, 16.8)
    .C(6.5, 16.2, 3.0, 16.05, 0.0, 16.1)
    .C(-3.0, 16.15, -7.0, 16.35, -10.3, 16.8)
    .C(-10.75, 15.9, -10.7, 14.8, -10.1, 13.95)
    .close());

  // 匡內左：厚月（開口向右），月懷抱卵
  bi.塊(t => t.M(-2.5, 4.7)
    .C(-5.75, 5.2, -6.55, 7.5, -6.4, 9.3)
    .C(-6.25, 11.5, -4.95, 13.2, -2.7, 13.5)
    .C(-4.3, 12.5, -4.75, 11.0, -4.7, 9.25)
    .C(-4.65, 7.4, -3.95, 5.85, -2.5, 4.7)
    .close());
  bi.塊(t => 卵(t, -3.65, 9.4, 1.05, 1.4, 0.15));

  // 匡內右上：仰月（薄月開口向上，離璧留白）
  bi.塊(t => t.M(0.2, 5.0)
    .C(1.4, 6.0, 3.2, 6.05, 4.7, 5.3)
    .C(4.2, 6.6, 3.2, 7.35, 2.1, 7.4)
    .C(1.1, 7.45, 0.45, 6.4, 0.2, 5.0)
    .close());

  // 匡內右下：圓璧含竅（環珠）
  bi.塊(t => 卵(t, 2.35, 10.4, 2.5, 2.4));
  bi.鑿(t => 卵(t, 2.45, 10.55, 0.88, 0.83));
};
