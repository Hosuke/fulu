// ─────────────────────────────────────────────────────────────────────────────
// 太上符（道法會元卷一百二・「太上」二字符頭七符・二） · 親筆
// 據圖版：參攷-local/雷法符/樣頁/樣_3931_卷102太上二字符頭七符-p07.png（右起第二）
// 判準：太上符頭通制（頂小鉤・大橫・左右長弧腳下垂入身・上字・敕曲核心
// ——十字桁貫豎承大与曲）＋大迴摺三重・底密摺二重承長垂・末由田框長尾。「右七符用皁紙」書材註記；行持說明
// 與咒記位不錄；七符之六在葉（右起第一葉緣截斷僅存左緣，俟原影補〔待核〕）。
// 摹形從簡。錨自 8 格裁圖（格102_五 ts1）：X=(c-3.95)×1.85、
// Z=(r-2.4)×1.85+1.0。幅比 3。逐筆釋讀〔待核〕。
// ─────────────────────────────────────────────────────────────────────────────
import type { 符筆 } from './index.js';

export const 太上符二: 符筆 = (bi) => {
  const u = bi.u, k = 1.85;
  const X = (c: number) => (c - 3.95) * k;
  const Z = (r: number) => (r - 2.4) * k + 1.0;
  const 線 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.55) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '回' }, 書);
  const 出 = (書: Parameters<typeof bi.一筆>[1], 寬 = 0.58) =>
    bi.一筆({ 寬: 寬 * u, 起: '藏', 收: '出' }, 書);

  // 太上冠：頂小鉤・大橫・左右長弧腳・上字
  出(p => p.M(X(3.75), Z(2.6)).C(X(3.85), Z(3.0), X(3.95), Z(3.3), X(4.0), Z(3.5)), 0.5);
  線(p => p.M(X(0.55), Z(4.0)).L(X(7.25), Z(3.92)), 0.62);
  出(p => p.M(X(3.85), Z(4.05)).C(X(2.6), Z(5.2), X(1.3), Z(6.0), X(0.8), Z(7.0)).L(X(0.78), Z(12.0)), 0.6);
  出(p => p.M(X(4.05), Z(4.05)).C(X(5.3), Z(5.2), X(6.6), Z(6.0), X(7.1), Z(7.0)).L(X(7.08), Z(9.0)), 0.6);
  線(p => p.M(X(3.95), Z(4.5)).L(X(3.93), Z(6.4)), 0.55);
  // 冠腳彎合（圓框底）
  出(p => p.M(X(0.78), Z(12.0)).C(X(0.9), Z(12.5), X(1.8), Z(12.75), X(3.2), Z(12.7)), 0.58);
  出(p => p.M(X(7.08), Z(9.0)).C(X(7.0), Z(9.7), X(6.6), Z(10.1), X(6.1), Z(10.2)), 0.58);
  線(p => p.M(X(4.05), Z(5.3)).L(X(5.1), Z(5.26)), 0.5);
  線(p => p.M(X(2.5), Z(6.5)).L(X(5.6), Z(6.44)), 0.55);
  // 敕曲核心
  線(p => p.M(X(1.7), Z(9.3)).L(X(4.6), Z(9.24)), 0.55);
  線(p => p.M(X(2.95), Z(8.6)).L(X(2.93), Z(11.2)), 0.55);
  出(p => p.M(X(2.95), Z(10.5)).L(X(2.2), Z(11.3)), 0.45);
  出(p => p.M(X(3.0), Z(10.5)).L(X(3.7), Z(11.2)), 0.45);
  線(p => p.M(X(4.6), Z(8.8)).C(X(5.9), Z(8.7), X(6.3), Z(9.5), X(5.3), Z(9.8)).C(X(4.4), Z(10.1), X(4.5), Z(10.8), X(5.5), Z(10.9)).C(X(6.1), Z(10.96), X(6.3), Z(11.3), X(6.2), Z(11.6)), 0.55);
  // 大迴摺三重
  線(p => p.M(X(6.6), Z(12.6)).C(X(1.2), Z(12.7), X(0.9), Z(13.6), X(2.0), Z(13.8)).C(X(4.5), Z(14.2), X(6.5), Z(14.0), X(6.6), Z(15.0)).C(X(6.7), Z(16.0), X(4.0), Z(15.8), X(2.2), Z(15.9)).C(X(1.0), Z(16.0), X(1.0), Z(16.9), X(2.2), Z(17.0)).L(X(5.9), Z(16.9)), 0.6);
  // 底密摺二重承長垂
  線(p => p.M(X(5.9), Z(17.0)).C(X(6.9), Z(17.2), X(6.9), Z(18.4), X(5.8), Z(18.5)).L(X(2.4), Z(18.6)).C(X(1.3), Z(18.7), X(1.3), Z(19.7), X(2.4), Z(19.8)).L(X(5.4), Z(19.7)).C(X(6.4), Z(19.75), X(6.4), Z(20.7), X(5.4), Z(20.8)).L(X(2.6), Z(20.9)).C(X(1.5), Z(21.0), X(1.5), Z(22.0), X(2.7), Z(22.05)).L(X(6.2), Z(21.95)).L(X(6.18), Z(25.0)), 0.6);
  // 末由田框密摺尾
  線(p => p.M(X(6.18), Z(25.0)).C(X(6.16), Z(26.0), X(5.0), Z(26.1), X(3.4), Z(26.1)).C(X(1.6), Z(26.1), X(1.2), Z(26.9), X(2.4), Z(27.0)).L(X(4.9), Z(26.9)).C(X(5.9), Z(26.95), X(5.9), Z(27.9), X(4.9), Z(28.0)).L(X(2.2), Z(28.1)).C(X(1.1), Z(28.2), X(1.1), Z(29.2), X(2.3), Z(29.25)).L(X(5.2), Z(29.15)), 0.6);
  線(p => p.M(X(2.2), Z(30.2)).L(X(2.2), Z(33.0)).L(X(5.4), Z(32.94)).L(X(5.4), Z(30.14)).L(X(2.15), Z(30.2)), 0.55);
  線(p => p.M(X(3.8), Z(30.25)).L(X(3.78), Z(32.95)), 0.5);
  線(p => p.M(X(2.3), Z(31.6)).L(X(5.3), Z(31.54)), 0.5);
  出(p => p.M(X(3.8), Z(33.0)).L(X(3.78), Z(35.4)), 0.55);
};
